/**
 * head -- output the first part of files.
 *
 * === What This Program Does ===
 *
 * This is a reimplementation of the GNU `head` utility in TypeScript. It
 * prints the first N lines (or bytes) of each file to standard output.
 * By default, N is 10.
 *
 * === How head Works ===
 *
 * head is the complement of tail. Together they let you peek at the
 * beginning or end of a file without loading the whole thing:
 *
 *     head file.txt          =>   first 10 lines
 *     head -n 5 file.txt     =>   first 5 lines
 *     head -c 100 file.txt   =>   first 100 bytes
 *
 * === Headers ===
 *
 * When processing multiple files, head prints a header before each file:
 *
 *     ==> file1.txt <==
 *     (first 10 lines of file1)
 *
 *     ==> file2.txt <==
 *     (first 10 lines of file2)
 *
 * The -q flag suppresses headers; -v forces them even for single files.
 *
 * === Zero-Terminated Mode (-z) ===
 *
 * By default, head uses newline (\n) as the line delimiter. With -z,
 * it uses NUL (\0) instead. This is useful for processing file lists
 * generated by `find -print0`.
 *
 * @module head
 */

import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";

// ---------------------------------------------------------------------------
// Import CLI Builder.
// ---------------------------------------------------------------------------

import { Parser } from "@coding-adventures/cli-builder";

// ---------------------------------------------------------------------------
// Locate the JSON spec file.
// ---------------------------------------------------------------------------

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const SPEC_FILE = path.resolve(__dirname, "..", "head.json");

// ---------------------------------------------------------------------------
// Business Logic: Extract the first N lines or bytes.
// ---------------------------------------------------------------------------

/**
 * Extract the first `count` lines from content.
 *
 * We split on the delimiter (newline by default, NUL with -z), take the
 * first `count` elements, and rejoin them. The trailing delimiter is
 * included in the output to match GNU head behavior.
 *
 * === Why split-and-rejoin? ===
 *
 * An alternative approach is to scan for delimiters character by character.
 * But split-and-rejoin is simpler and perfectly adequate for head, which
 * typically processes small prefixes of files. The entire file must be read
 * into memory anyway (Node.js readFileSync), so the split cost is marginal.
 */
function headLines(content: string, count: number, delimiter: string): string {
  const lines = content.split(delimiter);

  // If the content ends with a delimiter, split produces a trailing empty
  // string. We keep it in the array but don't count it as a line.
  const hasTrailing = content.endsWith(delimiter);
  const effectiveLines = hasTrailing ? lines.slice(0, -1) : lines;

  const selected = effectiveLines.slice(0, count);
  return selected.join(delimiter) + (selected.length > 0 ? delimiter : "");
}

/**
 * Extract the first `count` bytes from content.
 *
 * We use Buffer to handle multi-byte characters correctly. A byte count
 * of 100 means exactly 100 bytes, even if that splits a multi-byte
 * UTF-8 character.
 */
function headBytes(content: string, count: number): string {
  const buf = Buffer.from(content, "utf-8");
  return buf.subarray(0, count).toString("utf-8");
}

// ---------------------------------------------------------------------------
// Main: parse args via CLI Builder, then output first part of files.
// ---------------------------------------------------------------------------

/**
 * Entry point.
 *
 * 1. Parse arguments with CLI Builder.
 * 2. Handle --help and --version.
 * 3. Determine line/byte mode and count.
 * 4. Process each file, printing headers when appropriate.
 */
function main(): void {
  // --- Step 1: Parse arguments ---------------------------------------------

  let result;

  try {
    const parser = new Parser(SPEC_FILE, process.argv);
    result = parser.parse();
  } catch (err: unknown) {
    if (err && typeof err === "object" && "errors" in err) {
      const errors = (err as { errors: Array<{ message: string }> }).errors;
      for (const error of errors) {
        process.stderr.write(`head: ${error.message}\n`);
      }
      process.exit(1);
    }
    throw err;
  }

  // --- Step 2: Dispatch on result type -------------------------------------

  if ("text" in result) {
    process.stdout.write(result.text + "\n");
    process.exit(0);
  }

  if ("version" in result && !("flags" in result)) {
    process.stdout.write(result.version + "\n");
    process.exit(0);
  }

  // --- Step 3: Extract flags and arguments ---------------------------------

  const flags = (result as { flags: Record<string, unknown> }).flags;
  const args = (result as { arguments: Record<string, unknown> }).arguments;

  const byteMode = flags["bytes"] !== undefined && flags["bytes"] !== null;
  const lineCount = (flags["lines"] as number) ?? 10;
  const byteCount = flags["bytes"] as number | undefined;
  const quiet = !!flags["quiet"];
  const verbose = !!flags["verbose"];
  const zeroTerminated = !!flags["zero_terminated"];
  const delimiter = zeroTerminated ? "\0" : "\n";

  // Normalize file list.
  let files = args["files"] as string[] | string | undefined;
  if (!files) {
    files = ["-"];
  } else if (typeof files === "string") {
    files = [files];
  }

  // Determine whether to print headers.
  // Default: print headers only when multiple files are given.
  // -q: never print headers. -v: always print headers.
  const showHeaders = quiet ? false : verbose ? true : files.length > 1;

  // --- Step 4: Process each file -------------------------------------------

  for (let i = 0; i < files.length; i++) {
    const file = files[i];

    // Print header if appropriate.
    if (showHeaders) {
      if (i > 0) process.stdout.write("\n");
      const label = file === "-" ? "standard input" : file;
      process.stdout.write(`==> ${label} <==\n`);
    }

    // Read the file content.
    let content: string;
    if (file === "-") {
      try {
        content = fs.readFileSync(0, "utf-8");
      } catch {
        continue;
      }
    } else {
      try {
        content = fs.readFileSync(file, "utf-8");
      } catch (err: unknown) {
        const message = err instanceof Error ? err.message : String(err);
        process.stderr.write(`head: ${file}: ${message}\n`);
        process.exitCode = 1;
        continue;
      }
    }

    // Extract the requested portion.
    if (byteMode && byteCount !== undefined) {
      process.stdout.write(headBytes(content, byteCount));
    } else {
      process.stdout.write(headLines(content, lineCount, delimiter));
    }
  }
}

// ---------------------------------------------------------------------------
// Run the program.
// ---------------------------------------------------------------------------

main();
