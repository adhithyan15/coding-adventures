/**
 * Extension Scaffold Generator
 * ============================
 *
 * Why a scaffold generator?
 * -------------------------
 * Creating a new browser extension involves many files with specific
 * conventions: manifest.json, package.json, vite.config.ts, tsconfig.json,
 * vitest.config.ts, BUILD, README.md, CHANGELOG.md, popup template,
 * service worker template, browser API shim, and CSS.
 *
 * Getting any of these wrong means debugging obscure errors. The scaffold
 * generator creates all of them correctly, so you can start writing
 * extension logic immediately.
 *
 * How it works
 * ------------
 * 1. Read template files from the `templates/` directory
 * 2. Replace `{{variable}}` placeholders with user-provided values
 * 3. Write the expanded files to the target directory
 *
 * The template system is intentionally simple — no template engine, no
 * conditionals, no loops. Just string replacement. This keeps the
 * templates readable and the generator predictable.
 *
 * Template variables
 * ------------------
 * | Variable        | Example                    | Used in              |
 * |-----------------|----------------------------|----------------------|
 * | `{{name}}`      | `my-extension`             | package.json, README |
 * | `{{description}}`| `Does something cool`     | package.json, README |
 * | `{{geckoId}}`   | `my-ext@coding-adventures` | manifest.json        |
 * | `{{date}}`      | `2026-03-29`               | CHANGELOG.md         |
 */

import * as fs from "node:fs";
import * as path from "node:path";

/**
 * Options for the scaffold generator.
 */
export interface ScaffoldOptions {
  /** Extension name in kebab-case (e.g., "my-extension"). */
  name: string;

  /** Short description of what the extension does. */
  description: string;

  /**
   * Directory where the extension will be created.
   * The generator creates a subdirectory named after the extension.
   * Defaults to the current working directory.
   */
  outputDir?: string;

  /**
   * Gecko (Firefox) extension ID.
   * Defaults to `{name}@coding-adventures`.
   */
  geckoId?: string;
}

/**
 * A file to be generated by the scaffold.
 * The `path` is relative to the extension root directory.
 */
export interface GeneratedFile {
  path: string;
  content: string;
}

/**
 * Replace all `{{variable}}` placeholders in a template string.
 *
 * This is the core of the template engine. It's a simple global
 * string replacement — no parsing, no AST, no escaping. This works
 * because our templates are static files with well-known placeholders.
 *
 * @param template - Template string with `{{variable}}` placeholders
 * @param variables - Map of variable names to replacement values
 * @returns The template with all placeholders replaced
 *
 * @example
 * ```typescript
 * expandTemplate("Hello, {{name}}!", { name: "World" });
 * // → "Hello, World!"
 * ```
 */
export function expandTemplate(
  template: string,
  variables: Record<string, string>,
): string {
  let result = template;
  for (const [key, value] of Object.entries(variables)) {
    // Replace ALL occurrences of {{key}} with the value.
    // We use a regex with the global flag instead of replaceAll()
    // to avoid issues with special regex characters in the key.
    result = result.split(`{{${key}}}`).join(value);
  }
  return result;
}

/**
 * Generate all files for a new extension project.
 *
 * This function does NOT write to disk — it returns an array of
 * [path, content] pairs. The caller decides what to do with them
 * (write to disk, display to user, run tests against, etc.).
 *
 * Separating generation from I/O makes this function easy to test
 * and reuse.
 *
 * @param options - Scaffold configuration
 * @returns Array of files to create
 *
 * @example
 * ```typescript
 * const files = generateFiles({
 *   name: "my-extension",
 *   description: "Does something cool",
 * });
 *
 * for (const file of files) {
 *   console.log(`Would create: ${file.path}`);
 * }
 * ```
 */
export function generateFiles(options: ScaffoldOptions): GeneratedFile[] {
  const variables: Record<string, string> = {
    name: options.name,
    description: options.description,
    geckoId: options.geckoId ?? `${options.name}@coding-adventures`,
    date: new Date().toISOString().split("T")[0],
  };

  // Each entry: [relative path in the generated project, template content]
  const templates: Array<[string, string]> = [
    ["manifest.json", MANIFEST_TEMPLATE],
    ["package.json", PACKAGE_JSON_TEMPLATE],
    ["tsconfig.json", TSCONFIG_TEMPLATE],
    ["vite.config.ts", VITE_CONFIG_TEMPLATE],
    ["vitest.config.ts", VITEST_CONFIG_TEMPLATE],
    ["BUILD", BUILD_TEMPLATE],
    ["README.md", README_TEMPLATE],
    ["CHANGELOG.md", CHANGELOG_TEMPLATE],
    ["src/lib/browser-api.ts", BROWSER_API_TEMPLATE],
    ["src/popup/popup.html", POPUP_HTML_TEMPLATE],
    ["src/popup/popup.ts", POPUP_TS_TEMPLATE],
    ["src/popup/popup.css", POPUP_CSS_TEMPLATE],
    ["src/background/service-worker.ts", SERVICE_WORKER_TEMPLATE],
    ["tests/popup.test.ts", POPUP_TEST_TEMPLATE],
  ];

  return templates.map(([filePath, template]) => ({
    path: filePath,
    content: expandTemplate(template, variables),
  }));
}

/**
 * Create a new extension project on disk.
 *
 * This is the high-level function that generates files and writes them.
 * It creates the target directory and all subdirectories as needed.
 *
 * @param options - Scaffold configuration
 * @returns Path to the created extension directory
 * @throws {Error} If the target directory already exists
 */
export function scaffold(options: ScaffoldOptions): string {
  const outputDir = options.outputDir ?? process.cwd();
  const extensionDir = path.join(outputDir, options.name);

  // Safety check: don't overwrite existing projects
  if (fs.existsSync(extensionDir)) {
    throw new Error(
      `Directory "${extensionDir}" already exists. ` +
        "Choose a different name or delete the existing directory.",
    );
  }

  const files = generateFiles(options);

  for (const file of files) {
    const filePath = path.join(extensionDir, file.path);
    const dir = path.dirname(filePath);

    // Create parent directories (like `mkdir -p`)
    fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(filePath, file.content, "utf-8");
  }

  return extensionDir;
}

