defmodule CodingAdventures.Conduit.Application do
  @moduledoc """
  Immutable struct holding the Conduit app definition: routes, filters,
  fallback handlers, and settings.

  Construction is functional: `Application.new() |> Application.get(...)`
  returns a new struct each time. The struct is purely data — no
  processes, no resources — so it can be inspected, copied, or
  serialised freely. The Server materialises it into a Rust resource
  and starts the dispatcher when `Server.start_link/2` is called.

  ## Why a struct, not macros?

  Plug and Phoenix use compile-time macros (`get "/" do ... end`) which
  let them do route compilation at compile time. The trade-off is that
  the DSL becomes a separate language to debug — and the framework's
  state is not directly inspectable. Conduit favors `IO.inspect(app)`
  showing you exactly what's registered, mirroring the Ruby/Python/Lua/
  TypeScript ports.

  ## Handler IDs

  Each registered function is assigned a sequential `handler_id` (an
  integer). The struct keeps:

      handlers: %{1 => fn1, 2 => fn2, ...}

  The Rust side stores only the IDs — never the function refs (BEAM
  function refs are tied to a process and can't safely outlive their
  construction site). When a request fires, Rust includes the `handler_id`
  in the message; the dispatcher uses the id to look the function up.
  """

  alias __MODULE__

  defstruct routes: [],
            before_filters: [],
            after_filters: [],
            not_found_handler: nil,
            error_handler: nil,
            settings: %{},
            handlers: %{},
            next_id: 1

  @type handler_id :: pos_integer
  @type handler :: (CodingAdventures.Conduit.Request.t() -> term)

  @type route_entry :: %{
          method: String.t(),
          pattern: String.t(),
          handler_id: handler_id
        }

  @type t :: %__MODULE__{
          routes: [route_entry],
          before_filters: [handler_id],
          after_filters: [handler_id],
          not_found_handler: handler_id | nil,
          error_handler: handler_id | nil,
          settings: %{optional(String.t() | atom) => term},
          handlers: %{optional(handler_id) => handler},
          next_id: pos_integer
        }

  @doc "Construct an empty application."
  @spec new() :: t
  def new, do: %__MODULE__{}

  # ── HTTP method helpers ────────────────────────────────────────────────────
  #
  # Spelled out one per line rather than generated by a `for` comprehension —
  # `def unquote(name)(...)` only works inside a `quote do ... end` block,
  # not at module body level. Sometimes plain code is the right answer.

  @doc "Register a GET handler for `pattern`."
  @spec get(t, String.t(), handler) :: t
  def get(%Application{} = app, pattern, handler)
      when is_binary(pattern) and is_function(handler, 1),
      do: add_route(app, "GET", pattern, handler)

  @doc "Register a POST handler for `pattern`."
  @spec post(t, String.t(), handler) :: t
  def post(%Application{} = app, pattern, handler)
      when is_binary(pattern) and is_function(handler, 1),
      do: add_route(app, "POST", pattern, handler)

  @doc "Register a PUT handler for `pattern`."
  @spec put(t, String.t(), handler) :: t
  def put(%Application{} = app, pattern, handler)
      when is_binary(pattern) and is_function(handler, 1),
      do: add_route(app, "PUT", pattern, handler)

  @doc "Register a DELETE handler for `pattern`."
  @spec delete(t, String.t(), handler) :: t
  def delete(%Application{} = app, pattern, handler)
      when is_binary(pattern) and is_function(handler, 1),
      do: add_route(app, "DELETE", pattern, handler)

  @doc "Register a PATCH handler for `pattern`."
  @spec patch(t, String.t(), handler) :: t
  def patch(%Application{} = app, pattern, handler)
      when is_binary(pattern) and is_function(handler, 1),
      do: add_route(app, "PATCH", pattern, handler)

  @doc "Generic route registration when method is dynamic."
  @spec add_route(t, String.t(), String.t(), handler) :: t
  def add_route(%Application{} = app, method, pattern, handler)
      when is_binary(method) and is_binary(pattern) and is_function(handler, 1) do
    {id, app} = assign_id(app, handler)

    %{app | routes: app.routes ++ [%{method: method, pattern: pattern, handler_id: id}]}
  end

  # ── Filters ───────────────────────────────────────────────────────────────

  @doc "Register a `before` filter — runs before route dispatch on every request."
  @spec before_filter(t, handler) :: t
  def before_filter(%Application{} = app, handler) when is_function(handler, 1) do
    {id, app} = assign_id(app, handler)
    %{app | before_filters: app.before_filters ++ [id]}
  end

  @doc "Register an `after` filter — runs after route dispatch on every response."
  @spec after_filter(t, (CodingAdventures.Conduit.Request.t() -> term)) :: t
  def after_filter(%Application{} = app, handler) when is_function(handler, 1) do
    {id, app} = assign_id(app, handler)
    %{app | after_filters: app.after_filters ++ [id]}
  end

  # ── Fallback handlers ─────────────────────────────────────────────────────

  @doc "Set the not-found handler (overwrites any previous)."
  @spec not_found_handler(t, handler) :: t
  def not_found_handler(%Application{} = app, handler) when is_function(handler, 1) do
    {id, app} = assign_id(app, handler)
    %{app | not_found_handler: id}
  end

  @doc """
  Set the error handler (overwrites any previous). Receives `req` and
  the error string; should return a response tuple.
  """
  @spec error_handler(t, (CodingAdventures.Conduit.Request.t() -> term)) :: t
  def error_handler(%Application{} = app, handler) when is_function(handler, 1) do
    {id, app} = assign_id(app, handler)
    %{app | error_handler: id}
  end

  # ── Settings ──────────────────────────────────────────────────────────────

  @doc "Store a setting value (string or any term — will be `to_string`'d for Rust)."
  @spec put_setting(t, atom | String.t(), term) :: t
  def put_setting(%Application{} = app, key, value) do
    %{app | settings: Map.put(app.settings, key, value)}
  end

  @doc "Read a setting; returns `default` if absent."
  @spec get_setting(t, atom | String.t(), term) :: term
  def get_setting(%Application{} = app, key, default \\ nil) do
    Map.get(app.settings, key, default)
  end

  # ── Internal: ID assignment ───────────────────────────────────────────────

  defp assign_id(%Application{next_id: id, handlers: handlers} = app, handler) do
    {id, %{app | next_id: id + 1, handlers: Map.put(handlers, id, handler)}}
  end
end
