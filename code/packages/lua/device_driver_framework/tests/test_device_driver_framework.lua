-- ============================================================================
-- SimulatedDisk tests
-- ============================================================================

describe("SimulatedDisk", function()
  it("new creates disk with defaults", function()
    local d = DDF.SimulatedDisk.new()
    assert.are.equal(d.name, "disk0")
    assert.are.equal(d.device_type, DDF.TYPE_BLOCK)
    assert.are.equal(d.major, 3)
    assert.are.equal(d.minor, 0)
    assert.are.equal(d.block_size, 512)
    assert.are.equal(d.total_blocks, 2048)
    assert.is_false(d.initialized)
    assert.are.equal(d.open_count, 0)
  end)

  it("new with custom options", function()
    local d = DDF.SimulatedDisk.new({ name = "disk1", major = 8, minor = 16, block_size = 256, total_blocks = 4 })
    assert.are.equal(d.name, "disk1")
    assert.are.equal(d.major, 8)
    assert.are.equal(d.minor, 16)
    assert.are.equal(d.block_size, 256)
    assert.are.equal(d.total_blocks, 4)
  end)

  it("initialize sets initialized flag", function()
    local d = DDF.SimulatedDisk.new()
    local st, d2 = d:initialize()
    assert.are.equal(st, "ok")
    assert.is_true(d2.initialized)
    assert.is_false(d.initialized)  -- immutable
  end)

  it("open before initialize returns error", function()
    local d = DDF.SimulatedDisk.new()
    local st, _ = d:open()
    assert.are.equal(st, "not_initialized")
  end)

  it("open and close lifecycle", function()
    local d = DDF.SimulatedDisk.new()
    local _, d2 = d:initialize()
    local st1, d3 = d2:open()
    assert.are.equal(st1, "ok")
    assert.are.equal(d3.open_count, 1)
    local st2, d4 = d3:close()
    assert.are.equal(st2, "ok")
    assert.are.equal(d4.open_count, 0)
  end)

  it("close without open returns error", function()
    local d = DDF.SimulatedDisk.new()
    local _, d2 = d:initialize()
    local st, _ = d2:close()
    assert.are.equal(st, "not_open")
  end)

  it("write_block and read_block round-trip", function()
    local d = DDF.SimulatedDisk.new({ block_size = 8, total_blocks = 4 })
    local data = "ABCDEFGH"
    local st1, d2 = d:write_block(0, data)
    assert.are.equal(st1, "ok")
    local st2, _, result = d2:read_block(0)
    assert.are.equal(st2, "ok")
    assert.are.equal(result, "ABCDEFGH")
  end)

  it("write_block to non-zero block", function()
    local d = DDF.SimulatedDisk.new({ block_size = 4, total_blocks = 8 })
    local _, d2 = d:write_block(3, "TEST")
    local _, _, r0 = d2:read_block(0)
    local _, _, r3 = d2:read_block(3)
    assert.are.equal(r3, "TEST")
    assert.are.equal(r0, "\0\0\0\0")
  end)

  it("write_block rejects out-of-bounds block", function()
    local d = DDF.SimulatedDisk.new({ block_size = 4, total_blocks = 2 })
    local st, _ = d:write_block(2, "XXXX")
    assert.are.equal(st, "out_of_bounds")
    st, _ = d:write_block(-1, "XXXX")
    assert.are.equal(st, "out_of_bounds")
  end)

  it("write_block rejects wrong-size data", function()
    local d = DDF.SimulatedDisk.new({ block_size = 8, total_blocks = 4 })
    local st, _ = d:write_block(0, "short")
    assert.are.equal(st, "wrong_size")
  end)

  it("read_block rejects out-of-bounds", function()
    local d = DDF.SimulatedDisk.new({ block_size = 4, total_blocks = 2 })
    local st, _, _ = d:read_block(5)
    assert.are.equal(st, "out_of_bounds")
  end)

  it("immutability: write does not mutate original", function()
    local d = DDF.SimulatedDisk.new({ block_size = 4, total_blocks = 4 })
    local _, d2 = d:write_block(0, "ABCD")
    local _, _, r1 = d:read_block(0)
    local _, _, r2 = d2:read_block(0)
    assert.are.equal(r1, "\0\0\0\0")
    assert.are.equal(r2, "ABCD")
  end)

  it("ioctl get_block_size", function()
    local d = DDF.SimulatedDisk.new({ block_size = 256 })
    local st, val = d:ioctl("get_block_size")
    assert.are.equal(st, "ok")
    assert.are.equal(val, 256)
  end)

  it("ioctl get_total_blocks", function()
    local d = DDF.SimulatedDisk.new({ total_blocks = 100 })
    local st, val = d:ioctl("get_total_blocks")
    assert.are.equal(st, "ok")
    assert.are.equal(val, 100)
  end)

  it("ioctl unsupported command", function()
    local d = DDF.SimulatedDisk.new()
    local st, _ = d:ioctl("format", nil)
    assert.are.equal(st, "unsupported")
  end)
end)

