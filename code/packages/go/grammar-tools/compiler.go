package grammartools

// compiler.go — Compile TokenGrammar and ParserGrammar into Go source code.
//
// The grammar-tools library parses .tokens and .grammar files into in-memory
// data structures. This file adds the *compile* step: given a parsed grammar
// object, generate Go source code that embeds the grammar as native Go data
// structures.
//
// Why compile grammars?
// ---------------------
//
// The default workflow reads .tokens and .grammar files at runtime. This has
// three costs that compilation eliminates:
//
//  1. File I/O at startup — every process must find and open the files.
//     Packages walk up the directory tree to find code/grammars/, which
//     couples them to the repo layout.
//
//  2. Parse overhead at startup — the grammar is re-parsed every run.
//
//  3. Deployment coupling — .tokens and .grammar files must ship with the
//     program alongside the compiled binary.
//
// The generated Go file directly instantiates TokenGrammar or ParserGrammar
// with literal data and can be imported like any other Go package.
//
// Generated output shape (json.tokens → json_tokens.go):
//
//	// Code generated by grammar-tools. DO NOT EDIT.
//	// Source: json.tokens
//	package generated
//
//	import gt "github.com/adhithyan15/coding-adventures/code/packages/go/grammar-tools"
//
//	var TokenGrammarData = &gt.TokenGrammar{
//	    Version:         1,
//	    CaseSensitive:   true,
//	    Definitions:     []gt.TokenDefinition{...},
//	    ...
//	}
//
// Round-trip fidelity
// -------------------
//
// The generated code preserves all fields of the grammar object. Compiling
// a grammar and then loading the generated file gives back an equivalent
// object — same tokens, same order, same patterns, same aliases.
//
// String quoting strategy
// -----------------------
//
// Token patterns often contain backslashes, quotes, and control characters.
// We use Go raw string literals (`...`) when the pattern does not contain a
// backtick, since raw strings need no escaping. When a backtick appears in
// the pattern we fall back to a double-quoted string with standard Go escapes.
// This keeps the generated code readable for the common case.

import (
	"fmt"
	"strings"
)

// goStringLit returns a Go string literal for s.
//
// If s contains no backtick, we use a raw string literal ` ... ` which
// requires zero escaping — perfect for regex patterns that contain
// backslashes and quotes.
//
// If s contains a backtick we fall back to a double-quoted string with the
// standard Go escape sequences applied.
func goStringLit(s string) string {
	if !strings.Contains(s, "`") {
		return "`" + s + "`"
	}
	// Fall back: double-quoted with escape processing.
	return fmt.Sprintf("%q", s)
}

// goOptStringLit returns the Go literal for a possibly-empty string.
//
// In the TokenGrammar model, optional string fields use the zero value ""
// to mean "not set". We preserve the exact value without special-casing nil.
func goOptStringLit(s string) string {
	return goStringLit(s)
}

// goStringSliceLit renders a []string literal.
func goStringSliceLit(ss []string) string {
	if len(ss) == 0 {
		return "[]string{}"
	}
	var sb strings.Builder
	sb.WriteString("[]string{")
	for i, s := range ss {
		if i > 0 {
			sb.WriteString(", ")
		}
		sb.WriteString(goStringLit(s))
	}
	sb.WriteString("}")
	return sb.String()
}

// goTokenDefLit renders one TokenDefinition as a struct literal.
func goTokenDefLit(d TokenDefinition, pkg string) string {
	alias := goStringLit(d.Alias)
	return fmt.Sprintf(
		"%s.TokenDefinition{Name: %s, Pattern: %s, IsRegex: %v, LineNumber: %d, Alias: %s}",
		pkg,
		goStringLit(d.Name),
		goStringLit(d.Pattern),
		d.IsRegex,
		d.LineNumber,
		alias,
	)
}

// goTokenDefSliceLit renders a []TokenDefinition as a slice literal.
func goTokenDefSliceLit(defs []TokenDefinition, pkg, indent string) string {
	if len(defs) == 0 {
		return fmt.Sprintf("[]%s.TokenDefinition{}", pkg)
	}
	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("[]%s.TokenDefinition{\n", pkg))
	for _, d := range defs {
		sb.WriteString(indent + "\t\t" + goTokenDefLit(d, pkg) + ",\n")
	}
	sb.WriteString(indent + "\t}")
	return sb.String()
}

// CompileTokenGrammar generates Go source code that embeds grammar as a
// TokenGrammar value. The caller chooses the target pkg name for the
// generated file and the import alias for the grammar-tools package.
//
// Parameters:
//   - grammar: The parsed TokenGrammar to embed.
//   - sourceFile: The original .tokens filename, used in the header comment.
//     Pass "" to omit the source reference.
//   - pkgName: The Go package name for the generated file (e.g. "generated").
//
// Returns a complete Go source file as a string. Write it to a .go file.
func CompileTokenGrammar(grammar *TokenGrammar, sourceFile, pkgName string) string {
	result, _ := StartNew[string]("grammar-tools.CompileTokenGrammar", "",
		func(op *Operation[string], rf *ResultFactory[string]) *OperationResult[string] {
			op.AddProperty("sourceFile", sourceFile)
			op.AddProperty("pkgName", pkgName)
			return rf.Generate(true, false, compileTokenGrammarImpl(grammar, sourceFile, pkgName))
		}).GetResult()
	return result
}

func compileTokenGrammarImpl(grammar *TokenGrammar, sourceFile, pkgName string) string {
	const importPath = "github.com/adhithyan15/coding-adventures/code/packages/go/grammar-tools"
	const alias = "gt"
	indent := "\t"

	// Strip newlines so a crafted filename cannot break out of the comment line
	// and inject arbitrary code into the generated file.
	sourceFile = strings.ReplaceAll(sourceFile, "\n", "_")
	sourceFile = strings.ReplaceAll(sourceFile, "\r", "_")

	sourceLine := ""
	if sourceFile != "" {
		sourceLine = fmt.Sprintf("// Source: %s\n", sourceFile)
	}

	// Build Groups map literal.
	groupsSrc := "map[string]*gt.PatternGroup{}"
	if len(grammar.Groups) > 0 {
		var sb strings.Builder
		sb.WriteString("map[string]*gt.PatternGroup{\n")
		for name, group := range grammar.Groups {
			defsLit := goTokenDefSliceLit(group.Definitions, alias, indent+"\t\t")
			sb.WriteString(fmt.Sprintf(
				"%s\t\t%s: {Name: %s, Definitions: %s},\n",
				indent, goStringLit(name), goStringLit(group.Name), defsLit,
			))
		}
		sb.WriteString(indent + "\t}")
		groupsSrc = sb.String()
	}

	defsLit := goTokenDefSliceLit(grammar.Definitions, alias, indent)
	skipLit := goTokenDefSliceLit(grammar.SkipDefinitions, alias, indent)
	errLit := goTokenDefSliceLit(grammar.ErrorDefinitions, alias, indent)

	return fmt.Sprintf(
		`// Code generated by grammar-tools. DO NOT EDIT.
%s// Regenerate with: grammar-tools compile-tokens %s
//
// This file embeds a TokenGrammar as native Go data structures.
// Downstream packages use TokenGrammarData directly instead of reading
// and parsing the .tokens file at runtime.

package %s

import %s %q

// TokenGrammarData is the embedded token grammar.
var TokenGrammarData = &%s.TokenGrammar{
	Version:          %d,
	CaseInsensitive:  %v,
	CaseSensitive:    %v,
	Definitions:      %s,
	Keywords:         %s,
	Mode:             %s,
	EscapeMode:       %s,
	SkipDefinitions:  %s,
	ReservedKeywords: %s,
	ErrorDefinitions: %s,
	Groups:           %s,
	LayoutKeywords:   %s,
}
`,
		sourceLine,
		sourceFile,
		pkgName,
		alias, importPath,
		alias,
		grammar.Version,
		grammar.CaseInsensitive,
		grammar.CaseSensitive,
		defsLit,
		goStringSliceLit(grammar.Keywords),
		goStringLit(grammar.Mode),
		goStringLit(grammar.EscapeMode),
		skipLit,
		goStringSliceLit(grammar.ReservedKeywords),
		errLit,
		groupsSrc,
		goStringSliceLit(grammar.LayoutKeywords),
	)
}

// goElementLit renders a GrammarElement as a Go expression.
//
// Because GrammarElement is an interface (not a union type), we use a
// type switch and recursively render children. The result is a nested Go
// struct literal that can be assigned to a GrammarElement variable.
func goElementLit(element GrammarElement, pkg, indent string) string {
	next := indent + "\t"
	switch e := element.(type) {
	case RuleReference:
		return fmt.Sprintf(
			"%s.RuleReference{Name: %s, IsToken: %v}",
			pkg, goStringLit(e.Name), e.IsToken,
		)
	case Literal:
		return fmt.Sprintf("%s.Literal{Value: %s}", pkg, goStringLit(e.Value))

	case Sequence:
		var sb strings.Builder
		sb.WriteString(fmt.Sprintf("%s.Sequence{Elements: []%s.GrammarElement{\n", pkg, pkg))
		for _, child := range e.Elements {
			sb.WriteString(next + goElementLit(child, pkg, next) + ",\n")
		}
		sb.WriteString(indent + "}}")
		return sb.String()

	case Alternation:
		var sb strings.Builder
		sb.WriteString(fmt.Sprintf("%s.Alternation{Choices: []%s.GrammarElement{\n", pkg, pkg))
		for _, child := range e.Choices {
			sb.WriteString(next + goElementLit(child, pkg, next) + ",\n")
		}
		sb.WriteString(indent + "}}")
		return sb.String()

	case Repetition:
		child := goElementLit(e.Element, pkg, next)
		return fmt.Sprintf("%s.Repetition{Element: %s}", pkg, child)

	case Optional:
		child := goElementLit(e.Element, pkg, next)
		return fmt.Sprintf("%s.Optional{Element: %s}", pkg, child)

	case Group:
		child := goElementLit(e.Element, pkg, next)
		return fmt.Sprintf("%s.Group{Element: %s}", pkg, child)

	case PositiveLookahead:
		child := goElementLit(e.Element, pkg, next)
		return fmt.Sprintf("%s.PositiveLookahead{Element: %s}", pkg, child)

	case NegativeLookahead:
		child := goElementLit(e.Element, pkg, next)
		return fmt.Sprintf("%s.NegativeLookahead{Element: %s}", pkg, child)

	case OneOrMoreRepetition:
		child := goElementLit(e.Element, pkg, next)
		return fmt.Sprintf("%s.OneOrMoreRepetition{Element: %s}", pkg, child)

	case SeparatedRepetition:
		elem := goElementLit(e.Element, pkg, next)
		sep := goElementLit(e.Separator, pkg, next)
		return fmt.Sprintf("%s.SeparatedRepetition{Element: %s, Separator: %s, AtLeastOne: %v}",
			pkg, elem, sep, e.AtLeastOne)

	default:
		return fmt.Sprintf("/* unknown element: %T */", element)
	}
}

// CompileParserGrammar generates Go source code that embeds grammar as a
// ParserGrammar value.
//
// Parameters:
//   - grammar: The parsed ParserGrammar to embed.
//   - sourceFile: The original .grammar filename, used in the header comment.
//   - pkgName: The Go package name for the generated file.
//
// Returns a complete Go source file as a string.
func CompileParserGrammar(grammar *ParserGrammar, sourceFile, pkgName string) string {
	result, _ := StartNew[string]("grammar-tools.CompileParserGrammar", "",
		func(op *Operation[string], rf *ResultFactory[string]) *OperationResult[string] {
			op.AddProperty("sourceFile", sourceFile)
			op.AddProperty("pkgName", pkgName)
			return rf.Generate(true, false, compileParserGrammarImpl(grammar, sourceFile, pkgName))
		}).GetResult()
	return result
}

func compileParserGrammarImpl(grammar *ParserGrammar, sourceFile, pkgName string) string {
	const importPath = "github.com/adhithyan15/coding-adventures/code/packages/go/grammar-tools"
	const alias = "gt"
	indent := "\t"

	// Strip newlines so a crafted filename cannot break out of the comment line.
	sourceFile = strings.ReplaceAll(sourceFile, "\n", "_")
	sourceFile = strings.ReplaceAll(sourceFile, "\r", "_")

	sourceLine := ""
	if sourceFile != "" {
		sourceLine = fmt.Sprintf("// Source: %s\n", sourceFile)
	}

	var rulesSrc strings.Builder
	if len(grammar.Rules) == 0 {
		rulesSrc.WriteString("[]gt.GrammarRule{}")
	} else {
		rulesSrc.WriteString("[]gt.GrammarRule{\n")
		for _, rule := range grammar.Rules {
			bodySrc := goElementLit(rule.Body, alias, indent+"\t\t\t")
			rulesSrc.WriteString(fmt.Sprintf(
				"%s\t{Name: %s, Body: %s, LineNumber: %d},\n",
				indent, goStringLit(rule.Name), bodySrc, rule.LineNumber,
			))
		}
		rulesSrc.WriteString(indent + "}")
	}

	return fmt.Sprintf(
		`// Code generated by grammar-tools. DO NOT EDIT.
%s// Regenerate with: grammar-tools compile-grammar %s
//
// This file embeds a ParserGrammar as native Go data structures.
// Downstream packages use ParserGrammarData directly instead of reading
// and parsing the .grammar file at runtime.

package %s

import %s %q

// ParserGrammarData is the embedded parser grammar.
var ParserGrammarData = &%s.ParserGrammar{
	Version: %d,
	Rules:   %s,
}
`,
		sourceLine,
		sourceFile,
		pkgName,
		alias, importPath,
		alias,
		grammar.Version,
		rulesSrc.String(),
	)
}
