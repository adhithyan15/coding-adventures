package virtualmachine

import "testing"

func TestVirtualMachine(t *testing.T) {
	vm := NewVirtualMachine()
	// x = 1 + 2; print x
	code := AssembleCode(
		[]Instruction{
			{Opcode: OpLoadConst, Operand: 0},
			{Opcode: OpLoadConst, Operand: 1},
			{Opcode: OpAdd},
			{Opcode: OpStoreName, Operand: 0},
			{Opcode: OpLoadName, Operand: 0},
			{Opcode: OpPrint},
			{Opcode: OpHalt},
		},
		[]interface{}{1, 2},
		[]string{"x"},
	)

	traces := vm.Execute(code)
	if len(traces) != 7 {
		t.Fatalf("Expected 7 traces, got %d", len(traces))
	}

	if len(vm.Output) != 1 || vm.Output[0] != "3" {
		t.Errorf("Print output expected '3', got %v", vm.Output)
	}
	
	if vm.Variables["x"] != 3 {
		t.Errorf("Variable x should equal 3")
	}
}

func TestFunctionCalls(t *testing.T) {
	vm := NewVirtualMachine()
	subFunc := AssembleCode(
		[]Instruction{
			{Opcode: OpLoadConst, Operand: 0}, // Push 10
			{Opcode: OpStoreName, Operand: 0}, // Store in "y"
			{Opcode: OpReturn},
		},
		[]interface{}{10},
		[]string{"y"},
	)
	
	vm.Variables["my_func"] = subFunc
	
	code := AssembleCode(
		[]Instruction{
			{Opcode: OpLoadConst, Operand: 0}, // Push 100
			{Opcode: OpStoreName, Operand: 1}, // Store in "z" (before call)
			{Opcode: OpCall, Operand: 0}, // call "my_func"
			{Opcode: OpLoadName, Operand: 2}, // load "y"
			{Opcode: OpPrint}, 
			{Opcode: OpHalt},
		},
		[]interface{}{100},
		[]string{"my_func", "z", "y"},
	)
	
	vm.Execute(code)
	if len(vm.Output) != 1 || vm.Output[0] != "10" {
		t.Errorf("Should print '10' generated by function side-effect.")
	}
}

func TestJump(t *testing.T) {
	vm := NewVirtualMachine()
	code := AssembleCode(
		[]Instruction{
			{Opcode: OpLoadConst, Operand: 0}, // PC0: Load 0 (false)
			{Opcode: OpJumpIfFalse, Operand: 4}, // PC1: Jump to PC4
			{Opcode: OpLoadConst, Operand: 1}, // PC2: Load "should skip"
			{Opcode: OpPrint},                 // PC3: Print
			{Opcode: OpLoadConst, Operand: 2}, // PC4: Target, Load "success"
			{Opcode: OpPrint},                 // PC5: Print
			{Opcode: OpHalt},
		},
		[]interface{}{0, "should skip", "success"},
		nil,
	)
	
	vm.Execute(code)
	if len(vm.Output) != 1 || vm.Output[0] != "success" {
		t.Errorf("Expected only 'success', got %v", vm.Output)
	}
}
