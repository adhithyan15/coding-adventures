final List<OpcodeInfo> wasmOpcodeTable = List<OpcodeInfo>.unmodifiable(
  _rawOpcodes.map(
    (entry) => OpcodeInfo(
      opcode: entry[0] as int,
      name: entry[1] as String,
      category: entry[2] as String,
      immediates: List<String>.unmodifiable(entry[3] as List<String>),
      stackPop: entry[4] as int,
      stackPush: entry[5] as int,
    ),
  ),
);

final Map<int, OpcodeInfo> wasmOpcodesByCode = Map<int, OpcodeInfo>.unmodifiable(
  {
    for (final opcode in wasmOpcodeTable) opcode.opcode: opcode,
  },
);

final Map<String, OpcodeInfo> wasmOpcodesByName = Map<String, OpcodeInfo>.unmodifiable(
  {
    for (final opcode in wasmOpcodeTable) opcode.name: opcode,
  },
);

OpcodeInfo? getOpcode(int byte) => wasmOpcodesByCode[byte];

OpcodeInfo? getOpcodeByName(String name) => wasmOpcodesByName[name];

OpcodeInfo? opcodeByCode(int code) => wasmOpcodesByCode[code];
