/**
 * Build the extension manifest.  This is the most important output:
 * VS Code reads `contributes.*` to know which languages, grammars,
 * debuggers, and settings the extension provides.
 *
 * The shape we emit is intentionally close to what rust-analyzer and
 * Pylance ship, so the resulting extensions look familiar to anyone
 * who has poked at those.
 */
export function buildPackageJson(opts: GeneratorOptions): string {
  const id = opts.languageId;
  const hasLsp = opts.lspBinary !== "";
  const hasDap = opts.dapBinary !== "";

  const activationEvents: string[] = [`onLanguage:${id}`];
  if (hasDap) activationEvents.push("onDebug");

  const categories: string[] = ["Programming Languages"];
  if (hasDap) categories.push("Debuggers");

  // contributes.languages — always present.  Tells VS Code which file
  // extensions trigger the language id.
  const languages = [
    {
      id,
      aliases: [opts.languageName],
      extensions: opts.fileExtensions,
      configuration: "./language-configuration.json",
    },
  ];

  // contributes.grammars — only when we're shipping a TextMate grammar.
  // Without this VS Code falls back to plain text rendering.
  const grammars = opts.keywords.length > 0
    ? [
        {
          language: id,
          scopeName: `source.${id}`,
          path: `./syntaxes/${id}.tmLanguage.json`,
        },
      ]
    : undefined;

  // contributes.debuggers — only when we have a DAP adapter.  The
  // shape is dictated by VS Code: type/label/languages + the launch
  // configuration schema users put in .vscode/launch.json.
  const debuggers = hasDap
    ? [
        {
          type: id,
          label: opts.languageName,
          languages: [id],
          configurationAttributes: {
            launch: {
              required: ["program"],
              properties: {
                program: {
                  type: "string",
                  description: `Path to the ${opts.languageName} source file to debug.`,
                },
                stopOnEntry: {
                  type: "boolean",
                  description: "Stop on entry to the program.",
                  default: false,
                },
              },
            },
          },
          initialConfigurations: [
            {
              type: id,
              request: "launch",
              name: `Launch ${opts.languageName} program`,
              program: "${file}",
              stopOnEntry: false,
            },
          ],
        },
      ]
    : undefined;

  // contributes.configuration — settings the user can override in
  // settings.json.  We surface the binary paths so users can install
  // the LSP/DAP server somewhere off PATH and still use the extension.
  const configurationProperties: Record<string, unknown> = {};
  if (hasLsp) {
    configurationProperties[`${id}.serverPath`] = {
      type: "string",
      default: opts.lspBinary,
      description: `Path to the ${opts.languageName} language server binary.`,
    };
    configurationProperties[`${id}.trace.server`] = {
      type: "string",
      enum: ["off", "messages", "verbose"],
      default: "off",
      description: "Trace LSP messages between VS Code and the language server.",
    };
  }
  if (hasDap) {
    configurationProperties[`${id}.adapterPath`] = {
      type: "string",
      default: opts.dapBinary,
      description: `Path to the ${opts.languageName} debug adapter binary.`,
    };
  }

  const contributes: Record<string, unknown> = {
    languages,
  };
  if (grammars) contributes.grammars = grammars;
  if (debuggers) contributes.debuggers = debuggers;
  // contributes.breakpoints — opt the language into VS Code's
  // breakpoint UI.  Without this, the gutter doesn't show breakpoint
  // dots when the user clicks, even if a debugger is contributed —
  // VS Code only allows breakpoints in languages that explicitly
  // declare support here.  Required whenever a DAP adapter is wired.
  if (hasDap) {
    contributes.breakpoints = [{ language: id }];
  }
  contributes.configuration = {
    title: opts.languageName,
    properties: configurationProperties,
  };

  // dependencies — vscode-languageclient is the only runtime dep we need,
  // and only when LSP is wired.  DAP uses VS Code's built-in API, no
  // runtime dependency.
  const dependencies: Record<string, string> | undefined = hasLsp
    ? { "vscode-languageclient": "^9.0.1" }
    : undefined;

  const devDependencies: Record<string, string> = {
    "@types/node": "^22.0.0",
    "@types/vscode": "^1.82.0",
    typescript: "^5.0.0",
    vitest: "^3.0.0",
  };

  const description =
    opts.description !== "" ? opts.description : defaultDescription(opts);

  // Construct the object with keys in a deterministic order.  Don't
  // mutate it after this point — JSON.stringify preserves insertion
  // order for object keys.
  // VS Code requires a non-empty `publisher` field on every extension —
  // both the marketplace and the local-install path
  // (~/.vscode/extensions/<publisher>.<name>-<version>/).  Without it,
  // local installs are silently rejected.  Default to a per-extension
  // namespace derived from the language id; users can override at
  // package time with `vsce package --publisher <name>` or by editing
  // package.json before publishing.
  const publisher = `coding-adventures-${id}`;

  const pkg: Record<string, unknown> = {
    name: `${id}-vscode`,
    displayName: opts.languageName,
    publisher,
    description,
    version: opts.extensionVersion,
    main: "./out/extension.js",
    engines: { vscode: "^1.82.0" },
    activationEvents,
    categories,
    contributes,
    scripts: {
      build: "tsc -p .",
      test: "vitest run",
    },
  };
  if (dependencies) pkg.dependencies = dependencies;
  pkg.devDependencies = devDependencies;

  return stableJson(pkg);
}

