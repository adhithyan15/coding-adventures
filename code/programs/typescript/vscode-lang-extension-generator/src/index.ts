/**
 * vscode-lang-extension-generator -- emit a fully-wired VS Code extension
 * package for any language built on the LANG-VM pipeline.
 *
 * === Why this tool exists ===
 *
 * Every language with a `*-lsp-server` (per LS02) and/or `*-dap` (per
 * LS03) binary needs a thin VS Code extension to expose those servers
 * to the editor.  The extension itself is mostly boilerplate:
 *
 *   - `package.json` declaring `contributes.languages`,
 *     `contributes.grammars`, `contributes.debuggers`, and
 *     `contributes.configuration`
 *   - `extension.ts` activation function that starts the LanguageClient
 *     and/or registers the DebugAdapterDescriptorFactory
 *
 * Hand-writing this scaffold per language has the same problem the
 * monorepo's other generators address: it's mechanical, repetitive,
 * and the failure modes are subtle (missing `activationEvents`,
 * malformed `contributes.debuggers`, wrong `main` entry).
 *
 * The generator follows the standard VS Code extension pattern used by
 * rust-analyzer, Pylance, and the TypeScript extension itself: one
 * extension wraps both the LSP and DAP capabilities, the user installs
 * one thing, and binaries are looked up via VS Code settings with PATH
 * fallback.
 *
 * Spec: `code/specs/LS04-vscode-extension-generator.md`.
 *
 * @module vscode-lang-extension-generator
 */

