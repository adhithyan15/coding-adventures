/**
 * Minimum-viable language config: comment markers (if provided),
 * standard bracket pairs, and matching auto-close behaviour.
 *
 * VS Code uses this file for indentation, comment toggling
 * (Cmd-/), and bracket auto-pairing.  Extending it is a future
 * concern — most languages need only what we emit here.
 */
export function buildLanguageConfiguration(opts: GeneratorOptions): string {
  const comments: Record<string, unknown> = {};
  if (opts.lineComment) comments.lineComment = opts.lineComment;
  if (opts.blockCommentStart && opts.blockCommentEnd) {
    comments.blockComment = [opts.blockCommentStart, opts.blockCommentEnd];
  }

  const config: Record<string, unknown> = {};
  if (Object.keys(comments).length > 0) config.comments = comments;
  config.brackets = [
    ["(", ")"],
    ["[", "]"],
    ["{", "}"],
  ];
  config.autoClosingPairs = [
    ["(", ")"],
    ["[", "]"],
    ["{", "}"],
    ['"', '"'],
  ];
  config.surroundingPairs = [
    ["(", ")"],
    ["[", "]"],
    ["{", "}"],
    ['"', '"'],
  ];

  return stableJson(config);
}

// =========================================================================
// TextMate grammar (only if keywords provided)
// =========================================================================

/**
 * Emit a *minimal* TextMate grammar derived from the keywords list.
 * This is intentionally limited — covering keywords, comments, and
 * strings, no semantic concepts.  Languages that want better
 * highlighting should hand-author or generate a richer grammar in a
 * separate package.
 *
 * Returns null when no keywords are provided.  The package.json
 * grammar contribution is also omitted in that case, keeping the
 * extension's contributions consistent.
 */
export function buildTextMateGrammar(opts: GeneratorOptions): string | null {
  if (opts.keywords.length === 0) return null;

  const id = opts.languageId;
  const name = opts.languageName;

  const patterns: Record<string, unknown>[] = [];

  // Comment patterns derived from the language config.  We only emit
  // a comment pattern if the user provided a line-comment marker;
  // block comments need begin/end pairs which are more involved.
  if (opts.lineComment) {
    patterns.push({
      name: `comment.line.${id}`,
      match: `${escapeRegex(opts.lineComment)}.*$`,
    });
  }

  // String literals — common-denominator double-quoted strings.
  patterns.push({
    name: `string.quoted.double.${id}`,
    begin: '"',
    end: '"',
    patterns: [
      { name: `constant.character.escape.${id}`, match: "\\\\." },
    ],
  });

  // Numbers — basic integer and float forms.
  patterns.push({
    name: `constant.numeric.${id}`,
    match: "\\b-?\\d+(\\.\\d+)?\\b",
  });

  // Keywords — the regex alternation must be wrapped in word boundaries
  // so we don't match keyword fragments inside identifiers.
  const escapedKeywords = opts.keywords.map(escapeRegex).join("|");
  patterns.push({
    name: `keyword.control.${id}`,
    match: `\\b(${escapedKeywords})\\b`,
  });

  const grammar: Record<string, unknown> = {
    $schema:
      "https://raw.githubusercontent.com/martinring/tmlanguage/master/tmlanguage.json",
    name,
    scopeName: `source.${id}`,
    patterns,
  };
  return stableJson(grammar);
}

