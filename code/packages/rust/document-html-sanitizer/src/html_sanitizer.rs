//! Regex-based HTML string sanitizer.
//!
//! Operates on an opaque HTML string — it does not parse a DOM. This makes
//! the sanitizer portable across all target languages (Go, Python, Ruby, Rust,
//! Elixir, Lua) where a native DOM parser is not available.
//!
//! # Algorithm overview
//!
//! The sanitizer applies transformations in this order:
//!
//! ```text
//! 1. Drop HTML comments         (<!-- … -->)
//! 2. Drop dangerous elements    (<script>…</script>, including content)
//! 3. Strip on* event handlers   (onclick="…", onload="…", etc.)
//! 4. Strip extra attributes     (srcdoc, formaction, and policy.drop_attributes)
//! 5. Sanitize href/src URLs     (replace unsafe schemes with "")
//! 6. Sanitize style attributes  (remove expression() and url() CSS injection)
//! ```
//!
//! # Why order matters
//!
//! Steps are ordered from most dangerous to least:
//! - Comments first — they can hide script tags from subsequent steps
//! - Elements second — removes entire attack surfaces including their attributes
//! - Attributes third — catches inline JS on surviving elements
//!
//! # Limitations
//!
//! Regex-based HTML sanitization is inherently imperfect. It cannot handle:
//! - Malformed or maliciously crafted HTML that exploits parser quirks
//! - Nested elements of the same tag name (the regex uses a simple pattern)
//! - HTML entities that decode to angle brackets (`&lt;script&gt;`)
//!
//! For high-security contexts where the HTML comes from untrusted user input
//! AND passes through multiple rendering engines (email clients, browsers),
//! consider using a DOM-based sanitizer. The regex approach is suitable for
//! sanitizing HTML that was generated by a known, trusted renderer (e.g.
//! output from `document-ast-to-html`) as a secondary defence-in-depth layer.

use regex::Regex;
use std::sync::OnceLock;

use crate::policy::HtmlSanitizationPolicy;
use crate::url_utils::is_scheme_allowed;

// ─── Pre-compiled regex patterns ─────────────────────────────────────────────
//
// Regex compilation is expensive (~microseconds). We compile each pattern once
// using `OnceLock` and reuse across calls.
//
// Pattern notes:
//
// - `(?i)` — case-insensitive flag (handles `<SCRIPT>`, `<Script>`, etc.)
// - `(?s)` — dot-matches-newline (handles multi-line elements)
// - `[\s\S]*?` — non-greedy match for content between tags (safe for nesting)

/// Match HTML comments: `<!-- anything -->`.
///
/// The `(?s)` flag allows `.` to match newlines, catching multi-line comments.
/// This also catches IE conditional comments: `<!--[if IE]>…<![endif]-->`.
fn re_comment() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| Regex::new(r"(?s)<!--.*?-->").unwrap())
}

/// Match a complete element (open tag + content + close tag) for a given tag name.
///
/// Called dynamically with each tag in `drop_elements`. The element content
/// is captured greedily but with a non-greedy inner match, which handles
/// adjacent (non-nested) elements correctly. Deep nesting of the same tag
/// (e.g. `<div><div>`) is NOT handled — a known limitation of regex-based
/// sanitization.
fn re_element(tag: &str) -> Regex {
    // Pattern: <TAG[^>]*>  (anything)  </TAG>
    // (?i) = case-insensitive, (?s) = . matches newline
    let pattern = format!(r"(?is)<{0}(?:\s[^>]*)?>[\s\S]*?</{0}\s*>", regex::escape(tag));
    Regex::new(&pattern).unwrap()
}

/// Match a self-closing or void element (like `<meta ... />` or `<input ...>`).
///
/// Some dropped elements (`meta`, `link`, `base`, `input`, `button`) may
/// appear as void / self-closing elements with no separate closing tag.
fn re_void_element(tag: &str) -> Regex {
    // Pattern: <TAG[space][attrs]> or <TAG[space][attrs]/>
    let pattern = format!(r"(?is)<{0}(?:\s[^>]*)?>", regex::escape(tag));
    Regex::new(&pattern).unwrap()
}

/// Match event handler attributes: `on[anything]="..."` or `on[anything]='...'`
/// or bare `on[anything]=value`.
///
/// Handles:
/// - `onclick="alert(1)"` — double-quoted value
/// - `onclick='alert(1)'` — single-quoted value
/// - `onclick=alert(1)` — unquoted value (stops at whitespace or `>`)
fn re_event_handlers() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| {
        Regex::new(r#"(?i)\s+on\w+\s*=\s*(?:"[^"]*"|'[^']*'|[^\s>]*)"#).unwrap()
    })
}

/// Match a specific attribute by name.
///
/// Called dynamically for each attribute in `drop_attributes`.
/// Handles double-quoted, single-quoted, and unquoted attribute values.
fn re_attribute(attr: &str) -> Regex {
    let pattern = format!(
        r#"(?i)\s+{0}\s*=\s*(?:"[^"]*"|'[^']*'|[^\s>]*)"#,
        regex::escape(attr)
    );
    Regex::new(&pattern).unwrap()
}

/// Match a `href` attribute value.
///
/// Captures the quote style (group 1) and the value (group 2) for replacement.
fn re_href() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| Regex::new(r#"(?i)(\s+href\s*=\s*)(["'])([^"']*)(["'])"#).unwrap())
}

/// Match a `src` attribute value.
fn re_src() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| Regex::new(r#"(?i)(\s+src\s*=\s*)(["'])([^"']*)(["'])"#).unwrap())
}

/// Match a `style` attribute containing `expression(`.
///
/// The full `style="..."` attribute is matched for removal (not just the
/// expression portion) because partial CSS sanitization is unreliable.
fn re_style_expression() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| {
        Regex::new(r#"(?i)\s+style\s*=\s*(?:"[^"]*expression\([^"]*"|'[^']*expression\([^']*')"#)
            .unwrap()
    })
}

/// Match a `style` attribute containing a `url(...)` call with a non-http/https argument.
///
/// CSS `url()` with a `javascript:` or `data:` scheme is a CSS injection vector:
/// `style="background:url(javascript:alert(1))"`. We strip the entire `style`
/// attribute rather than trying to parse and rewrite the CSS value.
fn re_style_url_injection() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    // Matches style attributes where url() does NOT contain http:// or https://
    // We match any style containing url( that doesn't contain http
    RE.get_or_init(|| {
        Regex::new(r#"(?i)\s+style\s*=\s*(?:"[^"]*url\s*\([^"]*"|'[^']*url\s*\([^']*')"#)
            .unwrap()
    })
}

// ─── Public API ───────────────────────────────────────────────────────────────

/// Sanitize an HTML string by stripping dangerous elements and attributes.
///
/// Returns a new `String` with policy violations removed. The input is not
/// modified. The function is safe to call on empty strings.
///
/// # Examples
///
/// ```rust
/// use coding_adventures_document_html_sanitizer::html_sanitizer::sanitize_html;
/// use coding_adventures_document_html_sanitizer::policy::html_strict;
///
/// // Script elements are removed including their content
/// let result = sanitize_html("<p>Safe</p><script>alert(1)</script>", &html_strict());
/// assert_eq!(result, "<p>Safe</p>");
///
/// // Event handler attributes are stripped
/// let result = sanitize_html(r#"<img src="x.png" onload="alert(1)">"#, &html_strict());
/// assert_eq!(result, r#"<img src="x.png">"#);
///
/// // javascript: URLs in href are neutralised
/// let result = sanitize_html(r#"<a href="javascript:alert(1)">click</a>"#, &html_strict());
/// assert_eq!(result, r#"<a href="">click</a>"#);
///
/// // Comments are stripped
/// let result = sanitize_html("<!-- hidden --><p>ok</p>", &html_strict());
/// assert_eq!(result, "<p>ok</p>");
/// ```
pub fn sanitize_html(html: &str, policy: &HtmlSanitizationPolicy) -> String {
    let mut output = html.to_string();

    // Step 1: Strip HTML comments (<!-- … -->)
    // Done first because comments can hide dangerous tags from later steps.
    // IE conditional comments (<!--[if IE]><script>…</script><![endif]-->) are
    // also caught — the inner `<script>` tag never reaches step 2.
    if policy.drop_comments {
        output = re_comment().replace_all(&output, "").into_owned();
    }

    // Step 2: Drop dangerous elements including their content.
    // Each tag in the drop list is processed with two patterns:
    //   a) A paired open+close pattern for elements with body content
    //   b) A self-closing / void element pattern (for <meta>, <input>, etc.)
    for tag in &policy.drop_elements {
        // Remove paired elements: <tag ...>...</tag>
        let re = re_element(tag);
        output = re.replace_all(&output, "").into_owned();

        // Remove self-closing / void elements: <tag ... /> or <tag ...>
        let re_void = re_void_element(tag);
        output = re_void.replace_all(&output, "").into_owned();
    }

    // Step 3: Strip ALL on* event handler attributes from surviving elements.
    // This is done by the core logic, independent of policy.drop_attributes,
    // because event handlers are universally dangerous (onclick, onmouseover,
    // onload, onfocus, onerror — the list is unbounded).
    output = re_event_handlers().replace_all(&output, "").into_owned();

    // Step 4: Strip additional policy-specified attributes (srcdoc, formaction, etc.)
    for attr in &policy.drop_attributes {
        let re = re_attribute(attr);
        output = re.replace_all(&output, "").into_owned();
    }

    // Step 5: Sanitize href and src attribute values.
    // Replace dangerous URL schemes with an empty string.
    output = sanitize_url_attr(output, re_href(), &policy.allowed_url_schemes);
    output = sanitize_url_attr(output, re_src(), &policy.allowed_url_schemes);

    // Step 6: Sanitize style attributes.
    // Remove style attributes containing CSS expression() or url() injection.
    if policy.sanitize_style_attributes {
        output = re_style_expression().replace_all(&output, "").into_owned();
        // For url() we do a more nuanced check: only strip if the url() value
        // is not an http/https URL.
        output = sanitize_style_url(output, &policy.allowed_url_schemes);
    }

    output
}

// ─── Helper: URL attribute sanitization ──────────────────────────────────────

/// Sanitize all occurrences of a URL attribute (href or src) in the HTML string.
///
/// The regex must have 4 capture groups:
/// - Group 1: prefix (`\s+href\s*=\s*`)
/// - Group 2: opening quote (`"` or `'`)
/// - Group 3: URL value
/// - Group 4: closing quote (same as group 2 for well-formed HTML)
///
/// Unsafe URL values are replaced with an empty string, producing e.g.:
/// `href="javascript:alert(1)"` → `href=""`
fn sanitize_url_attr(
    html: String,
    re: &Regex,
    allowed_schemes: &Option<Vec<String>>,
) -> String {
    // We use replace_all with a closure to inspect the captured URL value
    re.replace_all(&html, |caps: &regex::Captures| {
        let prefix = &caps[1]; // e.g. ` href=`
        let open_quote = &caps[2];
        let url = &caps[3];
        let close_quote = &caps[4];

        if is_scheme_allowed(url, allowed_schemes) {
            // URL is safe — keep original attribute unchanged
            format!("{}{}{}{}", prefix, open_quote, url, close_quote)
        } else {
            // URL is dangerous — replace with empty string
            format!("{}{}{}", prefix, open_quote, close_quote)
        }
    })
    .into_owned()
}

// ─── Helper: Style attribute url() sanitization ───────────────────────────────

/// Sanitize `style` attributes containing `url(...)` calls.
///
/// We strip the entire `style` attribute when it contains a `url()` call
/// with a non-http/https scheme. This is conservative but safe.
///
/// # Why not just strip the url() value?
///
/// CSS is complex. A style value like `background: url(javascript:alert(1));`
/// has multiple layers of encoding and cascade — partial sanitization risks
/// leaving the injection intact in some rendering engines.
fn sanitize_style_url(html: String, allowed_schemes: &Option<Vec<String>>) -> String {
    // The regex matches any style attribute containing url(...)
    // We then inspect the content to decide whether to strip.
    let re = re_style_url_injection();

    re.replace_all(&html, |caps: &regex::Captures| {
        let matched = &caps[0]; // full match including the style attribute

        // Extract what's inside url(...)
        // We look for url( ... ) in the matched text
        let inner_re = Regex::new(r"(?i)url\s*\(\s*([^)]*)\s*\)").unwrap();
        let mut all_safe = true;
        for inner_cap in inner_re.captures_iter(matched) {
            let url_value = inner_cap[1].trim().trim_matches(|c| c == '"' || c == '\'');
            if !is_scheme_allowed(url_value, allowed_schemes) {
                all_safe = false;
                break;
            }
        }

        if all_safe {
            matched.to_string() // all url() values are safe — keep
        } else {
            String::new() // dangerous url() found — strip entire style attr
        }
    })
    .into_owned()
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::policy::{html_passthrough, html_relaxed, html_strict};

    // ─── Passthrough: nothing is modified ─────────────────────────────────────

    #[test]
    fn passthrough_preserves_script() {
        let html = "<script>alert(1)</script>";
        assert_eq!(sanitize_html(html, &html_passthrough()), html);
    }

    #[test]
    fn passthrough_strips_event_handlers() {
        // Per spec: on* event handler attributes are ALWAYS stripped as they are
        // universally dangerous. The passthrough policy preserves elements and
        // most attributes, but not event handlers — they are stripped by the
        // core logic before policy-specific attribute processing.
        //
        // This matches the spec intent: "passthrough — no sanitization" applies
        // to drop_elements and drop_attributes (the policy fields), but the
        // core always-on protections (on* stripping) remain active.
        let html = r#"<div onclick="alert(1)">x</div>"#;
        let result = sanitize_html(html, &html_passthrough());
        assert_eq!(result, "<div>x</div>");
    }

    // ─── Script element removal ────────────────────────────────────────────────

    #[test]
    fn script_removed_with_content() {
        let html = "<p>Safe</p><script>alert(1)</script>";
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>Safe</p>");
    }

    #[test]
    fn script_uppercase_removed() {
        let html = "<p>ok</p><SCRIPT>alert(1)</SCRIPT>";
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>ok</p>");
    }

    #[test]
    fn script_with_src_removed() {
        let html = r#"<script src="https://evil.com/xss.js"></script><p>text</p>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>text</p>");
    }

    #[test]
    fn style_element_removed_by_strict() {
        let html = "<style>body { display:none }</style><p>hi</p>";
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>hi</p>");
    }

    #[test]
    fn style_element_kept_by_relaxed() {
        let html = "<style>p { color: red; }</style><p>hi</p>";
        let result = sanitize_html(html, &html_relaxed());
        assert_eq!(result, "<style>p { color: red; }</style><p>hi</p>");
    }

    #[test]
    fn iframe_removed_by_strict() {
        let html = r#"<p>before</p><iframe src="https://evil.com"></iframe><p>after</p>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>before</p><p>after</p>");
    }

    // ─── Event handler removal ─────────────────────────────────────────────────

    #[test]
    fn onclick_stripped() {
        let html = r#"<a onclick="alert(1)">click</a>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<a>click</a>");
    }

    #[test]
    fn onload_stripped_from_img() {
        let html = r#"<img src="x.png" onload="alert(1)">"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<img src="x.png">"#);
    }

    #[test]
    fn onfocus_stripped() {
        let html = r#"<div onfocus="alert(1)" tabindex="0">x</div>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<div tabindex="0">x</div>"#);
    }

    #[test]
    fn svg_onload_stripped() {
        let html = r#"<svg onload="alert(1)"></svg>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<svg></svg>");
    }

    #[test]
    fn event_handlers_stripped_even_in_passthrough_elements() {
        // Even when using html_relaxed() (which keeps style), on* are always stripped
        let html = r#"<p onmouseover="alert(1)">hover</p>"#;
        let result = sanitize_html(html, &html_relaxed());
        assert_eq!(result, "<p>hover</p>");
    }

    // ─── URL sanitization in href/src ─────────────────────────────────────────

    #[test]
    fn javascript_href_neutralised() {
        let html = r#"<a href="javascript:alert(1)">click</a>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<a href="">click</a>"#);
    }

    #[test]
    fn javascript_uppercase_href_neutralised() {
        let html = r#"<a href="JAVASCRIPT:alert(1)">click</a>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<a href="">click</a>"#);
    }

    #[test]
    fn https_href_kept() {
        let html = r#"<a href="https://example.com">link</a>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, html);
    }

    #[test]
    fn data_uri_in_img_src_neutralised() {
        let html = r#"<img src="data:text/html,<script>alert(1)</script>">"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<img src="">"#);
    }

    #[test]
    fn relative_src_kept() {
        let html = r#"<img src="../images/cat.png">"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, html);
    }

    // ─── CSS expression injection ─────────────────────────────────────────────

    #[test]
    fn style_expression_stripped() {
        let html = r#"<p style="width:expression(alert(1))">x</p>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>x</p>");
    }

    #[test]
    fn style_url_javascript_stripped() {
        let html = r#"<p style="background:url(javascript:alert(1))">x</p>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>x</p>");
    }

    #[test]
    fn style_url_https_kept() {
        // url() with https: is safe — style attribute should be preserved
        let html = r#"<p style="background:url(https://example.com/bg.png)">x</p>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, html);
    }

    // ─── Comment stripping ────────────────────────────────────────────────────

    #[test]
    fn comment_stripped_by_strict() {
        let html = "<!-- comment --><p>ok</p>";
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>ok</p>");
    }

    #[test]
    fn comment_kept_by_relaxed() {
        let html = "<!-- comment --><p>ok</p>";
        let result = sanitize_html(html, &html_relaxed());
        assert_eq!(result, html);
    }

    #[test]
    fn ie_conditional_comment_stripped() {
        // <!--[if IE]><script>alert(1)</script><![endif]-->
        let html = "<!--[if IE]><script>alert(1)</script><![endif]--><p>ok</p>";
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>ok</p>");
    }

    #[test]
    fn multiline_comment_stripped() {
        let html = "<!--\n<img src=x onerror=alert(1)>\n--><p>safe</p>";
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>safe</p>");
    }

    // ─── srcdoc attribute (strict) ────────────────────────────────────────────

    #[test]
    fn srcdoc_attr_stripped_by_strict() {
        // srcdoc is in strict's drop_attributes list.
        // Use a <div> rather than <iframe> to isolate the attribute stripping
        // behaviour — <iframe> itself is in drop_elements and would be removed
        // entirely before the attribute check runs.
        let html = r#"<div srcdoc="<script>alert(1)</script>">x</div>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<div>x</div>"#);
    }

    // ─── XSS bypass vectors ───────────────────────────────────────────────────

    #[test]
    fn xss_nul_byte_javascript_blocked() {
        // java\x00script: — NUL byte stripped before scheme check
        let html = "<a href=\"java\x00script:alert(1)\">x</a>";
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<a href="">x</a>"#);
    }

    #[test]
    fn xss_vbscript_blocked() {
        let html = r#"<a href="vbscript:MsgBox(1)">x</a>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, r#"<a href="">x</a>"#);
    }

    // ─── Edge cases ───────────────────────────────────────────────────────────

    #[test]
    fn empty_string_is_safe() {
        assert_eq!(sanitize_html("", &html_strict()), "");
    }

    #[test]
    fn plain_text_unchanged() {
        let text = "Hello, world!";
        assert_eq!(sanitize_html(text, &html_strict()), text);
    }

    #[test]
    fn meta_void_element_removed() {
        // <meta> is a void element — no closing tag
        let html = r#"<meta http-equiv="refresh" content="0;url=https://evil.com"><p>hi</p>"#;
        let result = sanitize_html(html, &html_strict());
        assert_eq!(result, "<p>hi</p>");
    }
}
