"""Multivariate polynomial reduction (division with remainder).

Reduction is the fundamental operation in Gröbner-basis theory.
Given a polynomial ``f`` and a list of polynomials ``G = [g₁, …, gₖ]``,
we compute the *normal form* of ``f`` with respect to ``G``:

    f = q₁g₁ + q₂g₂ + … + qₖgₖ + r

where ``r`` is the *remainder* — no monomial of ``r`` is divisible by the
leading monomial of any ``gᵢ``.

This is the multivariate generalisation of polynomial long division.  The
univariate case is unique (the remainder is independent of divisor order);
the multivariate case depends on divisor order unless ``G`` is a Gröbner
basis — which is exactly why Buchberger's algorithm computes one.

Algorithm (informal)
--------------------
```
r = 0
p = f
while p ≠ 0:
    found a divisor?  YES:
        pick gᵢ whose lt(gᵢ) divides lt(p)
        p ← p − (lt(p) / lt(gᵢ)) · gᵢ
    NO:
        move lt(p) to r
        p ← p − lt(p)
return r
```

Note: we pick the *first* applicable divisor on each step (standard
implementation).  The remainder may differ if a different divisor is
chosen, unless ``G`` is a Gröbner basis.

Usage::

    from fractions import Fraction
    from cas_multivariate.polynomial import MPoly
    from cas_multivariate.reduce import reduce_poly, s_poly

    # Reduce x^2*y + x*y^2 + y^2 by [x*y - 1, y^2 - 1]
    f = MPoly({(2,1): Fraction(1), (1,2): Fraction(1), (0,2): Fraction(1)}, 2)
    g1 = MPoly({(1,1): Fraction(1), (0,0): Fraction(-1)}, 2)  # xy - 1
    g2 = MPoly({(0,2): Fraction(1), (0,0): Fraction(-1)}, 2)  # y^2 - 1
    r = reduce_poly(f, [g1, g2], order="grlex")
    # r is the remainder after full reduction
"""

from __future__ import annotations

from fractions import Fraction

from cas_multivariate.monomial import div_monomial, divides, lcm_monomial
from cas_multivariate.polynomial import MPoly

# ---------------------------------------------------------------------------
# Polynomial reduction (normal form)
# ---------------------------------------------------------------------------


def reduce_poly(f: MPoly, G: list[MPoly], order: str = "grlex") -> MPoly:
    """Reduce ``f`` by the list ``G`` and return the remainder.

    This implements the standard multivariate division algorithm.

    The result ``r`` satisfies: no leading monomial of any element in ``G``
    divides any monomial of ``r``.  If ``G`` is a Gröbner basis, ``r`` is
    the unique normal form of ``f`` (modulo the ideal generated by ``G``).

    Parameters
    ----------
    f:
        The polynomial to reduce.
    G:
        List of divisors (must all have the same ``nvars`` as ``f``).
    order:
        Monomial ordering: ``"grlex"`` (default), ``"lex"``, or ``"grevlex"``.

    Returns
    -------
    The remainder polynomial.  Returns ``MPoly.zero(f.nvars)`` if
    ``f`` reduces to zero (i.e. ``f ∈ ⟨G⟩``).

    Example::

        # x^2 - 1 reduced by [x - 1] should give 0 (x-1 divides x^2-1)
        f  = MPoly({(2,): Fraction(1), (0,): Fraction(-1)}, 1)   # x^2 - 1
        g  = MPoly({(1,): Fraction(1), (0,): Fraction(-1)}, 1)   # x - 1
        reduce_poly(f, [g], "lex")   # → MPoly.zero(1)

        # 5x^2*y + x reduced by [x^2*y - 1, x - 2] in grlex
        f = MPoly({(2,1): Fraction(5), (1,0): Fraction(1)}, 2)
        g1 = MPoly({(2,1): Fraction(1), (0,0): Fraction(-1)}, 2)  # x^2y - 1
        reduce_poly(f, [g1], "grlex")
        # = 5*(1) + x = x + 5  (leading term x^2y cancelled)
    """
    # Work on a running copy; r accumulates the remainder.
    p = f
    r = MPoly.zero(f.nvars)

    while not p.is_zero():
        lm_p = p.lm(order)

        # Try each divisor in order.
        reduced = False
        for g in G:
            if g.is_zero():
                continue
            lm_g = g.lm(order)
            if divides(lm_g, lm_p):
                # Subtract (lc_p / lc_g) * x^(lm_p - lm_g) * g from p.
                exp_diff = div_monomial(lm_p, lm_g)
                coeff = p.lc(order) / g.lc(order)
                p = p - g.mul_monomial(exp_diff, coeff)
                reduced = True
                break  # restart the loop with the new p

        if not reduced:
            # No divisor applies — move the leading term to the remainder.
            lt_p = p.lt(order)
            r = r + lt_p
            p = p - lt_p

    return r


# ---------------------------------------------------------------------------
# S-polynomial
# ---------------------------------------------------------------------------


def s_poly(f: MPoly, g: MPoly, order: str = "grlex") -> MPoly:
    """Compute the S-polynomial of ``f`` and ``g``.

    The S-polynomial is defined as::

        S(f, g) = (L / lt(f)) * f  −  (L / lt(g)) * g

    where  ``L = lcm(lm(f), lm(g))``  is the least-common-multiple of the
    leading monomials, and  ``L / lt(f)``  means the monomial ratio scaled
    by the inverse leading coefficient.

    Intuition: S(f, g) is designed so that the leading terms of ``f`` and
    ``g`` *cancel*.  If the resulting polynomial reduces to zero under a
    list ``G``, it means the pair ``(f, g)`` doesn't need a new basis
    element.  Buchberger's criterion says: ``G`` is a Gröbner basis iff
    every S-polynomial reduces to zero.

    Example::

        # f = x^2, g = x*y
        # lcm(x^2, xy) = x^2*y
        # S(f,g) = (x^2y / x^2)*f - (x^2y / xy)*g
        #        = y * x^2 - x * xy
        #        = x^2*y - x^2*y = 0   (in this case)

    Another example (the classic Buchberger pair)::

        # f = x^2 + y, g = x*y + 1
        # lt(f) = x^2, lt(g) = x*y
        # lcm = x^2*y
        # S = y*f - x*g = y(x^2+y) - x(xy+1) = x^2y+y^2 - x^2y - x = y^2 - x

    Parameters
    ----------
    f, g:
        Non-zero polynomials with the same ``nvars``.
    order:
        Monomial ordering.

    Returns
    -------
    The S-polynomial.  May be zero (good: means no new basis element needed).
    """
    assert not f.is_zero() and not g.is_zero(), (
        "S-polynomial undefined for zero polynomials"
    )

    lm_f = f.lm(order)
    lm_g = g.lm(order)
    lc_f = f.lc(order)
    lc_g = g.lc(order)

    # Least-common multiple of the leading monomials.
    lcm = lcm_monomial(lm_f, lm_g)

    # Multipliers: L/lt(f) means the monomial (lcm - lm_f) / lc_f
    exp_f = div_monomial(lcm, lm_f)  # exponent shift for f
    exp_g = div_monomial(lcm, lm_g)  # exponent shift for g

    coeff_f = Fraction(1) / lc_f
    coeff_g = Fraction(1) / lc_g

    # S(f, g) = (coeff_f * x^exp_f) * f - (coeff_g * x^exp_g) * g
    return f.mul_monomial(exp_f, coeff_f) - g.mul_monomial(exp_g, coeff_g)
