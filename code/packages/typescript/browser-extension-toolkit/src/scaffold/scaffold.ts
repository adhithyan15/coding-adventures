const POPUP_TS_TEMPLATE = `/**
 * Popup Script
 * ============
 *
 * This script runs when the user clicks the extension's toolbar icon.
 * It has access to the DOM of popup.html and can use the browser
 * extension API via the cross-browser shim.
 *
 * Remember: the popup is DESTROYED when the user clicks away. Any state
 * you need to persist must be saved via browser.storage or by messaging
 * the background service worker.
 */

import { getBrowserAPI } from "../lib/browser-api";

/**
 * Initialize the popup when the DOM is ready.
 *
 * We use DOMContentLoaded rather than running code at the top level
 * because the script might load before the HTML elements exist.
 */
export function initPopup(): void {
  const info = document.getElementById("browser-info");
  if (!info) return;

  try {
    const api = getBrowserAPI();
    const manifest = api.runtime.getManifest();
    info.textContent = \`Running \${manifest.name} v\${manifest.version}\`;
  } catch {
    info.textContent = "Running outside extension context";
  }
}

document.addEventListener("DOMContentLoaded", initPopup);
`;

