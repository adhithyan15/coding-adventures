/**
 * The activation entry point.  VS Code calls `activate()` when the
 * extension's activation events fire (a matching file is opened, or
 * a debug session of the registered type starts).  We dispatch into
 * the LSP and DAP submodules conditionally — only the ones we wired.
 */
export function buildExtensionTs(opts: GeneratorOptions): string {
  const hasLsp = opts.lspBinary !== "";
  const hasDap = opts.dapBinary !== "";

  const lines: string[] = [];
  lines.push("// Generated by vscode-lang-extension-generator (LS04).");
  lines.push("// Edit at your own risk; re-running the generator overwrites this file.");
  lines.push("");
  lines.push('import * as vscode from "vscode";');
  if (hasLsp) {
    lines.push('import { startLanguageClient, stopLanguageClient } from "./lsp";');
  }
  if (hasDap) {
    lines.push('import { registerDebugAdapter } from "./dap";');
  }
  lines.push("");
  lines.push("export async function activate(context: vscode.ExtensionContext): Promise<void> {");
  if (hasLsp) {
    lines.push("  await startLanguageClient(context);");
  }
  if (hasDap) {
    lines.push("  registerDebugAdapter(context);");
  }
  lines.push("}");
  lines.push("");
  lines.push("export async function deactivate(): Promise<void> {");
  if (hasLsp) {
    lines.push("  await stopLanguageClient();");
  }
  lines.push("}");
  lines.push("");
  return lines.join("\n");
}

// =========================================================================
// lsp.ts (only if LSP wired)
// =========================================================================

/**
 * The LSP client wrapper.  Spawns the language server binary as a
 * subprocess and connects over stdio JSON-RPC, the standard LSP
 * transport.  The binary path is resolved from VS Code settings —
 * if the user has the binary on PATH, the default works; otherwise
 * they can override `<id>.serverPath` to an absolute path.
 */
export function buildLspTs(opts: GeneratorOptions): string {
  // All user-controlled values are embedded via JSON.stringify so that
  // even if validation regresses, the result is a safe JS string literal
  // rather than executable code.  The doc-comment lines that interpolate
  // `id` and `lspBinary` are the only "raw" embeds — both fields are
  // strictly validated upstream.
  const idLit = JSON.stringify(opts.languageId);
  const nameLit = JSON.stringify(`${opts.languageName} Language Server`);
  const binLit = JSON.stringify(opts.lspBinary);
  const id = opts.languageId; // safe per LANGUAGE_ID_RE
  const lspBinary = opts.lspBinary; // safe per BINARY_NAME_RE
  return `// Generated by vscode-lang-extension-generator (LS04).
// Edit at your own risk; re-running the generator overwrites this file.

import * as vscode from "vscode";
import {
  LanguageClient,
  LanguageClientOptions,
  ServerOptions,
  TransportKind,
} from "vscode-languageclient/node";

let client: LanguageClient | undefined;

/**
 * Start the language client and connect to the server binary.
 *
 * Resolution order for the binary:
 *   1. Setting "${id}.serverPath" if the user has set it.
 *   2. The default "${lspBinary}", looked up on PATH by the OS.
 */
export async function startLanguageClient(
  _context: vscode.ExtensionContext,
): Promise<void> {
  const config = vscode.workspace.getConfiguration(${idLit});
  const serverPath = config.get<string>("serverPath", ${binLit});

  const serverOptions: ServerOptions = {
    command: serverPath,
    args: [],
    transport: TransportKind.stdio,
  };

  const clientOptions: LanguageClientOptions = {
    documentSelector: [{ scheme: "file", language: ${idLit} }],
    synchronize: {
      configurationSection: ${idLit},
    },
  };

  client = new LanguageClient(
    ${idLit},
    ${nameLit},
    serverOptions,
    clientOptions,
  );
  await client.start();
}

/** Cleanly stop the language client during extension deactivation. */
export async function stopLanguageClient(): Promise<void> {
  if (client) {
    await client.stop();
    client = undefined;
  }
}
`;
}

