type FileMap = Record<string, string>;

function parseCsv(value: string | undefined): string[] {
  if (value === undefined || value.trim() === "") {
    return [];
  }

  return [...new Set(value.split(",").map((part) => part.trim()).filter(Boolean))].sort();
}

function titleFromKebab(value: string): string {
  return value
    .split("-")
    .filter(Boolean)
    .map((part) => part[0]!.toUpperCase() + part.slice(1))
    .join(" ");
}

function joinedLower(value: string): string {
  return value.replaceAll("-", "");
}

function assertKebab(value: string, label: string): void {
  if (!/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(value)) {
    throw new Error(`${label} must be kebab-case, got "${value}"`);
  }
}

function appDir(repoRoot: string, name: string): string {
  return path.join(repoRoot, "code", "programs", "typescript", name);
}

function workflowPath(repoRoot: string, template: AppTemplate, slug: string): string {
  if (template === "visualization") {
    return path.join(repoRoot, ".github", "workflows", `deploy-${slug}.yml`);
  }

  return path.join(repoRoot, ".github", "workflows", `release-${slug}.yml`);
}

function repoRelative(absPath: string, repoRoot: string): string {
  return path.relative(repoRoot, absPath).replaceAll(path.sep, "/");
}

