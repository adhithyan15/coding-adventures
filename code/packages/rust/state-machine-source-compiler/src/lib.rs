//! Build-time source generation for typed state-machine definitions.
//!
//! A state-machine definition can be authored as TOML or JSON for humans and
//! tooling, but production packages should not need to load those text files at
//! runtime. This crate sits on the build side of that boundary: it receives an
//! already-typed `StateMachineDefinition`, validates the phase 1 automata
//! shape, and emits ordinary Rust source code that wrapper packages can link.

use std::collections::HashSet;
use std::error::Error;
use std::fmt;

use state_machine::{MachineKind, MatcherDefinition, StateMachineDefinition, TransitionDefinition};
use state_machine_markup_deserializer::{validate_definition, StateMachineMarkupError};

/// Errors that can occur before source text is emitted.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum StateMachineSourceError {
    /// The typed definition failed semantic validation.
    Validation(StateMachineMarkupError),
    /// The selected generator does not support this machine family yet.
    UnsupportedKind { kind: String },
    /// The generated Rust function stem would not be a usable identifier.
    EmptyGeneratedIdentifier { source: String },
    /// Two generated Rust identifiers would be identical.
    IdentifierCollision {
        left: String,
        right: String,
        identifier: String,
    },
    /// A generated Rust identifier would be a reserved word.
    ReservedIdentifier { identifier: String },
}

impl fmt::Display for StateMachineSourceError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Validation(error) => write!(f, "{error}"),
            Self::UnsupportedKind { kind } => {
                write!(
                    f,
                    "Rust source generation does not support `{kind}` machines yet"
                )
            }
            Self::EmptyGeneratedIdentifier { source } => {
                write!(
                    f,
                    "`{source}` does not contain any Rust identifier characters"
                )
            }
            Self::IdentifierCollision {
                left,
                right,
                identifier,
            } => write!(
                f,
                "`{left}` and `{right}` both generate the Rust identifier `{identifier}`"
            ),
            Self::ReservedIdentifier { identifier } => {
                write!(f, "`{identifier}` is a reserved Rust identifier")
            }
        }
    }
}

impl Error for StateMachineSourceError {}

/// Convenience result type for source generation.
pub type Result<T> = std::result::Result<T, StateMachineSourceError>;

/// Extension trait for emitting Rust source from typed definitions.
pub trait StateMachineRustSourceCompiler {
    /// Emit a deterministic Rust module for this definition.
    fn to_rust_source(&self) -> Result<String>;
}

impl StateMachineRustSourceCompiler for StateMachineDefinition {
    fn to_rust_source(&self) -> Result<String> {
        to_rust_source(self)
    }
}

/// Emit deterministic Rust source for a phase 1 DFA, NFA, or PDA definition.
///
/// The source generator deliberately accepts typed data rather than raw files.
/// TOML and JSON readers are separate tooling crates because parsing text is a
/// trust boundary; generated Rust should be static table data and constructors.
pub fn to_rust_source(definition: &StateMachineDefinition) -> Result<String> {
    ensure_supported_kind(definition)?;
    validate_definition(definition).map_err(StateMachineSourceError::Validation)?;

    let function_stem = rust_identifier(&definition.name)?;
    ensure_function_names_do_not_collide(&function_stem, definition)?;

    let mut source = String::new();
    source.push_str("//! Generated state-machine definition for `");
    source.push_str(&comment_text(&definition.name));
    source.push_str("`.\n");
    source.push_str("//!\n");
    source.push_str("//! This file was generated by state-machine-source-compiler.\n");
    source.push_str("//! Do not edit by hand.\n\n");
    source.push_str("#[allow(unused_imports)]\n");
    source.push_str("use state_machine::{");
    source.push_str(machine_imports(definition));
    source.push_str("};\n\n");

    emit_definition_function(&mut source, &function_stem, definition);
    emit_machine_constructor(&mut source, &function_stem, definition);
    Ok(source)
}

fn ensure_supported_kind(definition: &StateMachineDefinition) -> Result<()> {
    match definition.kind {
        MachineKind::Dfa | MachineKind::Nfa | MachineKind::Pda | MachineKind::Transducer => Ok(()),
        _ => Err(StateMachineSourceError::UnsupportedKind {
            kind: definition.kind.as_str().to_string(),
        }),
    }
}

fn ensure_function_names_do_not_collide(
    function_stem: &str,
    definition: &StateMachineDefinition,
) -> Result<()> {
    let mut seen = HashSet::new();
    for suffix in ["definition", machine_suffix(definition)] {
        let name = format!("{function_stem}_{suffix}");
        if is_rust_reserved(&name) {
            return Err(StateMachineSourceError::ReservedIdentifier { identifier: name });
        }
        if !seen.insert(name.clone()) {
            return Err(StateMachineSourceError::IdentifierCollision {
                left: "definition function".to_string(),
                right: "machine constructor".to_string(),
                identifier: name,
            });
        }
    }
    Ok(())
}

fn emit_definition_function(
    source: &mut String,
    function_stem: &str,
    definition: &StateMachineDefinition,
) {
    source.push_str("pub fn ");
    source.push_str(function_stem);
    source.push_str("_definition() -> StateMachineDefinition {\n");
    source.push_str("    let mut definition = StateMachineDefinition::new(");
    source.push_str(&rust_string_literal(&definition.name));
    source.push_str(", MachineKind::");
    source.push_str(machine_kind_variant(definition));
    source.push_str(");\n");

    if let Some(initial) = &definition.initial {
        source.push_str("    definition.initial = Some(");
        source.push_str(&rust_to_string_expr(initial));
        source.push_str(");\n");
    }
    if let Some(version) = &definition.version {
        source.push_str("    definition.version = Some(");
        source.push_str(&rust_to_string_expr(version));
        source.push_str(");\n");
    }
    if let Some(profile) = &definition.profile {
        source.push_str("    definition.profile = Some(");
        source.push_str(&rust_to_string_expr(profile));
        source.push_str(");\n");
    }
    if let Some(runtime_min) = &definition.runtime_min {
        source.push_str("    definition.runtime_min = Some(");
        source.push_str(&rust_to_string_expr(runtime_min));
        source.push_str(");\n");
    }
    if let Some(done) = &definition.done {
        source.push_str("    definition.done = Some(");
        source.push_str(&rust_to_string_expr(done));
        source.push_str(");\n");
    }
    emit_string_vec_assignment(source, "alphabet", &sorted_strings(&definition.alphabet));
    emit_string_vec_assignment(
        source,
        "stack_alphabet",
        &sorted_strings(&definition.stack_alphabet),
    );
    if let Some(initial_stack) = &definition.initial_stack {
        source.push_str("    definition.initial_stack = Some(");
        source.push_str(&rust_to_string_expr(initial_stack));
        source.push_str(");\n");
    }
    emit_string_vec_assignment(source, "includes", &sorted_strings(&definition.includes));
    emit_tokens(source, definition);
    emit_inputs(source, definition);
    emit_registers(source, definition);
    emit_guards(source, definition);
    emit_fixtures(source, definition);
    emit_states(source, definition);
    emit_transitions(source, definition);
    source.push_str("    definition\n");
    source.push_str("}\n\n");
}

fn emit_machine_constructor(
    source: &mut String,
    function_stem: &str,
    definition: &StateMachineDefinition,
) {
    match definition.kind {
        MachineKind::Dfa => {
            source.push_str("pub fn ");
            source.push_str(function_stem);
            source.push_str("_dfa() -> std::result::Result<DFA, String> {\n");
            source.push_str("    DFA::from_definition(&");
        }
        MachineKind::Nfa => {
            source.push_str("pub fn ");
            source.push_str(function_stem);
            source.push_str("_nfa() -> std::result::Result<NFA, String> {\n");
            source.push_str("    NFA::from_definition(&");
        }
        MachineKind::Pda => {
            source.push_str("pub fn ");
            source.push_str(function_stem);
            source.push_str("_pda() -> std::result::Result<PushdownAutomaton, String> {\n");
            source.push_str("    PushdownAutomaton::from_definition(&");
        }
        MachineKind::Transducer => {
            source.push_str("pub fn ");
            source.push_str(function_stem);
            source.push_str(
                "_transducer() -> std::result::Result<EffectfulStateMachine, String> {\n",
            );
            source.push_str("    EffectfulStateMachine::from_definition(&");
        }
        _ => unreachable!("unsupported kinds are rejected before source emission"),
    }
    source.push_str(function_stem);
    source.push_str("_definition())\n");
    source.push_str("}\n");
}

fn emit_string_vec_assignment(source: &mut String, field: &str, values: &[String]) {
    if values.is_empty() {
        return;
    }
    source.push_str("    definition.");
    source.push_str(field);
    source.push_str(" = ");
    source.push_str(&rust_string_vec(values, 4));
    source.push_str(";\n");
}

fn emit_tokens(source: &mut String, definition: &StateMachineDefinition) {
    if definition.tokens.is_empty() {
        return;
    }

    source.push_str("    definition.tokens = vec![\n");
    for token in &definition.tokens {
        source.push_str("        TokenDefinition {\n");
        source.push_str("            name: ");
        source.push_str(&rust_to_string_expr(&token.name));
        source.push_str(",\n");
        source.push_str("            fields: ");
        source.push_str(&rust_string_vec(&token.fields, 12));
        source.push_str(",\n");
        source.push_str("        },\n");
    }
    source.push_str("    ];\n");
}

fn emit_inputs(source: &mut String, definition: &StateMachineDefinition) {
    if definition.inputs.is_empty() {
        return;
    }

    source.push_str("    definition.inputs = vec![\n");
    for input in &definition.inputs {
        source.push_str("        InputDefinition {\n");
        source.push_str("            id: ");
        source.push_str(&rust_to_string_expr(&input.id));
        source.push_str(",\n");
        source.push_str("            matcher: ");
        source.push_str(&rust_matcher_expr(&input.matcher, 12));
        source.push_str(",\n");
        source.push_str("        },\n");
    }
    source.push_str("    ];\n");
}

fn emit_registers(source: &mut String, definition: &StateMachineDefinition) {
    if definition.registers.is_empty() {
        return;
    }

    source.push_str("    definition.registers = vec![\n");
    for register in &definition.registers {
        source.push_str("        RegisterDefinition {\n");
        source.push_str("            id: ");
        source.push_str(&rust_to_string_expr(&register.id));
        source.push_str(",\n");
        source.push_str("            type_name: ");
        source.push_str(&rust_to_string_expr(&register.type_name));
        source.push_str(",\n");
        source.push_str("        },\n");
    }
    source.push_str("    ];\n");
}

fn emit_guards(source: &mut String, definition: &StateMachineDefinition) {
    if definition.guards.is_empty() {
        return;
    }

    source.push_str("    definition.guards = vec![\n");
    for guard in &definition.guards {
        source.push_str("        GuardDefinition {\n");
        source.push_str("            id: ");
        source.push_str(&rust_to_string_expr(&guard.id));
        source.push_str(",\n");
        source.push_str("        },\n");
    }
    source.push_str("    ];\n");
}

fn emit_fixtures(source: &mut String, definition: &StateMachineDefinition) {
    if definition.fixtures.is_empty() {
        return;
    }

    source.push_str("    definition.fixtures = vec![\n");
    for fixture in &definition.fixtures {
        source.push_str("        FixtureDefinition {\n");
        source.push_str("            name: ");
        source.push_str(&rust_to_string_expr(&fixture.name));
        source.push_str(",\n");
        source.push_str("            input: ");
        source.push_str(&rust_to_string_expr(&fixture.input));
        source.push_str(",\n");
        source.push_str("            tokens: ");
        source.push_str(&rust_string_vec(&fixture.tokens, 12));
        source.push_str(",\n");
        source.push_str("        },\n");
    }
    source.push_str("    ];\n");
}

fn emit_states(source: &mut String, definition: &StateMachineDefinition) {
    let mut states = definition.states.clone();
    states.sort_by(|left, right| left.id.cmp(&right.id));

    source.push_str("    definition.states = vec![\n");
    for state in states {
        source.push_str("        StateDefinition {\n");
        source.push_str("            id: ");
        source.push_str(&rust_to_string_expr(&state.id));
        source.push_str(",\n");
        source.push_str("            initial: ");
        source.push_str(bool_literal(state.initial));
        source.push_str(",\n");
        source.push_str("            accepting: ");
        source.push_str(bool_literal(state.accepting));
        source.push_str(",\n");
        source.push_str("            final_state: ");
        source.push_str(bool_literal(state.final_state));
        source.push_str(",\n");
        source.push_str("            external_entry: ");
        source.push_str(bool_literal(state.external_entry));
        source.push_str(",\n");
        source.push_str("        },\n");
    }
    source.push_str("    ];\n");
}

fn emit_transitions(source: &mut String, definition: &StateMachineDefinition) {
    let transitions = sorted_transitions(definition);
    source.push_str("    definition.transitions = vec![\n");
    for transition in transitions {
        source.push_str("        TransitionDefinition {\n");
        source.push_str("            from: ");
        source.push_str(&rust_to_string_expr(&transition.from));
        source.push_str(",\n");
        source.push_str("            on: ");
        source.push_str(&rust_option_string(&transition.on));
        source.push_str(",\n");
        source.push_str("            matcher: ");
        source.push_str(&rust_option_matcher(&transition.matcher, 12));
        source.push_str(",\n");
        source.push_str("            to: ");
        source.push_str(&rust_string_vec(&transition.to, 12));
        source.push_str(",\n");
        source.push_str("            guard: ");
        source.push_str(&rust_option_string(&transition.guard));
        source.push_str(",\n");
        source.push_str("            stack_pop: ");
        source.push_str(&rust_option_string(&transition.stack_pop));
        source.push_str(",\n");
        source.push_str("            stack_push: ");
        source.push_str(&rust_string_vec(&transition.stack_push, 12));
        source.push_str(",\n");
        source.push_str("            actions: ");
        source.push_str(&rust_string_vec(&transition.actions, 12));
        source.push_str(",\n");
        source.push_str("            consume: ");
        source.push_str(bool_literal(transition.consume));
        source.push_str(",\n");
        source.push_str("        },\n");
    }
    source.push_str("    ];\n");
}

fn sorted_transitions(definition: &StateMachineDefinition) -> Vec<TransitionDefinition> {
    if definition.kind == MachineKind::Transducer {
        return definition.transitions.clone();
    }

    let mut transitions = definition.transitions.clone();
    transitions.sort_by(|left, right| {
        let left_matcher = left.matcher.as_ref().map(matcher_sort_key);
        let right_matcher = right.matcher.as_ref().map(matcher_sort_key);
        (
            &left.from,
            &left.on,
            &left_matcher,
            &left.to,
            &left.guard,
            &left.stack_pop,
            &left.stack_push,
            &left.actions,
            left.consume,
        )
            .cmp(&(
                &right.from,
                &right.on,
                &right_matcher,
                &right.to,
                &right.guard,
                &right.stack_pop,
                &right.stack_push,
                &right.actions,
                right.consume,
            ))
    });
    transitions
}

fn matcher_sort_key(value: &MatcherDefinition) -> String {
    match value {
        MatcherDefinition::Literal(value) => format!("literal:{value}"),
        MatcherDefinition::Eof => "eof".to_string(),
        MatcherDefinition::Anything => "anything".to_string(),
        MatcherDefinition::Class(value) => format!("class:{value}"),
        MatcherDefinition::OneOf(value) => format!("one_of:{value}"),
        MatcherDefinition::Range { start, end } => format!("range:{start}..{end}"),
        MatcherDefinition::AnyOfClasses(values) => {
            format!("any_of_classes:{}", values.join(","))
        }
        MatcherDefinition::Lookahead(inner) => {
            format!("lookahead:{}", matcher_sort_key(inner))
        }
    }
}

fn machine_imports(definition: &StateMachineDefinition) -> &'static str {
    match definition.kind {
        MachineKind::Dfa => {
            "DFA, FixtureDefinition, GuardDefinition, InputDefinition, MachineKind, MatcherDefinition, RegisterDefinition, StateDefinition, StateMachineDefinition, TokenDefinition, TransitionDefinition"
        }
        MachineKind::Nfa => {
            "FixtureDefinition, GuardDefinition, InputDefinition, MachineKind, MatcherDefinition, NFA, RegisterDefinition, StateDefinition, StateMachineDefinition, TokenDefinition, TransitionDefinition"
        }
        MachineKind::Pda => {
            "FixtureDefinition, GuardDefinition, InputDefinition, MachineKind, MatcherDefinition, PushdownAutomaton, RegisterDefinition, StateDefinition, StateMachineDefinition, TokenDefinition, TransitionDefinition"
        }
        MachineKind::Transducer => {
            "EffectfulStateMachine, FixtureDefinition, GuardDefinition, InputDefinition, MachineKind, MatcherDefinition, RegisterDefinition, StateDefinition, StateMachineDefinition, TokenDefinition, TransitionDefinition"
        }
        _ => unreachable!("unsupported kinds are rejected before source emission"),
    }
}

fn machine_kind_variant(definition: &StateMachineDefinition) -> &'static str {
    match definition.kind {
        MachineKind::Dfa => "Dfa",
        MachineKind::Nfa => "Nfa",
        MachineKind::Pda => "Pda",
        MachineKind::Transducer => "Transducer",
        _ => unreachable!("unsupported kinds are rejected before source emission"),
    }
}

fn machine_suffix(definition: &StateMachineDefinition) -> &'static str {
    match definition.kind {
        MachineKind::Dfa => "dfa",
        MachineKind::Nfa => "nfa",
        MachineKind::Pda => "pda",
        MachineKind::Transducer => "transducer",
        _ => unreachable!("unsupported kinds are rejected before source emission"),
    }
}

fn rust_string_vec(values: &[String], indent: usize) -> String {
    if values.is_empty() {
        return "Vec::new()".to_string();
    }

    let mut out = String::from("vec![\n");
    let padding = " ".repeat(indent + 4);
    for value in values {
        out.push_str(&padding);
        out.push_str(&rust_to_string_expr(value));
        out.push_str(",\n");
    }
    out.push_str(&" ".repeat(indent));
    out.push(']');
    out
}

fn rust_option_string(value: &Option<String>) -> String {
    match value {
        Some(value) => format!("Some({})", rust_to_string_expr(value)),
        None => "None".to_string(),
    }
}

fn rust_option_matcher(value: &Option<MatcherDefinition>, indent: usize) -> String {
    match value {
        Some(value) => format!("Some({})", rust_matcher_expr(value, indent)),
        None => "None".to_string(),
    }
}

fn rust_matcher_expr(value: &MatcherDefinition, indent: usize) -> String {
    match value {
        MatcherDefinition::Literal(value) => {
            format!("MatcherDefinition::Literal({})", rust_to_string_expr(value))
        }
        MatcherDefinition::Eof => "MatcherDefinition::Eof".to_string(),
        MatcherDefinition::Anything => "MatcherDefinition::Anything".to_string(),
        MatcherDefinition::Class(value) => {
            format!("MatcherDefinition::Class({})", rust_to_string_expr(value))
        }
        MatcherDefinition::OneOf(value) => {
            format!("MatcherDefinition::OneOf({})", rust_to_string_expr(value))
        }
        MatcherDefinition::Range { start, end } => format!(
            "MatcherDefinition::Range {{ start: {}, end: {} }}",
            rust_to_string_expr(start),
            rust_to_string_expr(end)
        ),
        MatcherDefinition::AnyOfClasses(values) => format!(
            "MatcherDefinition::AnyOfClasses({})",
            rust_string_vec(values, indent)
        ),
        MatcherDefinition::Lookahead(inner) => format!(
            "MatcherDefinition::Lookahead(Box::new({}))",
            rust_matcher_expr(inner, indent)
        ),
    }
}

fn rust_to_string_expr(value: &str) -> String {
    format!("{}.to_string()", rust_string_literal(value))
}

fn rust_string_literal(value: &str) -> String {
    let mut out = String::from("\"");
    for ch in value.chars() {
        match ch {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            '\0' => out.push_str("\\0"),
            ch if ch.is_control() => {
                out.push_str("\\u{");
                out.push_str(&format!("{:X}", ch as u32));
                out.push('}');
            }
            ch => out.push(ch),
        }
    }
    out.push('"');
    out
}

fn comment_text(value: &str) -> String {
    let mut out = String::new();
    for ch in value.chars() {
        match ch {
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            ch if ch.is_control() => {
                out.push_str("\\u{");
                out.push_str(&format!("{:X}", ch as u32));
                out.push('}');
            }
            ch => out.push(ch),
        }
    }
    out
}

fn rust_identifier(source: &str) -> Result<String> {
    let mut ident = String::new();
    let mut previous_underscore = false;
    for ch in source.chars() {
        if ch.is_ascii_alphanumeric() {
            ident.push(ch.to_ascii_lowercase());
            previous_underscore = false;
        } else if !previous_underscore && !ident.is_empty() {
            ident.push('_');
            previous_underscore = true;
        }
    }
    while ident.ends_with('_') {
        ident.pop();
    }
    if ident.is_empty() {
        return Err(StateMachineSourceError::EmptyGeneratedIdentifier {
            source: source.to_string(),
        });
    }
    if ident.as_bytes()[0].is_ascii_digit() {
        ident.insert_str(0, "machine_");
    }
    if is_rust_reserved(&ident) {
        return Err(StateMachineSourceError::ReservedIdentifier { identifier: ident });
    }
    Ok(ident)
}

fn is_rust_reserved(identifier: &str) -> bool {
    matches!(
        identifier,
        "as" | "async"
            | "await"
            | "become"
            | "box"
            | "break"
            | "const"
            | "continue"
            | "crate"
            | "do"
            | "dyn"
            | "else"
            | "enum"
            | "extern"
            | "false"
            | "final"
            | "fn"
            | "for"
            | "if"
            | "impl"
            | "in"
            | "let"
            | "loop"
            | "macro"
            | "match"
            | "mod"
            | "move"
            | "mut"
            | "override"
            | "priv"
            | "pub"
            | "ref"
            | "return"
            | "Self"
            | "self"
            | "static"
            | "struct"
            | "super"
            | "trait"
            | "true"
            | "try"
            | "type"
            | "typeof"
            | "unsafe"
            | "unsized"
            | "use"
            | "virtual"
            | "where"
            | "while"
            | "yield"
    )
}

fn bool_literal(value: bool) -> &'static str {
    if value {
        "true"
    } else {
        "false"
    }
}

fn sorted_strings(values: &[String]) -> Vec<String> {
    let mut sorted = values.to_vec();
    sorted.sort();
    sorted
}
