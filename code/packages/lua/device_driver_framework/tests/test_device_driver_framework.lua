describe("SimulatedNIC", function()
  it("new creates NIC with defaults", function()
    local n = DDF.SimulatedNIC.new()
    assert.are.equal(n.name, "eth0")
    assert.are.equal(n.device_type, DDF.TYPE_NETWORK)
    assert.are.equal(n.major, 5)
    assert.are.equal(n.minor, 0)
    assert.are.equal(#n.mac_address, 6)
    assert.is_false(n.initialized)
  end)

  it("initialize, open, close", function()
    local n = DDF.SimulatedNIC.new()
    local _, n2 = n:initialize()
    local _, n3 = n2:open()
    assert.are.equal(n3.open_count, 1)
    local st, n4 = n3:close()
    assert.are.equal(st, "ok")
    assert.are.equal(n4.open_count, 0)
  end)

  it("open before initialize returns error", function()
    local n = DDF.SimulatedNIC.new()
    local st, _ = n:open()
    assert.are.equal(st, "not_initialized")
  end)

  it("send and tx_queue", function()
    local n = DDF.SimulatedNIC.new()
    local st, n2 = n:send({ 0xAA, 0xBB, 0xCC })
    assert.are.equal(st, "ok")
    assert.are.equal(#n2.tx_queue, 1)
    assert.are.same(n2.tx_queue[1], { 0xAA, 0xBB, 0xCC })
  end)

  it("inject_rx and receive", function()
    local n = DDF.SimulatedNIC.new()
    local n2 = n:inject_rx({ 0x01, 0x02, 0x03 })
    local st, n3, pkt = n2:receive()
    assert.are.equal(st, "ok")
    assert.are.same(pkt, { 0x01, 0x02, 0x03 })
    assert.are.equal(#n3.rx_queue, 0)
  end)

  it("receive from empty queue returns empty", function()
    local n = DDF.SimulatedNIC.new()
    local st, _, pkt = n:receive()
    assert.are.equal(st, "empty")
    assert.is_nil(pkt)
  end)

  it("multiple packets in rx_queue — FIFO order", function()
    local n = DDF.SimulatedNIC.new()
    local n2 = n:inject_rx({ 1 })
    local n3 = n2:inject_rx({ 2 })
    local n4 = n3:inject_rx({ 3 })
    local _, n5, p1 = n4:receive()
    local _, n6, p2 = n5:receive()
    local _, _, p3 = n6:receive()
    assert.are.same(p1, { 1 })
    assert.are.same(p2, { 2 })
    assert.are.same(p3, { 3 })
  end)

  it("ioctl get_mac", function()
    local mac = { 0x11, 0x22, 0x33, 0x44, 0x55, 0x66 }
    local n = DDF.SimulatedNIC.new({ mac_address = mac })
    local st, got = n:ioctl("get_mac")
    assert.are.equal(st, "ok")
    assert.are.same(got, mac)
  end)

  it("ioctl unsupported", function()
    local n = DDF.SimulatedNIC.new()
    local st, _ = n:ioctl("reset", nil)
    assert.are.equal(st, "unsupported")
  end)

  it("close when not open returns error", function()
    local n = DDF.SimulatedNIC.new()
    local _, n2 = n:initialize()
    local st, _ = n2:close()
    assert.are.equal(st, "not_open")
  end)
end)

-- ============================================================================
-- Registry tests
-- ============================================================================

describe("Registry", function()
  it("new creates empty registry", function()
    local r = DDF.Registry.new()
    assert.are.same(r:list(), {})
  end)

  it("register and get by name", function()
    local r = DDF.Registry.new()
    local disk = DDF.SimulatedDisk.new()
    local st, r2 = r:register(disk)
    assert.are.equal(st, "ok")
    local st2, d = r2:get("disk0")
    assert.are.equal(st2, "ok")
    assert.are.equal(d.name, "disk0")
  end)

  it("register returns already_registered for duplicate", function()
    local r = DDF.Registry.new()
    local disk = DDF.SimulatedDisk.new()
    local _, r2 = r:register(disk)
    local st, _ = r2:register(disk)
    assert.are.equal(st, "already_registered")
  end)

  it("get returns not_found for missing device", function()
    local r = DDF.Registry.new()
    local st, _ = r:get("nodev")
    assert.are.equal(st, "not_found")
  end)

  it("get_by_major_minor", function()
    local r = DDF.Registry.new()
    local disk = DDF.SimulatedDisk.new({ major = 8, minor = 0 })
    local _, r2 = r:register(disk)
    local st, d = r2:get_by_major_minor(8, 0)
    assert.are.equal(st, "ok")
    assert.are.equal(d.name, "disk0")
  end)

  it("get_by_major_minor returns not_found", function()
    local r = DDF.Registry.new()
    local st, _ = r:get_by_major_minor(99, 0)
    assert.are.equal(st, "not_found")
  end)

  it("update replaces device", function()
    local r = DDF.Registry.new()
    local disk = DDF.SimulatedDisk.new({ block_size = 4, total_blocks = 4 })
    local _, r2 = r:register(disk)
    local _, d2 = disk:write_block(0, "ABCD")
    local st, r3 = r2:update("disk0", d2)
    assert.are.equal(st, "ok")
    local _, d3 = r3:get("disk0")
    local _, _, data = d3:read_block(0)
    assert.are.equal(data, "ABCD")
  end)

  it("update returns not_found for missing device", function()
    local r = DDF.Registry.new()
    local disk = DDF.SimulatedDisk.new()
    local st, _ = r:update("disk0", disk)
    assert.are.equal(st, "not_found")
  end)

  it("unregister removes device", function()
    local r = DDF.Registry.new()
    local disk = DDF.SimulatedDisk.new()
    local _, r2 = r:register(disk)
    local st, r3 = r2:unregister("disk0")
    assert.are.equal(st, "ok")
    local rs, _ = r3:get("disk0")
    assert.are.equal(rs, "not_found")
    -- major:minor also removed
    local rs2, _ = r3:get_by_major_minor(3, 0)
    assert.are.equal(rs2, "not_found")
  end)

  it("unregister returns not_found for missing device", function()
    local r = DDF.Registry.new()
    local st, _ = r:unregister("nodev")
    assert.are.equal(st, "not_found")
  end)

  it("list returns sorted device names", function()
    local r = DDF.Registry.new()
    local disk   = DDF.SimulatedDisk.new({ name = "disk0", major = 3, minor = 0 })
    local serial = DDF.SimulatedSerial.new({ name = "serial0", major = 4, minor = 0 })
    local nic    = DDF.SimulatedNIC.new({ name = "eth0", major = 5, minor = 0 })
    local _, r2 = r:register(disk)
    local _, r3 = r2:register(serial)
    local _, r4 = r3:register(nic)
    local names = r4:list()
    assert.are.same(names, { "disk0", "eth0", "serial0" })
  end)

  it("immutability: register does not mutate original", function()
    local r = DDF.Registry.new()
    local disk = DDF.SimulatedDisk.new()
    r:register(disk)
    assert.are.same(r:list(), {})
  end)

  it("full driver lifecycle: init → open → write → read → close", function()
    local disk = DDF.SimulatedDisk.new({ block_size = 8, total_blocks = 4 })
    local _, d2 = disk:initialize()
    local _, d3 = d2:open()
    local _, d4 = d3:write_block(1, "HELLO!!!")
    local _, _, data = d4:read_block(1)
    assert.are.equal(data, "HELLO!!!")
    local st, _ = d4:close()
    assert.are.equal(st, "ok")
  end)
end)
