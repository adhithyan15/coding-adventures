async function writeFiles(root: string, files: FileMap, force: boolean): Promise<void> {
  for (const [relativePath, content] of Object.entries(files)) {
    const absolutePath = path.join(root, relativePath);
    await fs.mkdir(path.dirname(absolutePath), { recursive: true });

    if (!force) {
      try {
        await fs.stat(absolutePath);
        throw new Error(`refusing to overwrite existing file ${relativePath}; pass --force to overwrite`);
      } catch (error) {
        if ((error as NodeJS.ErrnoException).code !== "ENOENT") {
          throw error;
        }
      }
    }

    await fs.writeFile(absolutePath, content);
  }
}

export async function generateVisualizationApp(options: GenerateVisualizationOptions, force = false): Promise<void> {
  assertKebab(options.name, "name");
  const pagesSlug = options.pagesSlug ?? options.name;
  assertKebab(pagesSlug, "pages slug");
  const packageDeps = [...new Set(options.packageDeps)].sort();

  const programRoot = appDir(options.repoRoot, options.name);
  const files: FileMap = {
    ...commonFiles(options.name, options.description),
    "package.json": rendererPackageJson(options.name, options.description, packageDeps),
    "tsconfig.json": rendererTsConfig(),
    "vite.config.ts": rendererViteConfig(pagesSlug),
    "vitest.config.ts": rendererVitestConfig(),
    "index.html": rendererIndexHtml(titleFromKebab(options.name)),
    "BUILD": rendererBuild(options.name, packageDeps),
    "src/vite-env.d.ts": rendererViteEnv(),
    "src/main.tsx": rendererMainTsx(),
    "src/App.tsx": rendererAppTsx(options.name, options.description),
    "src/styles/app.lattice": rendererLatticeStyles(),
    "src/styles/installLatticeStyles.ts": rendererLatticeInstaller(),
  };

  await writeFiles(programRoot, files, force);

  const workflow = visualizationWorkflow(options.name, pagesSlug, packageDeps);
  await writeFiles(options.repoRoot, {
    [repoRelative(workflowPath(options.repoRoot, "visualization", pagesSlug), options.repoRoot)]: workflow,
  }, force);
}

export async function generateElectronWrapper(options: GenerateElectronWrapperOptions, force = false): Promise<void> {
  assertKebab(options.name, "name");
  assertKebab(options.rendererApp, "renderer app");
  const productName = options.productName ?? titleFromKebab(options.name);
  const appId = options.appId ?? `com.codingadventures.${joinedLower(options.name)}`;
  const tagPrefix = options.tagPrefix ?? options.name;
  const rendererPackageDeps = [...new Set(options.rendererPackageDeps)].sort();

  const programRoot = appDir(options.repoRoot, options.name);
  const files: FileMap = {
    ...commonFiles(options.name, options.description),
    "package.json": electronPackageJson(options.name, options.description),
    "tsconfig.json": rendererTsConfig(),
    "BUILD": electronBuild(),
    "electron/tsconfig.json": electronTsConfig(),
    "electron/main.ts": electronMainTs(productName),
    "electron-builder.yml": electronBuilderYaml(appId, productName),
  };

  await writeFiles(programRoot, files, force);

  const workflow = electronWorkflow(options.name, tagPrefix, options.rendererApp, rendererPackageDeps);
  await writeFiles(options.repoRoot, {
    [repoRelative(workflowPath(options.repoRoot, "electron-wrapper", tagPrefix), options.repoRoot)]: workflow,
  }, force);
}

export async function findRepoRoot(startDir = process.cwd()): Promise<string> {
  let current = startDir;

  while (true) {
    try {
      await fs.stat(path.join(current, ".github"));
      await fs.stat(path.join(current, "code"));
      return current;
    } catch {
      const parent = path.dirname(current);
      if (parent === current) {
        throw new Error("could not find repo root");
      }
      current = parent;
    }
  }
}

export function parseCommand(argv: string[]): ParsedCommand {
  if (argv.length === 0) {
    throw new Error("usage: web-app-scaffold-generator <name> [--template visualization|electron-wrapper] [options]");
  }

  const name = argv[0]!;
  assertKebab(name, "name");

  const options = new Map<string, string | boolean>();
  for (let index = 1; index < argv.length; index += 1) {
    const token = argv[index]!;
    if (!token.startsWith("--")) {
      throw new Error(`unexpected positional argument "${token}"`);
    }

    const key = token.slice(2);
    if (key === "force") {
      options.set(key, true);
      continue;
    }

    const value = argv[index + 1];
    if (value === undefined || value.startsWith("--")) {
      throw new Error(`missing value for --${key}`);
    }
    options.set(key, value);
    index += 1;
  }

  const template = (options.get("template") as AppTemplate | undefined) ?? "visualization";
  if (template !== "visualization" && template !== "electron-wrapper") {
    throw new Error(`unknown template "${String(options.get("template"))}"`);
  }

  return {
    name,
    template,
    description: String(options.get("description") ?? `Interactive ${titleFromKebab(name)} app`),
    pagesSlug: options.get("pages-slug") as string | undefined,
    packageDeps: parseCsv(options.get("package-deps") as string | undefined),
    rendererApp: options.get("renderer-app") as string | undefined,
    rendererPackageDeps: parseCsv(options.get("renderer-package-deps") as string | undefined),
    productName: options.get("product-name") as string | undefined,
    appId: options.get("app-id") as string | undefined,
    tagPrefix: options.get("tag-prefix") as string | undefined,
    force: options.get("force") === true,
  };
}

export async function runCli(argv: string[]): Promise<void> {
  const command = parseCommand(argv);
  const repoRoot = await findRepoRoot();

  if (command.template === "visualization") {
    await generateVisualizationApp({
      repoRoot,
      name: command.name,
      description: command.description,
      pagesSlug: command.pagesSlug,
      packageDeps: command.packageDeps,
    }, command.force);

    console.log(`Created visualization app at ${appDir(repoRoot, command.name)}`);
    return;
  }

  if (command.rendererApp === undefined) {
    throw new Error("--renderer-app is required for --template electron-wrapper");
  }

  await generateElectronWrapper({
    repoRoot,
    name: command.name,
    description: command.description,
    rendererApp: command.rendererApp,
    rendererPackageDeps: command.rendererPackageDeps,
    productName: command.productName,
    appId: command.appId,
    tagPrefix: command.tagPrefix,
  }, command.force);

  console.log(`Created electron-wrapper app at ${appDir(repoRoot, command.name)}`);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  runCli(process.argv.slice(2)).catch((error: unknown) => {
    const message = error instanceof Error ? error.message : String(error);
    console.error(`web-app-scaffold-generator: ${message}`);
    process.exit(1);
  });
}
