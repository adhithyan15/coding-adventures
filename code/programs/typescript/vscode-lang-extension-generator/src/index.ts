// =========================================================================
// generate — orchestrates the full filesystem write
// =========================================================================

/**
 * Write a complete extension package to `opts.outputDir`.  Validates
 * inputs, refuses to clobber a non-empty directory, then emits each
 * file via the corresponding builder.  All builders are pure
 * (string-in, string-out) so the file write is the only side effect.
 */
export function generate(opts: GeneratorOptions): void {
  validateOptions(opts);

  // Refuse to write into a non-empty directory.  This protects against
  // the common case of the user accidentally pointing at an existing
  // package and losing data.
  if (fs.existsSync(opts.outputDir)) {
    const entries = fs.readdirSync(opts.outputDir);
    if (entries.length > 0) {
      throw new Error(
        `output directory '${opts.outputDir}' is not empty ` +
          `(${entries.length} entries); refusing to clobber`,
      );
    }
  }

  fs.mkdirSync(opts.outputDir, { recursive: true });

  const writeFile = (rel: string, content: string): void => {
    const full = path.join(opts.outputDir, rel);
    fs.mkdirSync(path.dirname(full), { recursive: true });
    // `flag: 'wx'` makes the write fail noisily if the file already
    // exists, converting the TOCTOU race between the empty-dir check
    // and the file writes into a clear error rather than a silent
    // overwrite.  This matters when another process (or a symlink
    // attack) plants files into outputDir between the check and the
    // write.
    fs.writeFileSync(full, content, { encoding: "utf-8", flag: "wx" });
  };

  // Top-level files.
  writeFile("package.json", buildPackageJson(opts));
  writeFile("tsconfig.json", buildTsconfig());
  writeFile(".vscodeignore", buildVscodeignore());
  writeFile(".gitignore", buildGitignore());
  writeFile("README.md", buildReadme(opts));
  writeFile("CHANGELOG.md", buildChangelog(opts));
  writeFile("BUILD", buildBuildScript());
  writeFile("BUILD_windows", buildBuildScriptWindows());
  writeFile("language-configuration.json", buildLanguageConfiguration(opts));

  // src/ — extension.ts always; lsp.ts and dap.ts conditional.
  writeFile("src/extension.ts", buildExtensionTs(opts));
  if (opts.lspBinary) {
    writeFile("src/lsp.ts", buildLspTs(opts));
  }
  if (opts.dapBinary) {
    writeFile("src/dap.ts", buildDapTs(opts));
  }

  // syntaxes/ — only if keywords provided.
  const grammar = buildTextMateGrammar(opts);
  if (grammar !== null) {
    writeFile(`syntaxes/${opts.languageId}.tmLanguage.json`, grammar);
  }

  // examples/ — a stub source file demonstrating the file extension.
  writeFile(`examples/sample${opts.fileExtensions[0]}`, buildSampleSource(opts));
}

