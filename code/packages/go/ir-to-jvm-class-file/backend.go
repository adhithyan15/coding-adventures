func (l *jvmClassLowerer) buildSyscallMethod() (methodSpec, error) {
	builder := &bytecodeBuilder{}
	labelRead := l.freshLabel("sys_read")
	labelHalt := l.freshLabel("sys_halt")
	labelHaveInput := l.freshLabel("sys_have_input")

	l.emitILoad(builder, 0)
	l.emitPushInt(builder, 1)
	builder.emitBranch(opIfIcmpne, labelRead)
	builder.emitU2Instruction(opGetstatic, l.cp.fieldRef("java/lang/System", "out", "Ljava/io/PrintStream;"))
	l.emitRegGet(builder, 4)
	l.emitPushInt(builder, 0xFF)
	builder.emitOpcode(opIand)
	builder.emitU2Instruction(opInvokevirt, l.cp.methodRef("java/io/PrintStream", "write", descPrintStreamWrite))
	builder.emitU2Instruction(opGetstatic, l.cp.fieldRef("java/lang/System", "out", "Ljava/io/PrintStream;"))
	builder.emitU2Instruction(opInvokevirt, l.cp.methodRef("java/io/PrintStream", "flush", descNoArgsVoid))
	builder.emitOpcode(opReturn)

	builder.mark(labelRead)
	l.emitILoad(builder, 0)
	l.emitPushInt(builder, 2)
	builder.emitBranch(opIfIcmpne, labelHalt)
	builder.emitU2Instruction(opGetstatic, l.cp.fieldRef("java/lang/System", "in", "Ljava/io/InputStream;"))
	builder.emitU2Instruction(opInvokevirt, l.cp.methodRef("java/io/InputStream", "read", descInputStreamRead))
	l.emitIStore(builder, 1)
	l.emitILoad(builder, 1)
	l.emitPushInt(builder, -1)
	builder.emitBranch(opIfIcmpne, labelHaveInput)
	l.emitPushInt(builder, 4)
	l.emitPushInt(builder, 0)
	builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
	builder.emitOpcode(opReturn)

	builder.mark(labelHaveInput)
	l.emitPushInt(builder, 4)
	l.emitILoad(builder, 1)
	builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
	builder.emitOpcode(opReturn)

	builder.mark(labelHalt)
	builder.emitOpcode(opReturn)

	code, err := builder.assemble()
	if err != nil {
		return methodSpec{}, err
	}
	return methodSpec{
		AccessFlags: accPrivate | jvmclassfile.ACC_STATIC,
		Name:        l.helperSyscall,
		Descriptor:  "(I)V",
		Code:        code,
		MaxStack:    4,
		MaxLocals:   2,
	}, nil
}

func (l *jvmClassLowerer) buildCallableMethod(region callableRegion) (methodSpec, error) {
	builder := &bytecodeBuilder{}

	for _, instruction := range region.Instructions {
		switch instruction.Opcode {
		case ir.OpLabel:
			label, err := asLabel(instruction.Operands, 0, "LABEL operand")
			if err != nil {
				return methodSpec{}, err
			}
			builder.mark(label.Name)
		case ir.OpComment:
			continue
		case ir.OpNop:
			builder.emitOpcode(opNop)
		case ir.OpLoadImm:
			dst, err := asRegister(instruction.Operands, 0, "LOAD_IMM dst")
			if err != nil {
				return methodSpec{}, err
			}
			imm, err := asImmediate(instruction.Operands, 1, "LOAD_IMM immediate")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitPushInt(builder, dst.Index)
			l.emitPushInt(builder, imm.Value)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpLoadAddr:
			dst, err := asRegister(instruction.Operands, 0, "LOAD_ADDR dst")
			if err != nil {
				return methodSpec{}, err
			}
			label, err := asLabel(instruction.Operands, 1, "LOAD_ADDR label")
			if err != nil {
				return methodSpec{}, err
			}
			offset, ok := l.dataOffsets[label.Name]
			if !ok {
				return methodSpec{}, backendError("Unknown data label: %s", label.Name)
			}
			l.emitPushInt(builder, dst.Index)
			l.emitPushInt(builder, offset)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpLoadByte:
			dst, err := asRegister(instruction.Operands, 0, "LOAD_BYTE dst")
			if err != nil {
				return methodSpec{}, err
			}
			base, err := asRegister(instruction.Operands, 1, "LOAD_BYTE base")
			if err != nil {
				return methodSpec{}, err
			}
			offset, err := asRegister(instruction.Operands, 2, "LOAD_BYTE offset")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitPushInt(builder, dst.Index)
			l.emitRegGet(builder, base.Index)
			l.emitRegGet(builder, offset.Index)
			builder.emitOpcode(opIadd)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperMemLoadByte, descIntToInt))
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpStoreByte:
			src, err := asRegister(instruction.Operands, 0, "STORE_BYTE src")
			if err != nil {
				return methodSpec{}, err
			}
			base, err := asRegister(instruction.Operands, 1, "STORE_BYTE base")
			if err != nil {
				return methodSpec{}, err
			}
			offset, err := asRegister(instruction.Operands, 2, "STORE_BYTE offset")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitRegGet(builder, base.Index)
			l.emitRegGet(builder, offset.Index)
			builder.emitOpcode(opIadd)
			l.emitRegGet(builder, src.Index)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperMemStoreByte, descIntIntToVoid))
		case ir.OpLoadWord:
			dst, err := asRegister(instruction.Operands, 0, "LOAD_WORD dst")
			if err != nil {
				return methodSpec{}, err
			}
			base, err := asRegister(instruction.Operands, 1, "LOAD_WORD base")
			if err != nil {
				return methodSpec{}, err
			}
			offset, err := asRegister(instruction.Operands, 2, "LOAD_WORD offset")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitPushInt(builder, dst.Index)
			l.emitRegGet(builder, base.Index)
			l.emitRegGet(builder, offset.Index)
			builder.emitOpcode(opIadd)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperLoadWord, descIntToInt))
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpStoreWord:
			src, err := asRegister(instruction.Operands, 0, "STORE_WORD src")
			if err != nil {
				return methodSpec{}, err
			}
			base, err := asRegister(instruction.Operands, 1, "STORE_WORD base")
			if err != nil {
				return methodSpec{}, err
			}
			offset, err := asRegister(instruction.Operands, 2, "STORE_WORD offset")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitRegGet(builder, base.Index)
			l.emitRegGet(builder, offset.Index)
			builder.emitOpcode(opIadd)
			l.emitRegGet(builder, src.Index)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperStoreWord, descIntIntToVoid))
		case ir.OpAdd, ir.OpSub, ir.OpAnd:
			dst, err := asRegister(instruction.Operands, 0, instruction.Opcode.String()+" dst")
			if err != nil {
				return methodSpec{}, err
			}
			lhs, err := asRegister(instruction.Operands, 1, instruction.Opcode.String()+" lhs")
			if err != nil {
				return methodSpec{}, err
			}
			rhs, err := asRegister(instruction.Operands, 2, instruction.Opcode.String()+" rhs")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitPushInt(builder, dst.Index)
			l.emitRegGet(builder, lhs.Index)
			l.emitRegGet(builder, rhs.Index)
			switch instruction.Opcode {
			case ir.OpAdd:
				builder.emitOpcode(opIadd)
			case ir.OpSub:
				builder.emitOpcode(opIsub)
			default:
				builder.emitOpcode(opIand)
			}
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpAddImm, ir.OpAndImm:
			dst, err := asRegister(instruction.Operands, 0, instruction.Opcode.String()+" dst")
			if err != nil {
				return methodSpec{}, err
			}
			src, err := asRegister(instruction.Operands, 1, instruction.Opcode.String()+" src")
			if err != nil {
				return methodSpec{}, err
			}
			imm, err := asImmediate(instruction.Operands, 2, instruction.Opcode.String()+" imm")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitPushInt(builder, dst.Index)
			l.emitRegGet(builder, src.Index)
			l.emitPushInt(builder, imm.Value)
			if instruction.Opcode == ir.OpAddImm {
				builder.emitOpcode(opIadd)
			} else {
				builder.emitOpcode(opIand)
			}
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpCmpEq, ir.OpCmpNe, ir.OpCmpLt, ir.OpCmpGt:
			dst, err := asRegister(instruction.Operands, 0, instruction.Opcode.String()+" dst")
			if err != nil {
				return methodSpec{}, err
			}
			lhs, err := asRegister(instruction.Operands, 1, instruction.Opcode.String()+" lhs")
			if err != nil {
				return methodSpec{}, err
			}
			rhs, err := asRegister(instruction.Operands, 2, instruction.Opcode.String()+" rhs")
			if err != nil {
				return methodSpec{}, err
			}
			trueLabel := l.freshLabel("cmp_true")
			doneLabel := l.freshLabel("cmp_done")
			branchOpcode := map[ir.IrOp]byte{
				ir.OpCmpEq: opIfIcmpeq,
				ir.OpCmpNe: opIfIcmpne,
				ir.OpCmpLt: opIfIcmplt,
				ir.OpCmpGt: opIfIcmpgt,
			}[instruction.Opcode]
			l.emitPushInt(builder, dst.Index)
			l.emitRegGet(builder, lhs.Index)
			l.emitRegGet(builder, rhs.Index)
			builder.emitBranch(branchOpcode, trueLabel)
			l.emitPushInt(builder, 0)
			builder.emitBranch(opGoto, doneLabel)
			builder.mark(trueLabel)
			l.emitPushInt(builder, 1)
			builder.mark(doneLabel)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpJump:
			label, err := asLabel(instruction.Operands, 0, "JUMP target")
			if err != nil {
				return methodSpec{}, err
			}
			builder.emitBranch(opGoto, label.Name)
		case ir.OpBranchZ, ir.OpBranchNz:
			reg, err := asRegister(instruction.Operands, 0, instruction.Opcode.String()+" reg")
			if err != nil {
				return methodSpec{}, err
			}
			label, err := asLabel(instruction.Operands, 1, instruction.Opcode.String()+" target")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitRegGet(builder, reg.Index)
			if instruction.Opcode == ir.OpBranchZ {
				builder.emitBranch(opIfeq, label.Name)
			} else {
				builder.emitBranch(opIfne, label.Name)
			}
		case ir.OpCall:
			label, err := asLabel(instruction.Operands, 0, "CALL target")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitPushInt(builder, 1)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(label.Name, descNoArgsInt))
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperRegSet, descIntIntToVoid))
		case ir.OpRet, ir.OpHalt:
			l.emitRegGet(builder, 1)
			builder.emitOpcode(opIreturn)
		case ir.OpSyscall:
			number, err := asImmediate(instruction.Operands, 0, "SYSCALL number")
			if err != nil {
				return methodSpec{}, err
			}
			l.emitPushInt(builder, number.Value)
			builder.emitU2Instruction(opInvokestatic, l.methodRef(l.helperSyscall, "(I)V"))
		default:
			return methodSpec{}, backendError("Unsupported IR opcode in Go JVM backend: %s", instruction.Opcode.String())
		}
	}

	code, err := builder.assemble()
	if err != nil {
		return methodSpec{}, err
	}
	accessFlags := uint16(accPrivate | jvmclassfile.ACC_STATIC)
	if region.Name == l.program.EntryLabel {
		accessFlags = jvmclassfile.ACC_PUBLIC | jvmclassfile.ACC_STATIC
	}
	return methodSpec{
		AccessFlags: accessFlags,
		Name:        region.Name,
		Descriptor:  descNoArgsInt,
		Code:        code,
		MaxStack:    16,
		MaxLocals:   0,
	}, nil
}

