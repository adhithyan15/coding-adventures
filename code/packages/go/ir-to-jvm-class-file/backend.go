func cloneOffsets(input map[string]int) map[string]int {
	out := make(map[string]int, len(input))
	for key, value := range input {
		out[key] = value
	}
	return out
}

func sortStringsByIndex(values []string, indices map[string]int) {
	for left := 0; left < len(values); left++ {
		best := left
		for right := left + 1; right < len(values); right++ {
			if indices[values[right]] < indices[values[best]] {
				best = right
			}
		}
		values[left], values[best] = values[best], values[left]
	}
}

func backendError(format string, args ...any) error {
	return JvmBackendError{Message: fmt.Sprintf(format, args...)}
}

func u2(value uint16) []byte {
	var storage [2]byte
	binary.BigEndian.PutUint16(storage[:], value)
	return storage[:]
}

func u4(value uint32) []byte {
	var storage [4]byte
	binary.BigEndian.PutUint32(storage[:], value)
	return storage[:]
}

func i2(value int16) []byte {
	return u2(uint16(value))
}

func appendI2(buffer []byte, value int16) []byte {
	return append(buffer, i2(value)...)
}

func appendI4(buffer []byte, value int32) []byte {
	return append(buffer, u4(uint32(value))...)
}
