module GrammarTools.Compiler
    ( compileTokenGrammar
    , compileParserGrammar
    ) where

import Data.List (intercalate, sortBy)
import qualified Data.Map.Strict as Map
import GrammarTools.ParserGrammar
import GrammarTools.TokenGrammar

compileTokenGrammar :: TokenGrammar -> String -> String -> String
compileTokenGrammar grammar sourceFile moduleName =
    unlines
        [ "-- Code generated by grammar-tools. DO NOT EDIT."
        , sourceComment sourceFile
        , "module " ++ moduleName ++ " where"
        , ""
        , "import qualified Data.Map.Strict as Map"
        , "import GrammarTools.TokenGrammar"
        , ""
        , "tokenGrammarData :: TokenGrammar"
        , "tokenGrammarData ="
        , "    TokenGrammar"
        , "        { tokenGrammarVersion = " ++ show (tokenGrammarVersion grammar)
        , "        , tokenGrammarCaseInsensitive = " ++ show (tokenGrammarCaseInsensitive grammar)
        , "        , tokenGrammarDefinitions = " ++ compileTokenDefinitions (tokenGrammarDefinitions grammar) 8
        , "        , tokenGrammarKeywords = " ++ show (tokenGrammarKeywords grammar)
        , "        , tokenGrammarMode = " ++ compileMaybeString (tokenGrammarMode grammar)
        , "        , tokenGrammarSkipDefinitions = " ++ compileTokenDefinitions (tokenGrammarSkipDefinitions grammar) 8
        , "        , tokenGrammarReservedKeywords = " ++ show (tokenGrammarReservedKeywords grammar)
        , "        , tokenGrammarEscapeMode = " ++ compileMaybeString (tokenGrammarEscapeMode grammar)
        , "        , tokenGrammarErrorDefinitions = " ++ compileTokenDefinitions (tokenGrammarErrorDefinitions grammar) 8
        , "        , tokenGrammarGroups = " ++ compileGroups (tokenGrammarGroups grammar) 8
        , "        , tokenGrammarCaseSensitive = " ++ show (tokenGrammarCaseSensitive grammar)
        , "        , tokenGrammarContextKeywords = " ++ show (tokenGrammarContextKeywords grammar)
        , "        , tokenGrammarSoftKeywords = " ++ show (tokenGrammarSoftKeywords grammar)
        , "        }"
        ]

compileParserGrammar :: ParserGrammar -> String -> String -> String
compileParserGrammar grammar sourceFile moduleName =
    unlines
        [ "-- Code generated by grammar-tools. DO NOT EDIT."
        , sourceComment sourceFile
        , "module " ++ moduleName ++ " where"
        , ""
        , "import GrammarTools.ParserGrammar"
        , ""
        , "parserGrammarData :: ParserGrammar"
        , "parserGrammarData ="
        , "    ParserGrammar"
        , "        { parserGrammarVersion = " ++ show (parserGrammarVersion grammar)
        , "        , parserGrammarRules = " ++ compileRules (parserGrammarRules grammar) 8
        , "        }"
        ]

sourceComment :: String -> String
sourceComment sourceFile
    | null sourceFile = ""
    | otherwise = "-- Source: " ++ sanitize sourceFile

sanitize :: String -> String
sanitize = map replaceNewline
  where
    replaceNewline '\n' = '_'
    replaceNewline '\r' = '_'
    replaceNewline ch = ch

compileMaybeString :: Maybe String -> String
compileMaybeString maybeValue =
    case maybeValue of
        Nothing -> "Nothing"
        Just value -> "Just " ++ show value

compileTokenDefinitions :: [TokenDefinition] -> Int -> String
compileTokenDefinitions definitions indentLevel =
    compileList (map compileTokenDefinition definitions) indentLevel

compileTokenDefinition :: TokenDefinition -> String
compileTokenDefinition definition =
    "TokenDefinition "
        ++ show (tokenDefinitionName definition)
        ++ " "
        ++ show (tokenDefinitionPattern definition)
        ++ " "
        ++ show (tokenDefinitionIsRegex definition)
        ++ " "
        ++ show (tokenDefinitionLineNumber definition)
        ++ " "
        ++ "("
        ++ compileMaybeString (tokenDefinitionAlias definition)
        ++ ")"

compileGroups :: Map.Map String PatternGroup -> Int -> String
compileGroups groups indentLevel
    | Map.null groups = "Map.empty"
    | otherwise =
        "Map.fromList "
            ++ compileList
                [ "("
                    ++ show name
                    ++ ", PatternGroup "
                    ++ show (patternGroupName group)
                    ++ " "
                    ++ compileInlineList (map compileTokenDefinition (patternGroupDefinitions group))
                    ++ ")"
                | (name, group) <- sortBy (\(left, _) (right, _) -> compare left right) (Map.toList groups)
                ]
                indentLevel

compileRules :: [GrammarRule] -> Int -> String
compileRules rules indentLevel =
    compileList (map compileRule rules) indentLevel

compileRule :: GrammarRule -> String
compileRule rule =
    "GrammarRule "
        ++ show (grammarRuleName rule)
        ++ " "
        ++ compileElement (grammarRuleBody rule)
        ++ " "
        ++ show (grammarRuleLineNumber rule)

compileElement :: GrammarElement -> String
compileElement element =
    case element of
        RuleReference name isToken ->
            "(RuleReference " ++ show name ++ " " ++ show isToken ++ ")"
        Literal value ->
            "(Literal " ++ show value ++ ")"
        Sequence elements ->
            "(Sequence " ++ compileInlineList (map compileElement elements) ++ ")"
        Alternation choices ->
            "(Alternation " ++ compileInlineList (map compileElement choices) ++ ")"
        Repetition child ->
            "(Repetition " ++ compileElement child ++ ")"
        Optional child ->
            "(Optional " ++ compileElement child ++ ")"
        Group child ->
            "(Group " ++ compileElement child ++ ")"
        PositiveLookahead child ->
            "(PositiveLookahead " ++ compileElement child ++ ")"
        NegativeLookahead child ->
            "(NegativeLookahead " ++ compileElement child ++ ")"
        OneOrMoreRepetition child ->
            "(OneOrMoreRepetition " ++ compileElement child ++ ")"
        SeparatedRepetition child separator atLeastOne ->
            "(SeparatedRepetition "
                ++ compileElement child
                ++ " "
                ++ compileElement separator
                ++ " "
                ++ show atLeastOne
                ++ ")"

compileList :: [String] -> Int -> String
compileList items indentLevel
    | null items = "[]"
    | otherwise =
        "[\n"
            ++ intercalate ",\n" [replicate indentLevel ' ' ++ item | item <- items]
            ++ "\n"
            ++ replicate (max 0 (indentLevel - 4)) ' '
            ++ "]"

compileInlineList :: [String] -> String
compileInlineList items = "[" ++ intercalate ", " items ++ "]"
