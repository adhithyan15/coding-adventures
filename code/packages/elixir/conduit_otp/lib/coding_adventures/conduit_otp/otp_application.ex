defmodule CodingAdventures.ConduitOtp.OtpApplication do
  @moduledoc """
  Teaching topic: The OTP `Application` behaviour.

  ## What is an OTP Application?

  In Erlang/Elixir, an **Application** (capital A, OTP sense) is the unit
  of "thing the BEAM can start and stop together":

  - A bundle of code modules (`.beam` files) plus configuration.
  - A **top-level supervisor** that starts when the application starts.
  - A **name** (the `:app` key in `mix.exs`) that the BEAM runtime can
    look up in its application registry.

  When you call `Application.ensure_all_started(:my_app)`, the BEAM:
  1. Loads the `.app` resource file (generated by `mix compile`).
  2. Calls `YourApp.start(:normal, [])` (this module's `start/2`).
  3. Expects a `{:ok, pid}` where `pid` is a supervisor process.
  4. Links the supervisor to the BEAM application master.

  If the top-level supervisor ever dies without being restarted,
  the BEAM shuts down the application.

  ## mix.exs connection

  The `mod:` key in `mix.exs`'s `application/0` points to this module:

      def application do
        [mod: {CodingAdventures.ConduitOtp.OtpApplication, []}, ...]
      end

  ## Empty default supervisor

  We start an *empty* supervisor here. The framework's real supervision
  tree (RouteTable + Acceptor + WorkerSupervisor) is started *per-server*
  by `Server.start_link/2`. A library should not bind to a port at
  application load time — that is the application programmer's job.

  Compare with Phoenix: `YourApp.Application.start/2` starts Ecto.Repo,
  PubSub, and the HTTP endpoint. We deliberately stay minimal.
  """

  use Application

  @impl true
  def start(_type, _args) do
    # An empty `children` list is fine — we just need a top-level supervisor
    # so the OTP application has the required structure. Server.start_link/2
    # starts its own OtpSupervisor dynamically, under this root.
    children = []

    opts = [
      strategy: :one_for_one,
      name: CodingAdventures.ConduitOtp.RootSupervisor
    ]

    Supervisor.start_link(children, opts)
  end
end
