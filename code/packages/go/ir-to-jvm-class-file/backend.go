	accFinal   = 0x0010

	minInt8  = -128
	maxInt8  = 127
	minInt16 = -32768
	maxInt16 = 32767
	minInt32 = -2147483648
	maxInt32 = 2147483647
	maxUint8 = 255

	constantUTF8        = 1
	constantInteger     = 3
	constantClass       = 7
	constantString      = 8
	constantFieldref    = 9
	constantMethodref   = 10
	constantNameAndType = 12

	opNop          = 0x00
	opIconstM1     = 0x02
	opIconst0      = 0x03
	opIconst1      = 0x04
	opIconst2      = 0x05
	opIconst3      = 0x06
	opIconst4      = 0x07
	opIconst5      = 0x08
	opBipush       = 0x10
	opSipush       = 0x11
	opLdc          = 0x12
	opLdcW         = 0x13
	opIload        = 0x15
	opIload0       = 0x1A
	opIstore       = 0x36
	opIstore0      = 0x3B
	opIaload       = 0x2E
	opBaload       = 0x33
	opIastore      = 0x4F
	opBastore      = 0x54
	opPop          = 0x57
	opIadd         = 0x60
	opIsub         = 0x64
	opIshl         = 0x78
