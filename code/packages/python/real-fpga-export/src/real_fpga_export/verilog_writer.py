"""HIR -> structural Verilog backwriter.

Walks an HIR document and emits IEEE 1364-2005 Verilog. The synthesizable
subset is the focus for v0.1.0; behavioral processes (always blocks) and
testbench constructs are documented as v0.2.0.
"""

from __future__ import annotations

from io import StringIO
from pathlib import Path

from hdl_ir import (
    HIR,
    BinaryOp,
    Concat,
    ContAssign,
    Direction,
    Expr,
    Lit,
    Module,
    NetRef,
    PortRef,
    Replication,
    Slice,
    Ternary,
    TyLogic,
    TyVector,
    UnaryOp,
    VarRef,
)

VERILOG_RESERVED = frozenset({
    "always", "and", "assign", "begin", "buf", "case", "casex", "casez",
    "default", "defparam", "else", "end", "endcase", "endfunction",
    "endgenerate", "endmodule", "endtask", "for", "function", "generate",
    "genvar", "if", "initial", "inout", "input", "integer", "localparam",
    "module", "nand", "negedge", "nor", "not", "or", "output", "parameter",
    "posedge", "real", "reg", "signed", "supply0", "supply1", "task",
    "tri", "unsigned", "wire", "xnor", "xor",
})


_BINOP_TO_VERILOG = {
    "+": "+", "-": "-", "*": "*", "/": "/", "%": "%", "**": "**",
    "AND": "&", "OR": "|", "XOR": "^", "NAND": "~&", "NOR": "~|", "XNOR": "~^",
    "&": "&", "|": "|", "^": "^",
    "<<": "<<", ">>": ">>", "<<<": "<<<", ">>>": ">>>",
    "<": "<", "<=": "<=", ">": ">", ">=": ">=",
    "==": "==", "!=": "!=", "===": "===", "!==": "!==",
    "&&": "&&", "||": "||",
}

_UNARY_TO_VERILOG = {
    "NOT": "~", "NEG": "-", "POS": "+", "LOGIC_NOT": "!",
    "AND_RED": "&", "OR_RED": "|", "XOR_RED": "^",
    "NAND_RED": "~&", "NOR_RED": "~|", "XNOR_RED": "~^",
}


def write_verilog(hir: HIR, path: str | Path) -> None:
    """Emit a Verilog file containing every Module in the HIR."""
    Path(path).write_text(write_verilog_str(hir))


def write_verilog_str(hir: HIR) -> str:
    """Return the Verilog source as a string."""
    out = StringIO()
    out.write("// Generated by real-fpga-export 0.1.0\n")
    out.write(f"// HIR top: {hir.top}\n\n")
    for _name, mod in hir.modules.items():
        out.write(_module_to_verilog(mod))
        out.write("\n")
    return out.getvalue()


def _module_to_verilog(mod: Module) -> str:
    out = StringIO()
    out.write(f"module {_escape_id(mod.name)} (\n")

    # Port list
    port_lines = []
    for p in mod.ports:
        dir_str = (
            "input" if p.direction == Direction.IN
            else "output" if p.direction == Direction.OUT
            else "inout"
        )
        range_str = _type_to_range(p.type)
        port_lines.append(f"  {dir_str} {range_str}{_escape_id(p.name)}".rstrip())
    out.write(",\n".join(port_lines))
    out.write("\n);\n")

    # Internal nets
    for net in mod.nets:
        range_str = _type_to_range(net.type)
        out.write(f"  wire {range_str}{_escape_id(net.name)};\n")

    # Continuous assignments
    for ca in mod.cont_assigns:
        out.write(f"  {_continuous_assign_to_verilog(ca)};\n")

    # Instances
    for inst in mod.instances:
        out.write(_instance_to_verilog(inst))

    out.write("endmodule\n")
    return out.getvalue()


def _type_to_range(ty: object) -> str:
    """Convert a Ty into a Verilog range prefix string. Empty for 1-bit."""
    if isinstance(ty, TyVector):
        return f"[{ty.width - 1}:0] "
    if isinstance(ty, TyLogic):
        return ""
    return ""  # other types: best-effort empty


def _continuous_assign_to_verilog(ca: ContAssign) -> str:
    target_str = _expr_to_verilog(ca.target)
    rhs_str = _expr_to_verilog(ca.rhs)
    return f"assign {target_str} = {rhs_str}"


def _instance_to_verilog(inst: object) -> str:
    """Emit one instance line."""
    inst_name = _escape_id(inst.name)  # type: ignore[union-attr]
    cell_type = _escape_id(inst.module)  # type: ignore[union-attr]

    parts = [f"  {cell_type}"]

    # Parameters
    if inst.parameters:  # type: ignore[union-attr]
        params = []
        for k, v in inst.parameters.items():  # type: ignore[union-attr]
            params.append(f".{_escape_id(k)}({_expr_to_verilog(v)})")
        parts.append("#(" + ", ".join(params) + ")")

    parts.append(inst_name)

    # Connections
    if inst.connections:  # type: ignore[union-attr]
        conns = []
        for k, v in inst.connections.items():  # type: ignore[union-attr]
            conns.append(f".{_escape_id(k)}({_expr_to_verilog(v)})")
        parts.append("(" + ", ".join(conns) + ")")
    else:
        parts.append("()")

    return " ".join(parts) + ";\n"


def _expr_to_verilog(expr: Expr) -> str:
    """Recursively convert an HIR expression to Verilog source text."""
    if isinstance(expr, Lit):
        return _lit_to_verilog(expr)
    if isinstance(expr, NetRef):
        return _escape_id(expr.name)
    if isinstance(expr, PortRef):
        return _escape_id(expr.name)
    if isinstance(expr, VarRef):
        return _escape_id(expr.name)
    if isinstance(expr, Slice):
        base = _expr_to_verilog(expr.base)
        if expr.msb == expr.lsb:
            return f"{base}[{expr.msb}]"
        return f"{base}[{expr.msb}:{expr.lsb}]"
    if isinstance(expr, Concat):
        parts = ", ".join(_expr_to_verilog(p) for p in expr.parts)
        return "{" + parts + "}"
    if isinstance(expr, Replication):
        return f"{{{_expr_to_verilog(expr.count)}{{{_expr_to_verilog(expr.body)}}}}}"
    if isinstance(expr, UnaryOp):
        op = _UNARY_TO_VERILOG.get(expr.op, "?")
        return f"({op}{_expr_to_verilog(expr.operand)})"
    if isinstance(expr, BinaryOp):
        op = _BINOP_TO_VERILOG.get(expr.op, "?")
        return f"({_expr_to_verilog(expr.lhs)} {op} {_expr_to_verilog(expr.rhs)})"
    if isinstance(expr, Ternary):
        return (
            f"({_expr_to_verilog(expr.cond)} ? "
            f"{_expr_to_verilog(expr.then_expr)} : "
            f"{_expr_to_verilog(expr.else_expr)})"
        )
    return f"/* unsupported expr: {type(expr).__name__} */"


def _lit_to_verilog(lit: Lit) -> str:
    v = lit.value
    if isinstance(v, bool):
        return "1'b1" if v else "1'b0"
    if isinstance(v, int):
        if isinstance(lit.type, TyVector):
            return f"{lit.type.width}'d{v}"
        return str(v)
    if isinstance(v, tuple):
        # Pack as a binary literal
        bits = "".join(str(int(b)) for b in v)
        return f"{len(v)}'b{bits}"
    if isinstance(v, str):
        # Treat as a string literal
        return f'"{v}"'
    if isinstance(v, float):
        return str(v)
    return "/* unknown lit */"


def _escape_id(name: str) -> str:
    """Escape a Verilog identifier if needed (reserved word or special char)."""
    if name in VERILOG_RESERVED:
        return f"\\{name} "
    if not name:
        return name
    if not (name[0].isalpha() or name[0] == "_"):
        return f"\\{name} "
    if not all(c.isalnum() or c in "_$" for c in name):
        return f"\\{name} "
    return name
