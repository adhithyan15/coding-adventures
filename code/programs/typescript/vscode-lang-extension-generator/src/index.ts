/**
 * Inputs to the generator, validated by `validateOptions`.
 *
 * Every field is required at the type level; "absent" is encoded as the
 * empty string for strings and the empty array for arrays.  This makes
 * the validation rules explicit rather than relying on `undefined`
 * sentinels that can be confused with missing properties.
 */
export interface GeneratorOptions {
  /** Internal language slug, must match `^[a-z][a-z0-9-]*$`. */
  languageId: string;
  /** Display name shown in the VS Code UI (e.g. "Twig"). */
  languageName: string;
  /** Source file extensions; each must start with `.`. */
  fileExtensions: string[];
  /** LSP server binary name; empty means LSP not wired. */
  lspBinary: string;
  /** DAP adapter binary name; empty means DAP not wired. */
  dapBinary: string;
  /** Directory to create.  Must not exist or must be empty. */
  outputDir: string;
  /** Single-line comment prefix; empty means no line comment. */
  lineComment: string;
  /** Block comment opening.  Both start and end required, or neither. */
  blockCommentStart: string;
  /** Block comment closing. */
  blockCommentEnd: string;
  /** Optional keyword list for a minimal TextMate grammar. */
  keywords: string[];
  /** Extension description (autogenerated if empty). */
  description: string;
  /** Extension semver. */
  extensionVersion: string;
}

// =========================================================================
// Validation
// =========================================================================

/** Thrown by `validateOptions` when an invariant is violated. */
export class ValidationError extends Error {
  constructor(message: string) {
    super(message);
    this.name = "ValidationError";
  }
}

/** A language id like "my-lang-2" — lowercase, starts with a letter. */
const LANGUAGE_ID_RE = /^[a-z][a-z0-9-]*$/;

/**
 * A file extension shape — leading dot, then one or more "filename
 * character" runs.  Tightly drawn so a malicious `--language-spec`
 * JSON cannot smuggle path separators or `..` segments through the
 * generator and onto disk.
 *
 * Pre-LS04.1 the validator only checked "starts with `.`, no
 * whitespace".  That let `"./../../etc"` slip past;
 * since `fileExtensions[0]` flows into `path.join(outputDir,
 * "examples/sample" + ext)`, an attacker-controlled spec file could
 * coerce the generator into writing the sample file outside
 * `outputDir`.  This regex closes that.
 *
 * The character class covers every real-world file extension I know
 * of: `.c`, `.cpp`, `.c++`, `.h`, `.rs`, `.ts`, `.tsx`, `.f90`,
 * `.r6`, `.go`, `.py`, `.twig`, `.tw`, `.tar.gz` (multi-dot is
 * permitted; embedded dots are fine).
 *
 * The shape `^\.[alnum_+-][alnum_+.-]*$` enforces a non-dot leading
 * character so values like `".."` and `"..."` are rejected.
 */
const FILE_EXTENSION_RE = /^\.[A-Za-z0-9_+-][A-Za-z0-9_+.-]*$/;

/**
 * Binary names — alphanumeric plus hyphens, underscores, dots, and
 * forward slashes (the latter two for relative paths).  We deliberately
 * exclude characters that have meaning in shells or in JS string
 * literals (quotes, backticks, semicolons, parens, dollar signs,
 * backslashes, whitespace) since these names get written into source
 * code that VS Code executes.
 */
const BINARY_NAME_RE = /^[A-Za-z0-9_./-]+$/;

/**
 * `languageName` and `description` flow into source-code string
 * literals.  Reject characters that could break out of the literal
 * (quotes, backticks, backslashes, newlines) or open a template
 * substitution (`${`).  See `buildLspTs`/`buildDapTs` — even though
 * we now use `JSON.stringify` to embed these values (defense in
 * depth), keeping the inputs sanitised gives a clear surface.
 */
const FREE_TEXT_FORBIDDEN_RE = /["'`\\\r\n]|\$\{/;

/**
 * Throws `ValidationError` if `opts` violates any spec rule.  Otherwise
 * returns silently.  Pure — does not touch the filesystem.
 */
export function validateOptions(opts: GeneratorOptions): void {
  if (!opts.languageId) {
    throw new ValidationError("languageId is required");
  }
  if (!LANGUAGE_ID_RE.test(opts.languageId)) {
    throw new ValidationError(
      `languageId '${opts.languageId}' must match ${LANGUAGE_ID_RE} ` +
        `(lowercase, starts with a letter, hyphens allowed inside)`,
    );
  }
  if (!opts.languageName) {
    throw new ValidationError("languageName is required");
  }
  if (FREE_TEXT_FORBIDDEN_RE.test(opts.languageName)) {
    throw new ValidationError(
      `languageName must not contain quotes, backticks, backslashes, ` +
        `newlines, or template-substitution syntax`,
    );
  }
  if (opts.description && FREE_TEXT_FORBIDDEN_RE.test(opts.description)) {
    throw new ValidationError(
      `description must not contain quotes, backticks, backslashes, ` +
        `newlines, or template-substitution syntax`,
    );
  }
  if (opts.lspBinary && !BINARY_NAME_RE.test(opts.lspBinary)) {
    throw new ValidationError(
      `lspBinary '${opts.lspBinary}' must match ${BINARY_NAME_RE} ` +
        `(alphanumerics, dots, underscores, hyphens, forward slashes)`,
    );
  }
  if (opts.dapBinary && !BINARY_NAME_RE.test(opts.dapBinary)) {
    throw new ValidationError(
      `dapBinary '${opts.dapBinary}' must match ${BINARY_NAME_RE} ` +
        `(alphanumerics, dots, underscores, hyphens, forward slashes)`,
    );
  }
  if (opts.fileExtensions.length === 0) {
    throw new ValidationError("at least one fileExtension is required");
  }
  for (const ext of opts.fileExtensions) {
    if (!ext.startsWith(".")) {
      throw new ValidationError(
        `fileExtension '${ext}' must start with a dot (e.g. '.twig')`,
      );
    }
    if (!FILE_EXTENSION_RE.test(ext)) {
      throw new ValidationError(
        `fileExtension '${ext}' must match ${FILE_EXTENSION_RE} ` +
          `(leading dot, then alphanumerics, underscore, plus, dot, hyphen). ` +
          `Path separators and '..' are rejected to prevent the spec-driven ` +
          `flow from writing files outside --output-dir.`,
      );
    }
    // Belt-and-braces: even though FILE_EXTENSION_RE excludes them,
    // call out `..` segments explicitly so the error message is clear
    // when the input is a malicious extension like ".." or ".a.b..c".
    if (ext.split(".").includes("..")) {
      throw new ValidationError(
        `fileExtension '${ext}' contains a '..' path segment`,
      );
    }
  }
  if (!opts.lspBinary && !opts.dapBinary) {
    throw new ValidationError(
      "at least one of lspBinary or dapBinary must be provided " +
        "(otherwise the extension would do nothing)",
    );
  }
  if (!opts.extensionVersion) {
    throw new ValidationError("extensionVersion is required");
  }
}

// =========================================================================
// Helpers
// =========================================================================

/**
 * Escape a string so it can be safely embedded in a regex.  Used by the
 * TextMate grammar builder to keep keywords like `a.b` from matching
 * `axb`.  The set of metacharacters we escape is the standard JS regex
 * special set.
 */
export function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

/**
 * Stable JSON pretty-printer.  Object keys serialise in insertion order,
 * which is good for determinism: each builder constructs the object
 * with keys in a fixed sequence, so the resulting JSON is byte-stable.
 */
function stableJson(value: unknown): string {
  return JSON.stringify(value, null, 2) + "\n";
}

/**
 * Generate a default description if the user didn't supply one.
 */
function defaultDescription(opts: GeneratorOptions): string {
  const features: string[] = [];
  if (opts.lspBinary) features.push("language intelligence");
  if (opts.dapBinary) features.push("debugging");
  return `${opts.languageName} ${features.join(" and ")} for VS Code`;
}

