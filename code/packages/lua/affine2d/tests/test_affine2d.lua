-- Tests for coding_adventures.affine2d
package.path = "../src/?.lua;../src/?/init.lua;../../trig/src/?.lua;../../trig/src/?/init.lua;../../point2d/src/?.lua;../../point2d/src/?/init.lua;" .. package.path

local aff = require("coding_adventures.affine2d")
local trig = require("coding_adventures.trig")

local DELTA = 1e-9
local function approx(a, b) return math.abs(a - b) < DELTA end
local function pt_approx(a, b) return approx(a.x, b.x) and approx(a.y, b.y) end

describe("affine2d", function()
    it("identity apply", function()
        local pt = aff.apply_to_point(aff.identity(), {x=3, y=4})
        assert.is_true(pt_approx(pt, {x=3, y=4}))
    end)
    it("translate", function()
        local pt = aff.apply_to_point(aff.translate(2, 3), {x=1, y=1})
        assert.is_true(pt_approx(pt, {x=3, y=4}))
    end)
    it("rotate 90", function()
        local pt = aff.apply_to_point(aff.rotate(trig.PI / 2), {x=1, y=0})
        assert.is_true(approx(pt.x, 0) and approx(pt.y, 1))
    end)
    it("scale", function()
        local pt = aff.apply_to_point(aff.scale(2, 3), {x=1, y=1})
        assert.is_true(pt_approx(pt, {x=2, y=3}))
    end)
    it("scale uniform", function()
        local pt = aff.apply_to_point(aff.scale_uniform(5), {x=2, y=3})
        assert.is_true(pt_approx(pt, {x=10, y=15}))
    end)
    it("compose translations", function()
        local m = aff.compose(aff.translate(1, 0), aff.translate(0, 2))
        local pt = aff.apply_to_point(m, {x=0, y=0})
        assert.is_true(pt_approx(pt, {x=1, y=2}))
    end)
    it("determinant identity", function()
        assert.is_true(approx(aff.determinant(aff.identity()), 1))
    end)
    it("determinant scale", function()
        assert.is_true(approx(aff.determinant(aff.scale(2, 3)), 6))
    end)
    it("invert identity", function()
        local inv = aff.invert(aff.identity())
        assert.is_not_nil(inv)
        assert.is_true(aff.is_identity(inv))
    end)
    it("invert translate roundtrip", function()
        local m = aff.translate(3, -5)
        local inv = aff.invert(m)
        assert.is_not_nil(inv)
        local pt = aff.apply_to_point(inv, aff.apply_to_point(m, {x=1, y=2}))
        assert.is_true(pt_approx(pt, {x=1, y=2}))
    end)
    it("invert singular", function()
        assert.is_nil(aff.invert(aff.scale(0, 1)))
    end)
    it("is_identity", function()
        assert.is_true(aff.is_identity(aff.identity()))
        assert.is_false(aff.is_identity(aff.translate(1, 0)))
    end)
    it("is_translation_only", function()
        assert.is_true(aff.is_translation_only(aff.translate(5, -3)))
        assert.is_false(aff.is_translation_only(aff.rotate(0.1)))
    end)
    it("apply_to_vector excludes translation", function()
        local v = aff.apply_to_vector(aff.translate(100, 100), {x=1, y=0})
        assert.is_true(pt_approx(v, {x=1, y=0}))
    end)
    it("rotate_around pivot", function()
        local m = aff.rotate_around(trig.PI / 2, 1, 0)
        local pt = aff.apply_to_point(m, {x=1, y=0})
        assert.is_true(approx(pt.x, 1) and approx(pt.y, 0))
    end)
end)
