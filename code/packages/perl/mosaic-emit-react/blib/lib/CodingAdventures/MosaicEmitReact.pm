package CodingAdventures::MosaicEmitReact;

# ============================================================================
# CodingAdventures::MosaicEmitReact — Emits a TypeScript React functional component (.tsx)
# ============================================================================
#
# This is the React backend for the Mosaic compiler pipeline:
#
#   Source → Lexer → Parser → Analyzer → MosaicIR → VM → **ReactRenderer** → .tsx
#
# The renderer implements the duck-typed renderer interface required by
# CodingAdventures::MosaicVm. It builds JSX output using a string stack:
# each open node pushes a buffer, each close node pops it and appends the
# resulting JSX element string to the parent's buffer.
#
# Output file structure
# ---------------------
#
#   // AUTO-GENERATED by Mosaic Perl compiler. Do not edit.
#   import React from "react";
#   interface ComponentNameProps { ... }
#   export function ComponentName({ ... }: ComponentNameProps): React.ReactElement {
#     return (
#       <root-element ...>
#         ...
#       </root-element>
#     );
#   }
#
# Primitive node → JSX element mapping
# -------------------------------------
#
#   Column  → <div style={{ display: 'flex', flexDirection: 'column' }}>
#   Row     → <div style={{ display: 'flex', flexDirection: 'row' }}>
#   Box     → <div style={{ position: 'relative' }}>
#   Stack   → <div style={{ position: 'relative' }}>
#   Text    → <span>
#   Image   → <img ... /> (self-closing)
#   Spacer  → <div style={{ flex: 1 }} />
#   Divider → <hr ... /> (self-closing)
#   Scroll  → <div style={{ overflow: 'auto' }}>
#   Icon    → <span>
#   other   → <Component ...> (component-type node)
#
# Property mapping
# ----------------
#
#   padding, margin, etc. → inline style (camelCase)
#   content (Text)        → JSX children
#   source (Image)        → src attribute
#   a11y-label            → aria-label
#   background            → backgroundColor
#   Colors                → rgba(r, g, b, alpha/255)
#   Dimensions            → Npx (dp/sp) or N% (percent)
#
# Public API
# ----------
#
#   my $result = CodingAdventures::MosaicEmitReact->emit($component);
#
# Returns a hashref: { filename => "Name.tsx", content => "..." }

use strict;
use warnings;

use CodingAdventures::MosaicVm;
use CodingAdventures::MosaicAnalyzer;

our $VERSION = '0.01';

# ============================================================================
# Primitive node registry
# ============================================================================

my %PRIMITIVE_TAG = (
    Column  => 'div',
    Row     => 'div',
    Box     => 'div',
    Stack   => 'div',
    Text    => 'span',
    Image   => 'img',
    Spacer  => 'div',
    Divider => 'hr',
    Scroll  => 'div',
    Icon    => 'span',
);

my %SELF_CLOSING = ( Image => 1, Divider => 1, Spacer => 1 );

# Base styles for each primitive
my %BASE_STYLE = (
    Column  => "display: 'flex', flexDirection: 'column'",
    Row     => "display: 'flex', flexDirection: 'row'",
    Box     => "position: 'relative'",
    Stack   => "position: 'relative'",
    Scroll  => "overflow: 'auto'",
    Spacer  => "flex: 1",
);

# ============================================================================
# Public API
# ============================================================================

sub emit {
    my ($class, $component) = @_;

    my $renderer = CodingAdventures::MosaicEmitReact::Renderer->new();
    my ($result, $err) = CodingAdventures::MosaicVm->run($component, $renderer);
    die "MosaicEmitReact: $err" if $err;
    return $result;
}

# ============================================================================
# Renderer class
# ============================================================================

package CodingAdventures::MosaicEmitReact::Renderer;

sub new {
    bless {
        component_name => '',
        slots          => [],
        stack          => [],
    }, shift;
}

sub begin_component {
    my ($self, $name, $slots) = @_;
    $self->{component_name} = $name;
    $self->{slots}          = $slots;
    $self->{stack}          = [ { kind => 'component', lines => [] } ];
}

sub end_component { }

sub begin_node {
    my ($self, $tag, $is_primitive, $props, $ctx) = @_;

    my $frame = $self->_build_frame($tag, $is_primitive, $props);
    push @{ $self->{stack} }, $frame;
}

sub end_node {
    my ($self, $tag) = @_;
    my $frame  = pop @{ $self->{stack} };
    my $jsx    = $self->_build_jsx($frame);
    $self->_append($jsx);
}

sub render_slot_child {
    my ($self, $slot_name, $slot_type, $ctx) = @_;
    $self->_append("{$slot_name}");
}

sub begin_when {
    my ($self, $slot_name, $ctx) = @_;
    push @{ $self->{stack} }, { kind => 'when', slot_name => $slot_name, lines => [] };
}

sub end_when {
    my ($self) = @_;
    my $frame = pop @{ $self->{stack} };
    my $inner = join("\n", @{ $frame->{lines} });
    my $jsx   = "{$frame->{slot_name} && (\n$inner\n)}";
    $self->_append($jsx);
}

sub begin_each {
    my ($self, $slot_name, $item_name, $element_type, $ctx) = @_;
    push @{ $self->{stack} },
        { kind => 'each', slot_name => $slot_name, item_name => $item_name, lines => [] };
}

sub end_each {
    my ($self) = @_;
    my $frame = pop @{ $self->{stack} };
    my $inner = join("\n", @{ $frame->{lines} });
    my $jsx   = "{$frame->{slot_name}.map(($frame->{item_name}) => (\n$inner\n))}";
    $self->_append($jsx);
}

sub emit {
    my ($self) = @_;
    my $content = $self->_build_file();
    return { filename => "$self->{component_name}.tsx", content => $content };
}

# ============================================================================
# Internal helpers
# ============================================================================

sub _build_frame {
    my ($self, $tag, $is_primitive, $props) = @_;

    my $jsx_tag = $is_primitive ? ($PRIMITIVE_TAG{$tag} // 'div') : $tag;
    my $self_closing = exists $SELF_CLOSING{$tag} ? 1 : 0;

    my %styles;
    my @attrs;
    my $text_content;

    # Base styles
    if ($is_primitive && exists $BASE_STYLE{$tag}) {
        # Will be merged with property styles — store base separately
    }

    for my $prop (@$props) {
        my $name  = $prop->{name};
        my $value = $prop->{value};

        # Text content
        if ($name eq 'content' && $tag eq 'Text') {
            my $v = $self->_value_to_jsx($value);
            # Slot refs and expressions must be wrapped in {} in JSX
            if ($value->{kind} eq 'slot_ref' || $value->{kind} eq 'number' || $value->{kind} eq 'bool') {
                $text_content = "{$v}";
            } else {
                $text_content = $v;
            }
            next;
        }
        # Image source
        if ($name eq 'source' && $tag eq 'Image') {
            my $v = $self->_value_to_jsx($value);
            push @attrs, "src={$v}";
            next;
        }
        # ARIA attributes
        if ($name eq 'a11y-label') {
            my $v = $self->_value_to_jsx($value);
            push @attrs, "aria-label={$v}";
            next;
        }
        if ($name eq 'a11y-hidden') {
            push @attrs, 'aria-hidden="true"';
            next;
        }

        # Regular style property
        my $css_prop = $self->_to_camel($name);
        $styles{$css_prop} = $self->_value_to_style($value);
    }

    return {
        kind         => 'node',
        tag          => $tag,
        jsx_tag      => $jsx_tag,
        base_style   => $is_primitive ? ($BASE_STYLE{$tag} // '') : '',
        styles       => \%styles,
        attrs        => \@attrs,
        text_content => $text_content,
        self_closing => $self_closing,
        lines        => [],
    };
}

sub _build_jsx {
    my ($self, $frame) = @_;

    my $jsx_tag = $frame->{jsx_tag};

    # Build style string
    my @style_parts;
    push @style_parts, $frame->{base_style} if $frame->{base_style};
    for my $k (sort keys %{ $frame->{styles} }) {
        push @style_parts, "$k: $frame->{styles}{$k}";
    }

    my @attr_strs = @{ $frame->{attrs} };
    push @attr_strs, "style={{ " . join(', ', @style_parts) . " }}"
        if @style_parts;

    my $attrs_str = @attr_strs ? ' ' . join(' ', @attr_strs) : '';

    if ($frame->{self_closing}) {
        return "<$jsx_tag$attrs_str />";
    }

    my $inner = '';
    if (defined $frame->{text_content}) {
        $inner = $frame->{text_content};
    } elsif (@{ $frame->{lines} }) {
        $inner = "\n" . join("\n", @{ $frame->{lines} }) . "\n";
    }

    return "<$jsx_tag$attrs_str>$inner</$jsx_tag>";
}

sub _build_file {
    my ($self) = @_;

    my $name  = $self->{component_name};
    my $slots = $self->{slots};

    # Props interface
    my $props_interface = $self->_build_props_interface($name, $slots);

    # Destructured params
    my @param_names = map { $_->{name} } @$slots;
    my $params = @param_names ? '{ ' . join(', ', @param_names) . ' }' : '{}';

    # Root JSX from component frame
    my $root_jsx = join("\n", @{ $self->{stack}[0]{lines} });

    return <<TSX;
// AUTO-GENERATED by Mosaic Perl compiler. Do not edit.
import React from "react";

$props_interface
export function $name($params: ${name}Props): React.ReactElement {
  return (
    $root_jsx
  );
}
TSX
}

sub _build_props_interface {
    my ($self, $name, $slots) = @_;
    my @lines = ("interface ${name}Props {");
    for my $slot (@$slots) {
        my $ts_type = $self->_type_to_ts($slot->{type});
        my $opt     = $slot->{required} ? '' : '?';
        push @lines, "  $slot->{name}$opt: $ts_type;";
    }
    push @lines, "}";
    return join("\n", @lines);
}

sub _type_to_ts {
    my ($self, $type) = @_;
    my %MAP = (
        text   => 'string',
        number => 'number',
        bool   => 'boolean',
        image  => 'string',
        color  => 'string',
        node   => 'React.ReactNode',
    );
    my $kind = $type->{kind};
    return $MAP{$kind} if exists $MAP{$kind};
    return "React.ReactNode"          if $kind eq 'component';
    return $self->_type_to_ts($type->{element_type}) . '[]' if $kind eq 'list';
    return 'unknown';
}

sub _value_to_jsx {
    my ($self, $value) = @_;
    my $kind = $value->{kind};

    return $value->{value}                      if $kind eq 'string';
    return $value->{value}                      if $kind eq 'number';
    return $value->{value} ? 'true' : 'false'   if $kind eq 'bool';
    return $value->{value}                      if $kind eq 'string' && $kind eq 'ident'; # won't hit
    return $value->{value}                      if $kind eq 'ident';
    return $value->{slot_name}                  if $kind eq 'slot_ref';
    return $self->_color_to_rgba($value)         if $kind eq 'color';
    return $self->_dim_to_css($value->{value}, $value->{unit}) if $kind eq 'dimension';
    return "$value->{namespace}.$value->{member}" if $kind eq 'enum';
    return '';
}

sub _value_to_style {
    my ($self, $value) = @_;
    my $kind = $value->{kind};

    if ($kind eq 'string') {
        my $v = $value->{value};
        $v =~ s/^"(.*)"$/$1/;  # strip outer quotes
        return "'$v'";
    }
    return $value->{value}                    if $kind eq 'number';
    return $value->{value} ? 'true' : 'false' if $kind eq 'bool';
    return $value->{slot_name}                if $kind eq 'slot_ref';
    return "'" . $self->_color_to_rgba($value) . "'" if $kind eq 'color';
    if ($kind eq 'dimension') {
        my $css = $self->_dim_to_css($value->{value}, $value->{unit});
        return "'$css'";
    }
    return "'$value->{value}'" if $kind eq 'ident';
    return "'" . $value->{namespace} . '.' . $value->{member} . "'" if $kind eq 'enum';
    return "'unknown'";
}

sub _color_to_rgba {
    my ($self, $v) = @_;
    my $alpha = sprintf("%.4g", $v->{a} / 255);
    return "rgba($v->{r}, $v->{g}, $v->{b}, $alpha)";
}

sub _dim_to_css {
    my ($self, $value, $unit) = @_;
    return "${value}%" if $unit eq '%';
    return "${value}px";  # dp and sp both map to px
}

# Convert a kebab-case property name to camelCase.
sub _to_camel {
    my ($self, $name) = @_;
    $name =~ s/-([a-z])/uc($1)/ge;
    return $name;
}

# Append a string to the innermost frame's lines array.
sub _append {
    my ($self, $str) = @_;
    push @{ $self->{stack}[-1]{lines} }, $str;
}

1;

__END__

=head1 NAME

CodingAdventures::MosaicEmitReact - Emits TypeScript React functional components from Mosaic IR

=head1 SYNOPSIS

    use CodingAdventures::MosaicEmitReact;
    use CodingAdventures::MosaicAnalyzer;

    my ($component, $err) = CodingAdventures::MosaicAnalyzer->analyze($source);
    die $err if $err;

    my $result = CodingAdventures::MosaicEmitReact->emit($component);
    print $result->{filename};  # "MyComponent.tsx"
    print $result->{content};

=head1 DESCRIPTION

Port of the TypeScript MosaicReactRenderer to Perl. Drives the MosaicVM with
a React-specific renderer that emits TypeScript JSX source code.

=head1 METHODS

=head2 emit($component)

Emit a React TSX file from the given MosaicIR component hashref.
Returns a hashref with keys C<filename> and C<content>.

