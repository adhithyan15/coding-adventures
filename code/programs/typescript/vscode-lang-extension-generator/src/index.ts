/**
 * The DAP adapter registration.  VS Code spawns the adapter binary
 * directly per debug session — no LanguageClient-style wrapper needed,
 * because VS Code handles all the protocol plumbing.  We just supply
 * the executable path.
 */
export function buildDapTs(opts: GeneratorOptions): string {
  // Same defense-in-depth as buildLspTs: all user values are embedded
  // via JSON.stringify.  The class-name and inline doc-comment uses of
  // `id` and `dapBinary` are guarded by their validators
  // (LANGUAGE_ID_RE and BINARY_NAME_RE).
  const idLit = JSON.stringify(opts.languageId);
  const nameLit = JSON.stringify(opts.languageName);
  const binLit = JSON.stringify(opts.dapBinary);
  const id = opts.languageId; // safe per LANGUAGE_ID_RE
  const dapBinary = opts.dapBinary; // safe per BINARY_NAME_RE
  const cls = pascalCase(id);
  return `// Generated by vscode-lang-extension-generator (LS04).
// Edit at your own risk; re-running the generator overwrites this file.

import * as vscode from "vscode";

/**
 * Factory that VS Code calls each time the user starts a debug session
 * with \`"type": "${id}"\`.  We resolve the adapter binary from the
 * "${id}.adapterPath" setting (defaulting to "${dapBinary}" on
 * PATH) and hand back a DebugAdapterExecutable — VS Code spawns the
 * subprocess and proxies DAP messages.
 */
class ${cls}DebugAdapterFactory
  implements vscode.DebugAdapterDescriptorFactory
{
  createDebugAdapterDescriptor(
    _session: vscode.DebugSession,
  ): vscode.ProviderResult<vscode.DebugAdapterDescriptor> {
    const config = vscode.workspace.getConfiguration(${idLit});
    const adapterPath = config.get<string>("adapterPath", ${binLit});
    return new vscode.DebugAdapterExecutable(adapterPath, []);
  }
}

/** Wire the factory into VS Code's debug subsystem. */
export function registerDebugAdapter(context: vscode.ExtensionContext): void {
  context.subscriptions.push(
    vscode.debug.registerDebugAdapterDescriptorFactory(
      ${idLit},
      new ${cls}DebugAdapterFactory(),
    ),
  );
}

/** Reference the language name so it shows up in the bundle metadata. */
export const LANGUAGE_NAME = ${nameLit};
`;
}

/**
 * "twig" -> "Twig", "my-lang" -> "MyLang".  Used for class names in
 * the generated dap.ts; class names with hyphens aren't valid TS.
 */
function pascalCase(id: string): string {
  return id
    .split("-")
    .map((p) => (p.length > 0 ? p[0].toUpperCase() + p.slice(1) : ""))
    .join("");
}

