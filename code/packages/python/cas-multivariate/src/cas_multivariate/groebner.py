"""Buchberger's algorithm for Gröbner basis computation.

Background
----------
An *ideal* I = ⟨f₁, …, fₖ⟩ in the polynomial ring Q[x₁, …, xₙ] is the
set of all polynomial combinations  c₁f₁ + … + cₖfₖ.  Deciding whether
a polynomial belongs to an ideal (the *ideal membership problem*) is
the fundamental question in algebraic geometry.

A **Gröbner basis** is a special generating set ``G`` for the ideal such
that:  f ∈ I  iff  the remainder of f on division by G is zero.
This makes membership decidable by the reduction algorithm in reduce.py.

**Buchberger's algorithm** (1965) computes a Gröbner basis from any
generating set:

1. Start with  G = {f₁, …, fₖ}.
2. For each pair (fᵢ, fⱼ), compute the S-polynomial S(fᵢ, fⱼ).
3. Reduce S(fᵢ, fⱼ) by G.
4. If the remainder r ≠ 0, add r to G and generate new pairs.
5. Repeat until all S-polynomials reduce to zero.

The final G is a Gröbner basis (for the ideal generated by the input).
Applying **inter-reduction** (each basis element reduced by all others)
yields the unique *reduced* Gröbner basis.

Termination
-----------
Buchberger always terminates because each new basis element r strictly
increases the set of leading monomials, and the set of all monomials is
*Noetherian* (has no infinite strictly ascending chains) by Dickson's
lemma.

Complexity warning
------------------
In the worst case (e.g., dense systems, high degree) Buchberger can be
doubly exponential.  We add a safety cap: if the basis grows beyond 50
polynomials, we raise ``GrobnerError`` so that the VM falls through to
unevaluated gracefully.

Usage::

    from fractions import Fraction
    from cas_multivariate.polynomial import MPoly
    from cas_multivariate.groebner import buchberger

    # f₁ = x + y - 1,  f₂ = x - y
    f1 = MPoly({(1,0): Fraction(1), (0,1): Fraction(1), (0,0): Fraction(-1)}, 2)
    f2 = MPoly({(1,0): Fraction(1), (0,1): Fraction(-1)}, 2)
    G = buchberger([f1, f2], order="lex")
    # G contains the reduced lex Gröbner basis — used to solve the system.
"""

from __future__ import annotations

from cas_multivariate.polynomial import MPoly
from cas_multivariate.reduce import reduce_poly, s_poly

# ---------------------------------------------------------------------------
# Safety limits
# ---------------------------------------------------------------------------

#: Maximum number of basis polynomials before we give up.
#: Real Gröbner bases for small systems are usually ≤ 10 elements.
_MAX_BASIS_SIZE: int = 50

#: Maximum total degree of any single polynomial we will track.
_MAX_DEGREE: int = 8


# ---------------------------------------------------------------------------
# Exception
# ---------------------------------------------------------------------------


class GrobnerError(Exception):
    """Raised when the basis exceeds our safety limits.

    The VM handler catches this and returns the expression unevaluated.
    """


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------


def _make_monic(p: MPoly, order: str) -> MPoly:
    """Return p / lc(p) — a polynomial whose leading coefficient is 1.

    Monic polynomials make the reduced basis canonical (unique).

    Example::

        p = MPoly({(2, 0): Fraction(3), (0, 0): Fraction(-6)}, 2)   # 3x^2 - 6
        _make_monic(p, "grlex")   # → x^2 - 2
    """
    if p.is_zero():
        return p
    lc = p.lc(order)
    return p.scale(1 / lc)


def _inter_reduce(G: list[MPoly], order: str) -> list[MPoly]:
    """Apply inter-reduction to ``G`` and return the minimal reduced basis.

    Inter-reduction:
    1. Remove any element whose leading monomial is divisible by the
       leading monomial of another element (minimality condition).
    2. Reduce each remaining element by all others to eliminate
       redundant monomials (fullness condition).
    3. Make each element monic.

    The result is the unique **reduced Gröbner basis** for the same ideal.

    Example::

        # If G = [x + y, x + y + 1, y], inter-reduce removes the first
        # because its leading term (x) is divisible by itself in the
        # presence of a "better" generator.  Actual result depends on
        # the ordering.
    """
    if not G:
        return []

    # Step 1: Minimality — remove elements with reducible leading monomials.
    # We keep g_i iff no OTHER g_j (j < i or j > i depending on tie-breaking)
    # has a leading monomial that divides lm(g_i).
    #
    # For the degenerate case where two elements share the same leading monomial,
    # we keep only the first one (earlier index wins).  This is valid because if
    # lm(h) == lm(g), h still divides lm(g), so g is redundant given h.
    from cas_multivariate.monomial import divides

    minimal: list[MPoly] = []
    for i, g in enumerate(G):
        if g.is_zero():
            continue
        lm_g = g.lm(order)
        dominated = False
        for j, h in enumerate(G):
            if i == j or h.is_zero():
                continue
            lm_h = h.lm(order)
            # h dominates g if lm(h) divides lm(g).
            # For equal lm's, j < i means h came first (prefer earlier index).
            if divides(lm_h, lm_g) and (lm_h != lm_g or j < i):
                dominated = True
                break
        if not dominated:
            minimal.append(_make_monic(g, order))

    # Step 2: Full reduction — reduce each element by all others.
    reduced: list[MPoly] = []
    for i, g in enumerate(minimal):
        others = [h for j, h in enumerate(minimal) if j != i]
        r = reduce_poly(g, others, order)
        if not r.is_zero():
            reduced.append(_make_monic(r, order))

    return reduced


# ---------------------------------------------------------------------------
# Buchberger's algorithm
# ---------------------------------------------------------------------------


def buchberger(F: list[MPoly], order: str = "grlex") -> list[MPoly]:
    """Compute a Gröbner basis for the ideal ⟨F⟩ using Buchberger's algorithm.

    Parameters
    ----------
    F:
        A list of generators for the ideal.  May be empty or contain zero
        polynomials (which are filtered out).
    order:
        Monomial ordering for all operations.  Use ``"grlex"`` (default)
        for Buchberger; use ``"lex"`` for a basis suitable for
        back-substitution (see :mod:`~cas_multivariate.solve`).

    Returns
    -------
    A reduced Gröbner basis for ⟨F⟩ as a list of monic polynomials.
    Returns ``[]`` for the zero ideal (F = [] or F = [0]).

    Raises
    ------
    GrobnerError
        If the basis exceeds :data:`_MAX_BASIS_SIZE`.

    Algorithm trace for [x + y - 1, x - y]:
    -----------------------------------------
    G₀ = [x+y-1, x-y]
    Pair (0,1): S = (x+y-1) - (x-y) = 2y-1
    Reduce 2y-1 by G — remainder 2y-1 (not zero)
    G₁ = [x+y-1, x-y, 2y-1]
    New pairs: (0,2) and (1,2)
    S(x+y-1, 2y-1): ... reduces to 0
    S(x-y, 2y-1): ... reduces to 0
    Done.  After inter-reduction: [x - 1/2, y - 1/2]
    """
    # Filter out zero polynomials.
    G: list[MPoly] = [f for f in F if not f.is_zero()]
    if not G:
        return []

    # Sanity-check degrees before starting.
    for p in G:
        if p.total_degree() > _MAX_DEGREE:
            raise GrobnerError(
                f"Input polynomial has total degree {p.total_degree()} "
                f"which exceeds the safety cap {_MAX_DEGREE}."
            )

    # Set of unprocessed pairs (i, j) with i < j.
    pairs: set[tuple[int, int]] = {
        (i, j) for i in range(len(G)) for j in range(i + 1, len(G))
    }

    while pairs:
        # Pop any pair (order doesn't affect correctness, only speed).
        i, j = pairs.pop()

        sp = s_poly(G[i], G[j], order)
        r = reduce_poly(sp, G, order)

        if r.is_zero():
            continue  # This pair is already taken care of — good.

        # Check safety limits.
        if len(G) >= _MAX_BASIS_SIZE:
            raise GrobnerError(
                f"Gröbner basis grew beyond {_MAX_BASIS_SIZE} elements.  "
                "The system is likely too complex for this implementation."
            )
        if r.total_degree() > _MAX_DEGREE:
            raise GrobnerError(
                f"New basis element has total degree {r.total_degree()} "
                f"which exceeds the safety cap {_MAX_DEGREE}."
            )

        # Add r to the basis and register new pairs.
        new_idx = len(G)
        new_pairs: set[tuple[int, int]] = {(k, new_idx) for k in range(new_idx)}
        pairs |= new_pairs
        G.append(r)

    # Apply inter-reduction to get the canonical reduced Gröbner basis.
    return _inter_reduce(G, order)
