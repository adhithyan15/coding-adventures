-- Tests for coding_adventures.device_driver_framework
-- Coverage target: 95%+

local DDF = require("coding_adventures.device_driver_framework")

-- Helper: make a zero-filled block string
local function zero_block(size)
  local t = {}
  for i = 1, size do t[i] = "\0" end
  return table.concat(t)
end

local function fill_block(char, size)
  local t = {}
  for i = 1, size do t[i] = char end
  return table.concat(t)
end

