/**
 * reducer.ts — Pure state reducer for the todo app.
 *
 * A reducer is a pure function: (state, action) → newState. It takes the
 * current application state and an action describing what happened, then
 * returns a NEW state object reflecting the change. The reducer never
 * mutates the existing state — it creates fresh objects via spread syntax.
 *
 * === Why immutability? ===
 *
 * React (via useSyncExternalStore) compares state snapshots by reference
 * (Object.is). If the reducer mutated the existing state object, React
 * wouldn't detect the change and wouldn't re-render. Returning a new
 * object with `{ ...state, tasks: [...] }` creates a new reference,
 * which React sees as "state changed, re-render."
 *
 * === State shape ===
 *
 * The entire app state is a single object:
 *   {
 *     tasks: Task[];               — persisted task records
 *     views: SavedView[];          — persisted named views
 *     calendars: CalendarSettings[]; — persisted calendar configs
 *     activeViewId: string;        — which view tab is currently open
 *   }
 *
 * ONLY PERSISTED data lives in the store. Ephemeral UI state (search input
 * contents, hover state, etc.) stays in component-local useState.
 */

import type { Action } from "@coding-adventures/store";
import type { Task, TaskStatus, Project } from "./types.js";
import type { SavedView } from "./views.js";
import type { CalendarSettings } from "./calendar-settings.js";
import type { GraphEdge } from "./graph.js";
import { buildGraph, wouldCreateCycle, newEdgeId } from "./graph.js";
import {
  TASK_CREATE,
  TASK_UPDATE,
  TASK_DELETE,
  TASK_TOGGLE_STATUS,
  TASK_SET_STATUS,
  STATE_LOAD,
  TASK_CLEAR_COMPLETED,
  VIEW_UPSERT,
  VIEW_SET_ACTIVE,
  CALENDAR_UPSERT,
  PROJECT_UPSERT,
  EDGE_ADD,
  EDGE_REMOVE,
} from "./actions.js";

// ── State ─────────────────────────────────────────────────────────────────
//
// AppState is a plain serializable object. Every field is a JSON-safe
// primitive or array. Class instances (like LabeledDirectedGraph) are never
// stored here — they are derived/rebuilt from `edges` on demand.

export interface AppState {
  tasks: Task[];
  views: SavedView[];
  calendars: CalendarSettings[];
  /**
   * All persisted projects. At least one project (the "default" built-in)
   * always exists after the first visit or v3→v4 migration.
   */
  projects: Project[];
  /**
   * All persisted graph edges. Flat serializable records that encode the
   * DAG topology. The in-memory LabeledDirectedGraph is rebuilt from this
   * array whenever graph queries are needed (via buildGraph()).
   *
   * In V1, all edges have label: "contains" and represent project→task or
   * project→subproject containment relationships.
   */
  edges: GraphEdge[];
  /**
   * The id of the currently displayed view.
   * Set on first load to the "All Tasks" view id, then updated whenever
   * the user clicks a different view tab.
   */
  activeViewId: string;
}

// ── Status cycle ──────────────────────────────────────────────────────────
//
// The toggle action cycles through statuses in order:
//   todo → in-progress → done → todo
//
// This function encapsulates that cycle so the reducer doesn't need
// a multi-branch if/else.

function nextStatus(current: TaskStatus): TaskStatus {
  switch (current) {
    case "todo":        return "in-progress";
    case "in-progress": return "done";
    case "done":        return "todo";
  }
}

// ── Reducer ───────────────────────────────────────────────────────────────

export function reducer(state: AppState, action: Action): AppState {
  switch (action.type) {

    // ── TASK_CREATE ──────────────────────────────────────────────────────
    //
    // Use the ID pre-generated by createTaskAction (in actions.ts). This
    // allows the audit middleware to record the entity ID BEFORE the reducer
    // runs. Falls back to crypto.randomUUID() for raw action objects
    // constructed in tests or outside of createTaskAction.
    //
    // SortOrder uses Date.now() so new items appear at the bottom of lists.
    case TASK_CREATE: {
      const now = Date.now();
      const newTask: Task = {
        // Use pre-generated ID from action (generated by createTaskAction).
        // Fall back to crypto.randomUUID() for raw action objects used in tests.
        id: (action.id as string | undefined) ?? crypto.randomUUID(),
        title: action.title as string,
        description: (action.description as string) || "",
        status: "todo",
        priority: (action.priority as Task["priority"]) || "medium",
        category: (action.category as string) || "",
        dueDate: (action.dueDate as string | null) || null,
        dueTime: (action.dueTime as string | null) || null,
        createdAt: now,
        updatedAt: now,
        completedAt: null,
        sortOrder: now,
      };
      // Atomically create a "contains" edge from the project to this task.
      // A brand-new task node has no outgoing edges, so it can never form a
      // cycle — the cycle check is skipped here for performance.
      const projectId = (action.projectId as string | undefined) ?? "default";
      const newEdge: GraphEdge = {
        id: newEdgeId(),
        fromId: projectId,
        toId: newTask.id,
        label: "contains",
        createdAt: now,
      };
      return {
        ...state,
        tasks: [...state.tasks, newTask],
        edges: [...state.edges, newEdge],
      };
    }

    // ── TASK_UPDATE ──────────────────────────────────────────────────────
    //
    // Find the task by ID, apply the patch (partial update), bump updatedAt.
    // If the ID doesn't match, the item passes through unchanged.
    case TASK_UPDATE: {
      const taskId = action.taskId as string;
      const patch = action.patch as Partial<Task>;
      return {
        ...state,
        tasks: state.tasks.map((task) =>
          task.id === taskId
            ? { ...task, ...patch, updatedAt: Date.now() }
            : task,
        ),
      };
    }

    // ── TASK_DELETE ──────────────────────────────────────────────────────
    //
    // Remove by ID. Cascade-remove all edges that reference this task as
    // either the source (fromId) or the target (toId) to keep the graph
    // consistent. filter() returns new arrays, satisfying immutability.
    case TASK_DELETE: {
      const taskId = action.taskId as string;
      return {
        ...state,
        tasks: state.tasks.filter((task) => task.id !== taskId),
        edges: state.edges.filter(
          (e) => e.fromId !== taskId && e.toId !== taskId,
        ),
      };
    }

    // ── TASK_TOGGLE_STATUS ───────────────────────────────────────────────
    //
    // Cycle: todo → in-progress → done → todo.
    // When transitioning TO "done", set completedAt. When leaving "done",
    // clear completedAt.
    case TASK_TOGGLE_STATUS: {
      const taskId = action.taskId as string;
      return {
        ...state,
        tasks: state.tasks.map((task) => {
          if (task.id !== taskId) return task;
          const newStatus = nextStatus(task.status);
          return {
            ...task,
            status: newStatus,
            completedAt: newStatus === "done" ? Date.now() : null,
            updatedAt: Date.now(),
          };
        }),
      };
    }

    // ── TASK_SET_STATUS ──────────────────────────────────────────────────
    //
    // Explicit status set (not a cycle). Used by dropdown or drag-and-drop.
    case TASK_SET_STATUS: {
      const taskId = action.taskId as string;
      const status = action.status as TaskStatus;
      return {
        ...state,
        tasks: state.tasks.map((task) => {
          if (task.id !== taskId) return task;
          return {
            ...task,
            status,
            completedAt: status === "done" ? Date.now() : null,
            updatedAt: Date.now(),
          };
        }),
      };
    }

    // ── TASK_CLEAR_COMPLETED ─────────────────────────────────────────────
    //
    // Remove all tasks with status "done". A cleanup operation.
    case TASK_CLEAR_COMPLETED: {
      return {
        ...state,
        tasks: state.tasks.filter((task) => task.status !== "done"),
      };
    }

    // ── VIEW_UPSERT ──────────────────────────────────────────────────────
    //
    // Add a new view or replace an existing one (matched by id).
    // Views are sorted by sortOrder for display.
    case VIEW_UPSERT: {
      const view = action.view as SavedView;
      const existing = state.views.findIndex((v) => v.id === view.id);
      const newViews =
        existing >= 0
          ? state.views.map((v) => (v.id === view.id ? view : v))
          : [...state.views, view];
      return { ...state, views: newViews };
    }

    // ── VIEW_SET_ACTIVE ──────────────────────────────────────────────────
    //
    // Update the currently displayed view. Components read activeViewId
    // to know which view tab is selected.
    case VIEW_SET_ACTIVE: {
      const viewId = action.viewId as string;
      return { ...state, activeViewId: viewId };
    }

    // ── CALENDAR_UPSERT ──────────────────────────────────────────────────
    //
    // Add a new CalendarSettings or replace an existing one (matched by id).
    case CALENDAR_UPSERT: {
      const calendar = action.calendar as CalendarSettings;
      const existing = state.calendars.findIndex((c) => c.id === calendar.id);
      const newCalendars =
        existing >= 0
          ? state.calendars.map((c) => (c.id === calendar.id ? calendar : c))
          : [...state.calendars, calendar];
      return { ...state, calendars: newCalendars };
    }

    // ── PROJECT_UPSERT ───────────────────────────────────────────────────
    //
    // Add a new project or replace an existing one (matched by id).
    // Same upsert pattern as VIEW_UPSERT and CALENDAR_UPSERT.
    case PROJECT_UPSERT: {
      const project = action.project as Project;
      const existing = state.projects.findIndex((p) => p.id === project.id);
      const newProjects =
        existing >= 0
          ? state.projects.map((p) => (p.id === project.id ? project : p))
          : [...state.projects, project];
      return { ...state, projects: newProjects };
    }

    // ── EDGE_ADD ─────────────────────────────────────────────────────────
    //
    // Add a directed edge to the graph. Enforce the DAG invariant: if the
    // edge would create a cycle, return state unchanged (silent no-op).
    //
    // The cycle check builds a temporary in-memory graph from state.edges
    // and calls wouldCreateCycle(). This is O(V + E) per add — acceptable
    // for V1 dataset sizes. Optimize with incremental graph updates later.
    case EDGE_ADD: {
      const edge = action.edge as GraphEdge;
      // Don't add duplicate edges (same fromId + toId + label)
      const isDuplicate = state.edges.some(
        (e) => e.fromId === edge.fromId && e.toId === edge.toId && e.label === edge.label,
      );
      if (isDuplicate) return state;
      // Cycle check using the current in-memory graph
      const graph = buildGraph(state.projects, state.tasks, state.edges);
      if (wouldCreateCycle(graph, edge.fromId, edge.toId)) return state;
      return { ...state, edges: [...state.edges, edge] };
    }

    // ── EDGE_REMOVE ──────────────────────────────────────────────────────
    //
    // Remove an edge by its stable id. Idempotent: no-op if id not found.
    case EDGE_REMOVE: {
      const edgeId = action.edgeId as string;
      return {
        ...state,
        edges: state.edges.filter((e) => e.id !== edgeId),
      };
    }

    // ── STATE_LOAD ───────────────────────────────────────────────────────
    //
    // Replace the entire state with data from IndexedDB. Called once on
    // startup. No merge logic — IndexedDB is the source of truth.
    // Projects and edges default to [] for backward compat with callers
    // that haven't been updated to pass the new fields yet.
    case STATE_LOAD: {
      const tasks = action.tasks as Task[];
      const views = (action.views as SavedView[]) || [];
      const calendars = (action.calendars as CalendarSettings[]) || [];
      const activeViewId = (action.activeViewId as string) || "";
      const projects = (action.projects as Project[]) || [];
      const edges = (action.edges as GraphEdge[]) || [];
      return { tasks, views, calendars, activeViewId, projects, edges };
    }

    // ── Unknown action ───────────────────────────────────────────────────
    //
    // Return state unchanged. Unknown actions must be no-ops because
    // middleware may dispatch internal actions the reducer doesn't handle.
    default:
      return state;
  }
}
