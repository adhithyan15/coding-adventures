describe("SimulatedSerial", function()
  it("new creates serial with defaults", function()
    local s = DDF.SimulatedSerial.new()
    assert.are.equal(s.name, "serial0")
    assert.are.equal(s.device_type, DDF.TYPE_CHARACTER)
    assert.are.equal(s.major, 4)
    assert.are.equal(s.minor, 0)
    assert.are.equal(s.baud_rate, 9600)
    assert.is_false(s.initialized)
  end)

  it("initialize, open, close", function()
    local s = DDF.SimulatedSerial.new()
    local _, s2 = s:initialize()
    assert.is_true(s2.initialized)
    local st1, s3 = s2:open()
    assert.are.equal(st1, "ok")
    assert.are.equal(s3.open_count, 1)
    local st2, s4 = s3:close()
    assert.are.equal(st2, "ok")
    assert.are.equal(s4.open_count, 0)
  end)

  it("open before initialize returns error", function()
    local s = DDF.SimulatedSerial.new()
    local st, _ = s:open()
    assert.are.equal(st, "not_initialized")
  end)

  it("close when not open returns error", function()
    local s = DDF.SimulatedSerial.new()
    local _, s2 = s:initialize()
    local st, _ = s2:close()
    assert.are.equal(st, "not_open")
  end)

  it("write and tx_contents", function()
    local s = DDF.SimulatedSerial.new()
    local _, s2 = s:initialize()
    local st, s3, n = s2:write("hello")
    assert.are.equal(st, "ok")
    assert.are.equal(n, 5)
    assert.are.equal(s3:tx_contents(), "hello")
  end)

  it("inject_rx and read", function()
    local s = DDF.SimulatedSerial.new()
    local _, s2 = s:initialize()
    local s3 = s2:inject_rx("world")
    local st, s4, data = s3:read(10)
    assert.are.equal(st, "ok")
    assert.are.equal(data, "world")
    assert.are.equal(#s4.rx_buffer, 0)
  end)

  it("read from empty buffer returns empty", function()
    local s = DDF.SimulatedSerial.new()
    local st, _, data = s:read(4)
    assert.are.equal(st, "empty")
    assert.are.equal(data, "")
  end)

  it("read partial", function()
    local s = DDF.SimulatedSerial.new()
    local s2 = s:inject_rx("hello world")
    local st, s3, data = s2:read(5)
    assert.are.equal(st, "ok")
    assert.are.equal(data, "hello")
    assert.are.equal(#s3.rx_buffer, 6)  -- " world"
  end)

  it("ioctl get_baud_rate", function()
    local s = DDF.SimulatedSerial.new({ baud_rate = 115200 })
    local st, val = s:ioctl("get_baud_rate")
    assert.are.equal(st, "ok")
    assert.are.equal(val, 115200)
  end)

  it("ioctl set_baud_rate", function()
    local s = DDF.SimulatedSerial.new()
    local st, s2 = s:ioctl("set_baud_rate", 115200)
    assert.are.equal(st, "ok")
    assert.are.equal(s2.baud_rate, 115200)
    assert.are.equal(s.baud_rate, 9600)  -- immutable
  end)

  it("ioctl unsupported", function()
    local s = DDF.SimulatedSerial.new()
    local st, _ = s:ioctl("flush", nil)
    assert.are.equal(st, "unsupported")
  end)

  it("multiple writes accumulate in tx buffer", function()
    local s = DDF.SimulatedSerial.new()
    local _, s2, _ = s:write("hel")
    local _, s3, _ = s2:write("lo")
    assert.are.equal(s3:tx_contents(), "hello")
  end)
end)

