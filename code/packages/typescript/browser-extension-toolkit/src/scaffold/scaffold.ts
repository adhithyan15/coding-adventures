const SERVICE_WORKER_TEMPLATE = `/**
 * Background Service Worker
 * =========================
 *
 * This script runs in the background, even when the popup is closed.
 * In Manifest V3, it's a Service Worker — the browser can suspend it
 * when idle and wake it when an event occurs.
 *
 * Key rules for Service Workers:
 * 1. No DOM access (no \`document\`, no \`window\`)
 * 2. No persistent global state (use browser.storage instead)
 * 3. Must register event listeners synchronously at the top level
 * 4. Can be terminated at any time after ~30s of inactivity
 *
 * For this hello-world extension, we just log when the extension
 * is first installed. Real extensions would register listeners for
 * browser events (tab changes, alarms, messages from popups, etc.).
 */

import { getBrowserAPI } from "../lib/browser-api";

// Register the install listener at the top level.
// This MUST be synchronous — the Service Worker specification requires
// event listeners to be registered in the initial execution, not inside
// async callbacks or setTimeout.
try {
  const api = getBrowserAPI();
  api.runtime.onInstalled.addListener((details) => {
    console.log(\`{{name}} installed (reason: \${details.reason})\`);
  });
} catch {
  // Outside extension context (e.g., during testing).
  // Service worker tests should mock the browser API.
  console.log("Service worker loaded outside extension context");
}
`;

